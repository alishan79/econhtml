	<h3>Research Paper Presentations</h3><br>
		<p>Muhammad Naveed and Shemiah Parshad presented their MPhil Economics research in the Economics Faculty Departmental Meeting held on 21st September, 2017, at 10:00 a.m. in TRC-1.<br><br>
Muhammad Naveed presented on "Investment in Early Childhood Health and its Implications for Human Capital Accumulation," and Shemiah Parshad presented on "Spousal Trust Game- Evidence from a Field Experiment in Pakistan."<br><br>

Muhammad Naveed presented a model that analyzes the role of early childhood health investments in human capital accumulation and economic growth. Health status of an individual depends on health investments made during infancy, childhood and inherited nutrition from parents. Better the health status of individuals is more is the accumulation of human capital in an economy. The author has also made the productivity of the human capital accumulation technology the function of early childhood health. The author considers four health financing modes including private financing in which individuals invest themselves, public financing I where government tax all to provide health subsidy to all individuals, public financing II where only high income individuals are taxed to finance the health subsidy and lastly, public financing III where government does not tax anyone but give health subsidy to poor individuals. <br><br>
The research by Shemiah Parshad explored trust dynamics between spouses, strangers of the same gender and strangers of the opposite gender by carrying out a Trust Game (BDM game) with a sample of 41 couples in Lahore, Pakistan. The author tried to ascertain whether any spouses choose the efficiency maximizing strategy. The results from the Trust Game showed that wives are more trusting as compared to husbands while overall, females are low on both trust and trustworthiness.
</p>
		
