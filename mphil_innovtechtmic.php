		<h3>Technology in the Sialkot Gloves Manufacturing Sector</h3>
		<p><b>Researcher: </b>Saba Firdousi (2016)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This paper uses a unique sample of sports glove manufacturers from Sialkot to develop an index of technological sophistication. The data shows that total factor productivity (TFP) and total revenue productivity (TRP) cluster around their mean levels. The medium-tech and high-tech firms seem to have a higher TFP and LP than the low-tech firms. Another interesting result is that, across firms, the level of retained earnings has a negative impact on TFP and TRP.<br><br>
		
