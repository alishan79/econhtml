		<h3>Causes and Consequences of Consanguineous Marriage in Pakistan</h3>
		<p><b>Researcher: </b>Theresa Chaudhry (Lahore School of Economics) and Mushfiq Mobarak (Yale University)</p>
		
		<p>
			Nine (out of Punjab's 35 districts) were randomly selected, comprising 70 sampling clusters. Interviews of just over 1000 households were conducted in October 2009. A total of 4643 pregnancies were reported by 391 first cousin (37.6% of the marriages) and 622 non consanguineous couples interviewed in the study.<br><br>
			Major objectives of the study: to more accurately measure the effects of consanguineous marriage, both negative and positive. On the negative side, the offspring of consanguineous marriages may be at greater risk of disease and infant and childhood death. The survey looks at the childhood morbidity and mortality of children born out of consanguineous and non-consanguineous marriages. On the positive side, there may be greater altruism toward the children of consanguineous unions through the extended family, because there is a greater genetic tie. In addition, given that consanguineous marriage reduces uncertainty about unobserved spousal characteristics, this may provide parents with a greater incentive to "invest" in their children, such as by educating daughters. Finally, are there other socioeconomic benefits of consanguineous marriage? Such as reducing dowry? Increasing empowerment of women? Decreasing violence toward women? The analysis will take an instrumental variables approach, to account for the possible endogeneity of cousin marriage. <br><br>
			Status: "Estimating the Health and Socioeconomic Effects of Cousin Marriage in South Asia," Theresa Chaudhry (Lahore School), Mushfiq Mobarak and others (Revise & Resubmit).

		</p>
		
