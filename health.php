		<h3>Birth Order, Gender, and Child Nutritional Status in Punjab </h3>
		<p><b>Researcher: </b>Theresa Chaudhry (Lahore School) and Maha Khan (Lahore School; current PhD. student, The University of York)</p>
		
		<p>
			Jayachandran and Pande (2015) have found a steep birth order gradient in height-for-age, a standard measure of long-term nutritional status, for Indian children in comparison to several African countries. On the other hand, they find that first-born sons are taller than their African equivalents, in part reflecting favoritism toward eldest sons. In replicating this exercise for data from Pakistan, we also find a steep birth order gradient. <br><br>
			In the next stage of the analysis, we are checking the robustness of this result through the use of mixture models and inclusion of additional sources of data.
			

		</p>
		
