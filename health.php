		<br>
		
		<h3>The State of Health in Pakistan: An Overview </h3>
		<p><b>Researcher: </b>Uzma Afzal (Lahore School) and Anam Yusuf (Lahore School)</p>
		
		<p>
		The Millennium Development Goals (MDGs) provide time bounded objectives to overcome extreme poverty and provide the basic human rights to health, education, and security that were pledged in the Universal Declaration of Human Rights and United Nations Millennium Declaration (Millennium Project, 2006). Two years short of the deadline in 2015, it is useful to see how Pakistan has performed in the health-related goals that were set in 2000. Health outcomes are useful in gauging a country's health performance over past decades and in conducting cross-country comparisons. <br><br>
		Pakistan is not on track to achieving most health-related MDGs. While there has been an improvement in the education sector, health remains on the periphery of the development landscape. With the eighth highest newborn death rate in the world ("Pakistan has the 8th highest," 2010), one in every ten children born in Pakistan during 2001-07 died before reaching the age of five years. Women have a 1 in 80 chance of dying of maternal health causes during their reproductive life (World Bank, 2010). Pakistan thus faces a daunting challenge in improving health outcomes for children and adults alike.<br><br>
		Pakistan is going through an epidemiological transition where it faces the double burden of communicable diseases combined with maternal and perinatal conditions, and chronic, noninfectious diseases. The landscape of public health service delivery presents an uneven distribution of resources between rural and urban areas. The rural poor are at a clear disadvantage in terms of primary and tertiary health services. They also fail to benefit fully from public programs such as the immunization of children. Following the 18th Amendment to the Constitution, the health sector has been devolved to the provinces, but the distribution of responsibilities and sources of revenue generation between the tiers remains unclear.<br><br>
		Given the current situation, there is much that needs to be done, possibly in every domain of the health sector. Women and children still have the most to lose. There is a dire need for aggressive intervention to strengthen the network of health services, expand the outreach of health programs, and introduce technologies to better monitor and strengthen the health programs in place. <br><br>
		It is imperative for the government to tackle the country's abysmal child and maternal health indicators. Maternal mortality needs to be addressed carefully by increasing the number of skilled health service providers such as female doctors and LHWs in rural areas. These workers should also focus on disseminating awareness of family planning services and supplies. <br><br>
		There is evidence that, apart from low nutritional intake, communicable diseases are also largely responsible for malnutrition in children. In addition to programs such as the EPI, preventive information on healthy practices such as washing hands, treating drinking water, and sanitation should be systematically disseminated. It is estimated that 4 billion cases of diarrhea each year, mostly in developing countries, cause at least 1.8 million deaths, of which 90 percent are children under the age of five (United Nations Children's Fund, 2008). About 88 percent of these deaths are attributable to unsafe water supply, inadequate sanitation, and poor hygiene (WHO, 2005).<br><br>
		 In this regard, the private sector and NGOs could play a crucial role in spreading awareness among schools, colleges, and universities. In 2009, the private multinational Procter & Gamble Pakistan and the NGO Save the Children came together to build 100 sanitation facilities in 100 days across Karachi, Lahore, and Quetta. They targeted 40,000 school-age children in their health and hygiene awareness campaign. Such measures could help reduce the burden of communicable disease to a large extent. <br><br>
		However, this does not shed the public sector's responsibility, which has the most to contribute in terms of improving the country's water and sanitation sector. Public-private partnerships need to be encouraged in tackling health issues.<br><br>
		 Addressing the issue of no communicable diseases is equally important for the adult population. Given the loss of healthy life years caused by these diseases, the government needs to incorporate programs at the BHU level to effectively prevent the spread of such diseases. Moreover, in order to accurately determine and analyze the burden of disease, a cause-of-death system that collects detailed information on disease prevalence and mortality should be put in place (Hyder & Morrow, 2000). The healthcare system must cater to the twin burden of communicable and noncommunicable diseases simultaneously, which again necessitates public-private support in health financing. <br><br>
		This leads us to the issue of health expenditure in Pakistan. With one of the lowest public expenditure shares on health, the government needs to make an effort to mobilize more resources. While out-of-pocket expenditure on free health services is growing enormously, approximately 4 percent of the population falls into poverty due to health shocks each year. This risk is even higher in rural areas-the highest being in KP-and increases with household size and lower income levels (World Bank, 2010). <br><br>
		Improved monitoring and evaluation is also necessary not only to improve the performance of the health sector but also to enhance existing programs and reforms. Some instruments, such as the Health Monitoring Information System developed by the government in 1992 with the help of USAID, are in place, but the public health surveillance system in Pakistan is still fragmented and has been unable to generate the data required to make informed public health decisions.3 Callen, Gulzar, Hasanain, and Khan (2013) draw on the admirable example of an intervention conducted at the BHU level to monitor public worker absenteeism. They use smartphone technology designed to increase inspections at rural clinics, which proved practical for their purposes. Such studies can be useful in developing effective means of monitoring and evaluating the health system.<br><br>
		Social protection in the form of health insurance could also play a critical role in protecting against health shocks, although the idea has limited scope in Pakistan. In KP, bilateral agencies have promoted social health insurance with support from the religious community, capitalizing on its nonprofit solidarity characteristics. This has served as a call to policymakers for future action. The federal and Punjab governments have also assessed the prospect of health insurance but no plans have been implemented. Needless to say, there is minimal linkage between Pakistan's social protection strategy and the health sector (Nishtar, 2010). In its budget for 2009/10, the government announced that the Benazir Income Support Program would cover health insurance, implying that it was a program only for the ultra-poor (World Bank, 2011). Most evaluations of the program are, however, cautiously optimistic. <br><br>
		We can analyze alternative means of health financing by looking at the health sector across the border in India. The Rajiv Aarogyasri Health Insurance Scheme is an example of one such successful program implemented in Andhra Pradesh. It has successfully covered 87 percent of families living below the poverty line, even though it only covers medical conditions that pertain to catastrophic illnesses (Yellaiah, 2013). The scheme has also substantially reduced out-of-pocket expenditure per capita in the state. On the downside, disease prevention is relegated to the background under such publicly funded insurance schemes, while hospitalization is seen to be a one-off solution to ill health (Karan & Selvaraj, 2012). Despite the pros and cons of such state-funded localized health insurance schemes, an ideal policy would target universal health coverage, providing a push from primary healthcare-level upward. <br><br>
		Following the promulgation of the 18th Amendment to the Constitution, provincial and local governments in Pakistan are constrained by a lack of expertise in formulating pertinent health policies and inadequate institutional capacity to carry out reforms. What the country needs at this point is a focused multi-pronged approach that will improve the provision of primary and tertiary healthcare by building on existing infrastructure and expanding services into areas with limited outreach. With more funds allocated to health, a system of monitoring should be in place to ensure that public resources are properly utilized. Once a universal health policy is formulated, the private sector can better identify its role in complementing that of the state in health service provision.<br><br>
		Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2018/Issue%20SP/10%20Afzal%20and%20Yousaf.pdf" target="_blank">The Lahore Journal of Economics, Volume18: SE (September 2013): pp. 233-247. </a>

		</p>
		
