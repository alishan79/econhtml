		<h3>What Matters in Child Health: An Instrumental Variable Analysis</h3>
		<p><b>Researcher: </b>Uzma Afzal (University of Nottingham; Lahore School of Economics - Center for Research in Economics and Business)</p>
		
		<p>
		This paper studies factors that affect health and the nutritional status of children under the age of five. It attempts to identify the impact of socioeconomic factors such as household characteristics, parental education, community-level infrastructure and health knowledge on the health (measured by height and weight) of children. The study's theoretical framework is based on the household production model and the instrumental variable technique has been implemented for estimation. Household income, illness from diarrhea and vitamin A supplements for children are treated as endogenous variables and have been instrumented. The paper uses data from Pakistan - Multiple Indicator Cluster Survey (MICS) for 2007/08 for Punjab which is a household level dataset gathered by the Punjab Bureau of Statistics. The results suggest that maternal education, health knowledge and household characteristics are important determinants of child health, among other significant indicators. The channel through which maternal education affects child health is considered to be better nurturing and healthcare since the income effect of education is controlled by household income. Household characteristics - income, the number of household members, ownership of durables - prove to significantly affect the health of children in that household. Another important finding of this paper is that female children under five have better height and weight z-scores than their male counterparts. This finding rejects the common presumption of gender bias at the household level in South Asia in early years of life.<br><br>
		Published Article: <a href="https://ssrn.com/abstract=2371408" target="_blank">Afzal, Uzma, What Matters in Child Health: An Instrumental Variable Analysis (December 23, 2013). Child Ind Res (2013) 6:673-693.</a>
		Available at SSRN: https://ssrn.com/abstract=2371408

		</p>
		
