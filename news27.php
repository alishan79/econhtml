			<p><b>Fatimah Shah</b></p>
			
			<p>This research seeks to identify the relationship between parental occupations and gender specific career aspirations of undergraduate students. To do this surveys would be conducted on fourth year undergraduate students in different universities of Lahore. These would be capturing data on demographic characteristics, parental occupations and career aspirations. As career aspirations are determinant of one's career and lifestyle choices, the results from this study may help in understanding prevailing gender gap in various labor market outcomes.</p><br>
			
