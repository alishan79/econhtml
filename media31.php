		<h3>Industrial Policy Urgently Needed to Solve Crisis in Pakistani Manufacturing </h3><br>

		<p>Manufacturing growth has played a vital role in the development of advanced economies as well as in most developing economies and has also helped in closing the income gap between the two. Presently, the manufacturing industry in Pakistan, which was seen as the engine of growth, is in a state of crisis. </p>						
		
		<p>Growth in the large scale manufacturing (LSM) sector, which accounts for 80 percent of the manufacturing sector, has shrunk to its all-time lowest level of 1.1 percent per annum over the last seven years, with still no signs of a pickup in the current period. One key reason is that over the last few decades, Pakistan's exports have not been significantly upgraded in terms of technology or sophistication. As a result of this stagnation in product sophistication, researchers from the Lahore School of Economics have found that Pakistan is on the brink of, if not already in the midst of, premature deindustrialization that blocks off the main avenue for the country to catch up with advanced economies. </p>
		
		<p>However, some positive recent developments like the improvement in the internal security conditions and the announcement of China-Pakistan Economic Corridor investment package of about US$ 46 billion, could boost both domestic and foreign direct investment in the country. This raises the chance that Pakistan's manufacturing growth might be revived and once again achieve the levels reached in the previous high growth cycles.</p>
		
		<p>The researchers found that the main reason behind the crises in the Pakistani manufacturing sector is that there has not been any proactive industrial policy since the 1990's. Based on their analysis, the researchers suggest that there is an urgent need to develop and implement a comprehensive and proactive industrial policy. This policy should consider the main constraints of the sector such as, curtailing unnecessary taxes on manufacturing, prioritizing manufacturing in the management of power and gas shortages, insulating the sector from the chronic exchange rate overvaluation, and finally helping the sector move up the sophistication curve by developing the required technical and skilled manpower. Only with a focused industrial policy can there be a revival in manufacturing growth in Pakistan.</p>
		
		<p>This research was funded by the Lahore School of Economics (LSE) and was published in the Lahore Journal of Economics. The researchers included Dr. Naved Hamid, Director, Centre for Research in Economics and Business, Lahore School of Economics and Ms. Maha Khan, Research and Teaching Fellow, Centre for Research in Economics and Business, Lahore School of Economics.</p>
		
		
		
