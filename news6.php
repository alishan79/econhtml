	<p><b>1.	Session on Survey Questionnaire Design was conducted Dr. Kate Vyborny, Postdoctoral Associate, Department of Economics, Duke University</b></p>
	
	
	
	<div align="center"><img src="images/sum181.png"></div><br>
	
	
		<p>The session was conducted on 7th May, 2018 and included: </p><br>
			
			
		<ul>
			<li>Questionnaire design for quantitative surveys: An interactive session</li>
		</ul>
			
		<p>	A survey involves directly collecting information from people (or sometimes organizations) whom we are interested in. The types of information will take account of the people's or organizations' level of knowledge, attitude, personalities, beliefs, or preferences. Questionnaires are widely used to collect such information. Well-designed questionnaires are highly structured to allow the same types of information to be collected from a large number of people in the same way and for data to be analyzed quantitatively and systematically. Questionnaires are best used for collecting factual data and appropriate questionnaire design is essential to ensure that we obtain valid responses to our questions. </p>
		
		
		
		
		<ul>
			<li>Objectives in designing questionnaires</li>
		</ul>
			
		<p>There are two main objectives in designing a questionnaire:	</p>
		
		<ol>
			<li>To maximize the proportion of subjects answering our questionnaire-that is, the response rate.</li>
			<li>To obtain accurate relevant information for our survey. To maximize our response rate, we have to consider carefully how we administer the questionnaire, establish rapport, explain the purpose of the survey, and remind those who have not responded. The length of the questionnaire should be appropriate. In order to obtain accurate relevant information, we have to give some thought to what questions we ask, how we ask them, the order we ask them in, and the general layout of the questionnaire. This session details how to design and administer surveys.</li>

		</ol>


