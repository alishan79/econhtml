	<h3>Lecture on Reigniting Economic Growth in Pakistan by Dr. Rashid Amjad</h3><br>
		<p>As part of the Distinguished Economist Seminar Series, the Lahore School Economics Society (LSES) organized its first seminar on February 18, 2015. The guest speaker was renowned Pakistani economist and Lahore School professor Dr. Rashid Amjad who talked about "Re-igniting economic growth in Pakistan". The seminar was chaired by the patron LSES, Dr. Waqar Wadho and was attended by distinguished professors and economists including Dr. Shahid Amjad Chaudhry (Rector, Lahore School of Economics), Dr. Azam Chaudhry (Dean, Department of Economics, Lahore School of Economics), Dr. Sohail Zafar, (Dean, Department of Business Administration), Dr. Naved Hamid (Director, Center for Research in Economics and Business) and Dr. Irfan-ul-Haque (Special Advisor for Financing for Development, South Centre, Geneva), Dr. Shakil Faruqi (Professor, Lahore School of Economics), faculty members of the Lahore School and a large number of both graduate and undergraduate students of Lahore School.</p>

		<div align="center"><img src="images/n17.jpg" width="450px"></div>				
		
		<p>Given his expertise on the management of Pakistan economy, Dr Rashid focused this seminar on the issue of economic growth in Pakistan. He started off the analysis by providing a very comprehensive overview of the performance of Pakistan economy in the past decades and highlighted the need to focus on the 'binding constraints' that hamper growth. He argued that foreign exchange has been a binding constraint for decades. Today, the most important constraint that the Pakistan economy faces is the energy shortage and therefore, the government should target this as its highest priority. He suggested that falling oil prices present a window of opportunity which should be exploited to alleviate the existing constraints. In his concluding remarks, Dr. Rashid advised the young economics students to focus on the underlying theory and fundamentals of economic policy-making along with thinking out-of-the-box so that more creative yet pragmatic solutions to the existing economic problems can be suggested.

</p>
		

