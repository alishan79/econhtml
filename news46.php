			<h3>Abstract</h3>
			<p>Development related funding is critical for the long term growth of any country, but the allocation of funds between politicians and different areas can vary due to various factors and one of the reasons can be strong connection of politicians with those who approves or allocate these funds. The paper by Tehreem Zia "The Impact of Political Connections on the Allocation of Development Projects in Faisalabad" tried to find out that does the strength of political connections impact the allocation of development schemes across politicians? Do politicians favor their own constituents with higher value or more popular development projects? and Does the allocation of development funds impact the decisions of voters in elections? According to literature politicians do favor those who are well connected with them but at the expense of the welfare of general public. Developmental schemes data of Faisalabad district has been used to answer these questions, where we have used data from 2010-2016 with the total development schemes of 6900. Faisalabad district has 8 towns, 11 national assembly constituencies and 22 provincial constituencies. TMA's, district government and provincial government approves the project of MNA/MPA and also allocate the funds to them. OLS regressions and Logit model has been used for the estimation of the hypotheses. Results show that strength of connection, electoral strength and party affiliation significantly impact the allocation of development funds in Faisalabad. Further if MNA/MPA spent more money in his constituency he managed to secure more votes in the next general election.</p>


			<h3>Abstract</h3>
			<p>In a developing country, the changes in the economic conditions of a household can lead to parents treating their children differently based on their gender. According to Global Gender Gap Index (2016), out of 144 countries, Pakistan's global ranking in terms of gender gap in economic participation, educational attainment, health and survival along with political empowerment are 143, 135, 124 and 90 respectively. This gender discrimination has given rise to the concept of son preference where the social and economic utility from having a son outweighs that of a daughter.<br>
The paper by Amal Tanvir "Measuring Mother's Empowerment through the Culture of Son Preference in Pakistan" analyzed the factors having an impact on mother's empowerment in Pakistan and focuses on how the prevalent culture of son preference is translated in the gender along with birth-order of the child to play an important role in determining mother's status within the household. To explore this relationship Pakistan Demographic and Health Survey (PDHS) for the year 2012-13 has been used.<br>
The results suggest that having a son or a number of sons (regardless of the birth-order they are born in) is more likely to have a significant and a positive impact on mother's overall empowerment as compared to having sons belonging to different birth-orders. These results hold true even when mother's empowerment is segregated in terms of behavioral and attitudinal dimensions. Moreover, young mothers who belong to an age bracket of 16 to 30 years are more positively impacted by the birth-order of the son(s) as compared to mothers belonging to an age bracket of 30 to 49 years. Similarly, mothers who are old are more likely to see an increase in their empowerment by the mere presence of a son or a number of sons (regardless of the order they are born in) as compared to young mothers.
</p>

