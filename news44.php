	<h3>Lahore Journal of Economics</h3><br>
	
			<div align="center"><img src="images/journal.png"></div><br>
	
		<p>We are pleased to inform you that the latest issue of the <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/volume22issuesp.aspx" target="_blank" style="color:#0066CC">Lahore Journal of Economics (Volume 22-SE, September 2017)</a> is now online.  </p>
		
			<p>This issue contains the Papers and Proceedings from the Lahore School's Thirteenth International Conference on the Management of the Pakistan Economy. The theme of this year's conference was "Igniting Technology Led Growth in Pakistan: Role of Monetary, Fiscal and Investment Policies." 
			<br><br>
			
			<b>The issue contains articles on a wide range of topics: </b></p>
			
			
			<ul>
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/01%20Chaudhry.pdf" target="_blank" style="color:#0066CC; font-size:13px">Chaudhry, Jamil, and Chaudhry</a> focus on the Pak-China Free Trade Agreement 2006/07 and find that exports and imports have both risen but that firm level productivity and value-added have fallen, both in the sectors made more vulnerable (by reductions in Pakistan's tariff rates) as well as in the sectors that China has given greater access (by reductions in China's tariffs).</li>
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/02%20Uppal.pdf" target="_blank" style="color:#0066CC; font-size:13px">Uppal</a> looks at the sustainability of Pakistan's external debut by focusing on yields on the country's Eurobonds and the spreads on the Credit Default Swaps (CDS) traded in the international markets and concludes that decreases in bond yields and CDS spreads may signal that the country's external debt is currently at sustainable levels. </li>
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/03%20Iqbal.pdf" target="_blank" style="color:#0066CC; font-size:13px">Iqbal, Din, and Ghani</a> look at the relationship between the fiscal deficit and economic growth in the case of Pakistan, and find that the latter is compromised when the deficit exceeds a threshold of about 5.6 percent. </li>
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/04%20Hamid%20and%20Mir.pdf" target="_blank" style="color:#0066CC; font-size:13px">Hamid and Mir</a> analyze the extent of exchange rate overvaluation in Pakistan and focus on how prior to March 2013, the policy target of exchange rate management was the stability of the real effective exchange rate while the current government's policy target has been the stability of the nominal exchange rate against the US dollar.</li>
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/05%20Mangla%20and%20Hyder.pdf" target="_blank" style="color:#0066CC; font-size:13px">Mangla and Hyder</a> investigate the monetary policy effectiveness of Pakistan and they find that monetary policy is quite effective in stabilizing consumer prices and the real exchange rate in the economy.</li>
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/06%20Ahmed%20Khalid.pdf" target="_blank" style="color:#0066CC; font-size:13px">Khalid</a> analyzes how financial deepening could lead to changes in the behavior of economic agents and then presents empirical evidence on the link between financial and trade integration and growth using data from a sample of countries from the Asia Pacific region.</li>	

				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/07%20Rasiah.pdf" target="_blank" style="color:#0066CC; font-size:13px">Rasiah</a> discusses how capital accumulation and macroeconomic management were critical in quickening economic growth in East Asia, and analyzes how investment regimes, sources of finance, technological upgrading and policy frameworks explain economic growth in Indonesia, Malaysia, Philippines, South Korea and Thailand.</li>	
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/08%20McCartney.pdf" target="_blank" style="color:#0066CC; font-size:13px">McCartney</a> looks at the case of Bangladesh as an example of a country that is at risk of falling into the "middle income trap" and argues that structural change is necessary for sustained growth as opposed to productivity, competitiveness and technological change. </li>	
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/09%20Ejaz%20and%20Hyder%20.pdf" target="_blank" style="color:#0066CC; font-size:13px">Ejaz and Hyder</a> examine the vulnerability of demographic groups in Pakistan to business cycle fluctuations and find that older and younger are more vulnerable than middle-aged workers, women and employers also tend to be more vulnerable and that more education is not always protective against business cycle fluctuations. </li>	
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/10%20Hussain%20and%20Said%20.pdf" target="_blank" style="color:#0066CC; font-size:13px">Hussain and Said</a> look at the role of income diversification strategies amongst rural households in Pakistan in raising living standards and they find that households that diversify have higher income and spending as compared to households that specialize in a single occupation, and that households with diversified occupations also tend to plant a larger number of crop types. </li>	
				
				<li><a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2022/Issue%20SP/11%20Rizvi.pdf" target="_blank" style="color:#0066CC; font-size:13px">Rizvi, Naqvi, and Tanveer</a> study the various models of mobile banking in Pakistan and then touch upon the ecosystem that needs to be built in Pakistan to utilize the full potential of mobile technology. </li>	
				
			</ul>
			
