		 <h4>Lahore School Faculty Members attend ADB 3ie Conference</h4> 

		
		<p> The research fellows at the Center for Research in Economics and Business (CREB), Lahore School of Economics, presented their on-going projects at the ADB 3ie Conference on Making Impact Evaluation Matter held on September 3-5, 2014 in Manila, Philippines. With more than 400 participants and speakers, this was the first large scale, global impact evaluation conference in the region. Speakers for the plenary sessions and panel discussion included distinguished international figures from the field of impact evaluation and policy making. Conference sessions covered a range of topics and impact evaluations being conducted in the field of health, education, community development, governance, climate change, poverty alleviation and other aspects of social development. The conference was preceded by 3 days of pre-conference workshops on September 1-3, 2014. </p>
		
		<div align="center"><img src="images/exevent3.jpg" width="500px" ></div><br>

		<p>Farah Said (Lahore School of Economics) presented the design of a study that is joint work with Mahreen Mahmud (University of Kent). The study is a Randomized Control Trial involving business loans provided to women in households where no female run enterprise exists currently. The study is being conducted in selected districts of Punjab, Pakistan in collaboration with Kashf, one of the largest private sector microfinance providers in the country and is expected to inform policy making by investing in a product that is targeted to draw new clients into their client pool.</p>
		<p>Asha Gul (Lahore School of Economics) presented the design of the Randomized Control Trial being funded by the Pakistan Poverty Alleviation Fund (PPAF) and is an academic collaboration with Hamna Ahmed (University of Kent), Kate Vyborny (Duke University) and Simon Quinn (University of Oxford). This study focused on incentivizing development across almost 800 Third Tier Community-based Organizations spread across Pakistan by testing the impact of two sustainable measures the donor can take to engage with these organizations: (i) self-reporting and (ii) non-financial incentives. The Baseline Survey for this study is presently being undertaken and expected to be completed by October 2014.</p>
