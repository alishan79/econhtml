			<hr>
			<p>Dr. Theresa Chaudhry is Professor of Economics and a fellow of the Centre for Research in Economics and Business (CREB) at the Lahore School of Economics. She is also a research affiliate with the International Growth Centre (IGC) in the UK. She received a BS in Foreign Service from Georgetown University in 1996, and a PhD in Economics from the University of Maryland, College Park in 2005. Prior to teaching at the Lahore School, Dr. Chaudhry worked at the World Bank in Washington, D.C. on issues of public finance and public sector governance.</p>
			
			<p>Dr. Chaudhry teaches microeconomics for the BSc Economics, MPhil Economics, and PhD Economics programs, and has supervised both BSc and MPhil theses. She also serves as an editor of the Lahore Journal of Economics, a bi-annual scholarly journal cited in the JEL. Her current on-going projects include survey-based and experimental research in Punjab, Pakistan on i) consanguineous marriage, ii) firm-level productivity, iii) child health, and iv) conditional cash transfers for girls' secondary schooling.</p>
			
			
