	<h3>Geographical Constraints and Health Facility Choice: Evidence from Kashf's Health Micro Insurance Program</h3><br>
			
			<p><b>Dareen Latif</b></p>
			
			<h3>Abstract</h3>	
	
	
	
		<p>Low income individuals are vulnerable to risk arising from health induced income shocks, which entail sizeable economic costs for the poor, such as, loss of income, inadequate consumption and high out of pocket expenditures. Moreover, modern medicines and quality health facilities are inaccessible for one billion people across the globe, and therefore these result in a detrimental impact on health outcomes. This low utilization and inaccessibility of health services is especially true in the case of Pakistan. In light of this; health micro insurance offers a promising mechanism to protect the poor against risk and vulnerability. This paper, therefore, studies the impact of physical distance to panel facilities on utilization of health services in panel versus non-panel hospitals in the context of Kashf Health Micro Insurance (HMI) Program offered in collaboration with Jubilee Insurance. Thus, we will address three main research questions: First, what is the impact of physical distance on choice of health facility? Second, whether the burden of physical distance is greater for women, especially those with gynecological conditions? Third, whether information and transportation constraints could be possible mechanisms through which distance impacts facility choice.  We will employ administrative data compiled using Kashf and Jubilee records on 30,000 hospitalization claims made by Kashf clients and their respective family members covered by the HMI program in Punjab. The time period considered is 2014-2017 which is the length of time the HMI program has been in operation. Additionally, the data on distance also comes from Kashf's records and alternatively, travel time is being tentatively calculated through GPS co-ordinates between the hospital and branches using geographic information system (GIS) software. Furthermore, district statistics related to length of metalled road network and government health infrastructure comes from Punjab Development Statistics (PDS) 2014.</p>

