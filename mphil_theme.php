		<h3>Labor Pooling As a Determinant of Industrial Agglomeration </h3>
		<p><b>Researcher: </b>Najam uz Zehra Gardezi (2012)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This paper analyses agglomeration behavior exhibited by the manufacturing firms in Punjab. It employs a unique data set to construct a distance based measure of agglomeration to test the existence of localization economies. The M function which is the industry level measure of concentration is regressed on a number of industry characteristics that measure the presence of positive externalities. In particular, a measure of each industry's potential for labour pooling is used to examine whether firms that experience greater fluctuations in employment are likely to be more concentrated. The results provide evidence as to the importance of labour pooling working through the stated mechanism in explaining the high level of concentration within industries.<br><br>
		
