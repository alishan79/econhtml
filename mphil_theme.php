		<h3>Effects of Agglomeration and Trade Liberalization on Firm Entry and Exit </h3>
		<p><b>Researcher: </b>Marjan Nasir (2012)</p>
		
		<h5>Abstract</h5>
		
		<p>
		The turnover of firms in an industry is affected by both domestic and international factors as has been highlighted by research in industrial organization. This thesis conducts a firm level analysis to see the impact of agglomeration on firm entry and exit in domestic industries, and the impact of trade liberalization on firm entry and exit in export industries in Punjab. The study also illustrates using maps how some industries are present in clusters while others are highly dispersed. The results suggest that higher firm entry and exit takes place in highly agglomerated industries and a real exchange rate depreciation increases firm entry and exit while tariff reduction has no significant impact on firm turnover.<br><br>
		
