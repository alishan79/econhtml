		<h3>Social and Economic Networks: An Investigation of Retailer Networks in Lahore & their Impact on Enterprise Performance </h3>
		<p><b>Researcher: </b>Amar Shuja (2016)</p>
		
		<h5>Abstract</h5>
		
		<p>
		A detailed study of a market in Lahore was conducted to create a map of the socioeconomic network of different retailers located in this particular area. The aim was to distinguish various ways in which these individual sellers were connected to one another, and then investigate how these network ties affected their economic lives. As opposed to developed economies where economic exchange is buttressed by formal market institutions, developing economies often depend on personal relationships between different economic actors for business transactions. This study looks at how networks allow entrepreneurs to navigate through a business environment that is troubled with sluggish and costly information transfer, prohibitive terms of credit, and feeble (often exploitative) formal institutions that are a detriment to contract enforcement. This research uses a respondent driven sampling method to obtain comprehensive social network data through extensive structured interviews with sellers of water pumps and motors in the Brandreth Road area of Lahore. Information on 6 different types of relationships, from who respondents took advice from, to who they shared information, and inventory with were obtained to construct a network of connections representative of the many ways in which retailers in these markets engage with and benefit from one another. Each individual seller's centrality in these networks was then determined to confirm that more central players in the market had a significantly different demographic makeup than their less central counterparts, and that they leveraged this position to secure more favorable terms of exchange for themselves: more central sellers paid significantly less for borrowed inventory, and did so with a significantly relaxed repayment schedule. Centrality, however, had an unexpected impact on the sellers' joint purchases arrangements, where less central sellers in the market reported receiving significantly larger discounts on jointly purchased supplies.<br><br>
