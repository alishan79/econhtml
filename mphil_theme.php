		<h3>Effects of Agglomeration on the Formation and Scale of Operations of New Firms </h3>
		<p><b>Researcher: </b>Maryiam Haroon (2012)</p>
		
		<h5>Abstract</h5>
		
		<p>
		New firm formation is an important determinant of economic and regional development. Agglomeration has been highlighted as one of the main factors enhancing formation and scale of operations of new firms in the industrial organization literature. This study has estimated a model which determines the effect of local conditions on new firm formation and scale of operations for the manufacturing sector in Punjab, Pakistan using data from the Directory of Industries (DOI). The findings of the study reveal that agglomeration through localization and urbanization has a strong impact on the formation of new firms and their scale of operations.<br><br>
