		<h3> Relative Wage Variation And Industry Location Within Districts Of Punjab</h3>
		<p><b>Researcher: </b>Zunia Saif Tirmazee (2012)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This study tests the wage differential between nonproduction (whitecollar) and production (blue-collar) workers across districts of Punjab; it also seeks to determine whether these variations affect the industrial structure of these districts for the periods 2000/01 and 2005/06. Such an analysis is important because (i) it yields information on the income convergence in a country and (ii) it points out that policies targeted at regional development must take into account data on factor prices. For instance, when addressing equity concerns, the government must focus on enhancing the skills of workers in regions where skill levels are low - giving them a better chance of finding jobs - rather than setting up blue-collar-intensive industries here since any wage differential would be arbitraged away when labor is perfectly mobile. The findings from the first stage of the analysis suggest there is evidence of relative wage inequality in Punjab: generally, nonproduction workers earn higher relative wages in central Punjab and lower relative wages in southern and western Punjab. These findings are consistent over the five-year period 2001-06. The second stage of the analysis shows that the industrial mix also varies across Punjab as districts differ in terms of the wages they offer both nonproduction and production workers.<br><br>
