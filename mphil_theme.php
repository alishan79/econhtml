		<h3>Resource Misallocation And Aggregate Productivity In Punjab </h3>
		<p><b>Researcher: </b>Muhammad Haseeb (2009)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This study follows Hsieh and Klenow (2009) to study the role of misallocation in aggregate productivity for manufacturing plants in Punjab. Data for manufacturing plants in Punjab is taken from the Census of Manufacturing 2000-01 and 2005-06. In this paper, we essentially look at the extent to which marginal products differ across firms within each industry. We then imitate the liberalization settings by allowing marginal products to equalize across the plants in each industry. We find relatively more productivity dispersion in Punjab as compared to Hsieh and Klenow (2009) in India and China. Furthermore, we find that moving to the US efficiency level would boost manufacturing TFP in Punjab by 22.33% and 55.83% in years 2000-01 and 2005-06 respectively. We also explore the potential sources of productivity dispersion for manufacturing plants in Punjab.<br><br>
		
