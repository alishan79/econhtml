		<h3>CPEC and Industrial Strategy - Opportunities and Synergies (An analysis done by the Lahore School of Economics)
		</h3><br>

		
		
		<p>Over the last few years, the Pakistani economy has started to gather some momentum. But despite the fact that growth is picking up, there are still many structural problems facing the economy which have yet to be addressed. Some of these like the slowdown in industrial growth, the stagnant exports and the persistent balance of payment problem are closely linked. If you add to this the fact that Pakistan is in the process of implementing initiatives related to the China Pakistan Economic Corridor (CPEC), then it is crucial to determine a coherent strategy for the coming years. In this context, the researchers from the Lahore School of Economics have proposed some key elements of an industrial strategy that has the potential to revive industrial growth as well as lead to higher value-added exports. In particular, some of the key questions in their analysis included where to locate clusters and special economic zones and what to produce in them, determining optimal locations for industries to benefit from CPEC, formulating a credit policy that meets the demands of SMEs, and developing a trade policy that promotes the import of high quality inputs that can lead to higher value-added exports. <br><br> 
	To begin with, a major gap that exists in the current Pakistani industrial strategies is that very little attention is paid to which sectors have the greatest potential to grow, in which areas these sectors should be promoted to determine if industry specific clusters should be promoted in these areas or if there is room for multi-industry special economic zones.<br><br>
	One way to begin this process is to map the CPEC related infrastructure that is planned, with a special emphasis on planned transportation routes. Then, one should look at the historical evidence (using firm level data) to see which sectors and locations have gained in the past from enhanced transportation routes. After this one should link the planned CPEC related transportation routes locations and industries to see which can potentially benefit (i.e. those areas and industries that will be better connected because of these new routes). Finally, one should find the locations (districts) as well as the industries that will benefit the most from these new routes. The reasoning behind this that some locations may seem to benefit because they are now closer to road networks, but if these locations lack certain factor endowments then one can't recommend specific industrial strategies in these locations. At the same time, other districts may gain by being closer to new routes and may have the factor endowments to support industry specific growth strategies. <br><br>
	The next question is how to formulate a strategic Trade Policy that supports an Industrial Policy? Historically, there has been much talk of creating the environment or providing the incentives for exporters to expand exports. But despite this discussion and years of government policy to support exporters, the fact remains that Pakistani exports are primarily from a narrow based (mostly textiles), that are remained focused on extremely low value-added goods, and are probably relatively inelastic to changes in the exchange rate. Indeed, there is recent news in which the government has proposed a wholesale reduction in tariffs and have used the argument that these tariff reductions will reduce the cost of imported inputs, which in turn will lead to the production of higher value-added goods and higher exports. <br><br>
In order to do this, one needs to determine the potential higher value-added goods that can be produced in Pakistan and what imported inputs go into these goods. This points to the role of trade policy to support an industrial strategy. More specifically, this means that Pakistani policy makers need to design a trade strategy that promotes the import of the types of inputs that can help exporters produce higher value-added exports. This also means that wholesale reduction of tariffs may not be an optimal strategy.<br><br>
	A final issue is the need to identify the role of Innovation and Technology in an Industrial Strategy. One of the biggest problem that exists in current industrial strategies is the lack of focus on technology and innovation. Some ways to develop a technology transfer strategy with an industrial strategy is:  First, determining the key industrial sectors worth focusing on (i.e. with the greatest export potential, greatest growth potential, etc.). Second, mapping the technology that exists in these sectors. Third, devising a strategy for <b>TECHNOLOGY TRANSFER</b> in these sectors. Finally determining how to incorporate this transfer of technology in the current CPEC related initiatives.	<br><br>
The researchers working on this study were Dr. Azam Chaudhry, Professor & Dean, Faculty of Economics, Lahore School of Economics; Dr. Theresa Thompson Chaudhry, Professor of Economics, Lahore School of Economics.<br><br>

</p>
		
		
