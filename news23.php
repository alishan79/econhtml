			<h3>Abstract</h3>	
			
			<p>This thesis analyzes a model of growth with endogenous time preferences. Economic growth is determined by factors like consumption, income, savings etc., and this thesis will include environmental quality as a factor in the production function. The analysis will focus on how incorporating endogenous discounting into individual preferences affects economic growth. I hypothesize the presence of multiple equilibria, one low growth and one high growth. Also the requisite stability analysis will be done to look at stable equilibria along the balanced growth path. Furthermore, I will analyze how fiscal policy can be used to change a low equilibrium growth path to a high equilibrium growth path.

