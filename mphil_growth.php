		<h3>Unique and Multiple Equilibria in a Macroeconomic Model with Environment: Stability Analysis and Transitional Dynamics</h3>
		<p><b>Researcher: </b>Hafsa Tanveer (2015)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This thesis develops and analyzes an economic growth model with environ- mental quality factor in the production and utility function. We first assume that the capital share equals to the inverse of intertemporal elasticity of substitution and solve our model for the balanced growth path. Unique low growth equilibrium is attained when environmental quality is given less weight in the utility function. The multiple equilibria exist if environmental quality is given greater weight in the utility function. These results hold even when we relax the assumption = and consider fairly general values of capital share and intertemporal elasticity of substitution. We study the stability and transitional dynamics of model. We conclude that an economy in which the environmental quality is given relatively less importance by the agents will be caught in low growth, high consumption poverty traps which is the case for developing countries whereas the economies can potentially reach a relatively low consumption, high growth steady state if they give greater importance to environmental quality.<br><br>
		
