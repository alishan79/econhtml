		<a name="Spillovers"></a>
		<br>
		
		<h3>Growth Spillovers: Separating the Impact of Cultural Distance from Geographical Distance</h3>
		<p><b>Researcher: </b>Rabia Ikram (2011)</p>
		
		<h5>Abstract</h5>
		
		<p>
		While recent advances in communications technology have effectively reduced the physical distance that knowledge and innovations have to travel between countries, cultural differences between countries still limit the ease with which innovations are transferred and adapted. So, countries with common cultural or linguistic characteristics can share technology and innovations more easily. This paper separates out the impact of cultural spillovers from geographic spillovers using the data on bilateral genetic distance used by Spolaore and Wacziarg (2009). We find that greater growth spillovers occur between countries that are geographically closer and also between countries that are culturally similar. We also find that there are greater growth spillovers between countries that have greater bilateral trust, even when one controls for the bilateral geographic distance.<br><br>
