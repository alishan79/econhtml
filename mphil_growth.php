		<h3>The Role Of International Migration Of Unskilled Labor And Parental Absenteeism In The Human Capital Formation Of Children Left Behind</h3>
		<p><b>Researcher: </b>Yumna Hasan (2016)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This study builds up a theoretical model to explore the combined effect of parental absenteeism due to emigration of unskilled labor and remittances on the economic growth in the source country through the channel of human capital accumulation of children left behind. The results of the study reveal that the international migration of unskilled adults constructively lowers down child labor in the economy. Moreover, this emigration is beneficial for the human capital formation of children left behind and economic growth in the source country only when the unskilled worker's relative wage is above a threshold level. In such a scenario, the positive effect of unskilled migration, in the form of an increase in the optimal proportion of time devoted to education by the child, overpowers the negative effect of parental absenteeism. The reverse holds true when the unskilled worker's relative wage is below a threshold level. This is because parental absenteeism emerges as a dominant force in this case and hampers human capital formation of children left behind, thus, adversely affecting the source country's growth rate.<br><br>
