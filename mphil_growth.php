		<a name="Determinants"></a>
		<br>
		
		<h3>Determinants of Female Labor Force Participation in Pakistan: An Instrumental Variable Approach</h3>
		<p><b>Researcher: </b>Mehak Ejaz (2010)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This working paper analyzes the determinants of female labor force participation (FLFP) across rural and urban Pakistan. It provides evidence supporting various aspects of the female labor supply by utilizing crosssectional data on females between the ages of 15 and 50 drawn from household data collected as part of the Pakistan Social and Living Standards Measurement Survey for 2006/07. Potential explanatory variables that determine FLFP include (i) females' own characteristics, (ii) household characteristics, and (iii) female empowerment indicators. Endogenous explanatory variables, such as ownership of home appliances, fertility, and co-residence, can lead to biased and inconsistent results due to reverse causality. Therefore, the potential instruments we use here include (i) the average number of home appliances owned in the locality, (ii) the gender of the firstborn child, (iii) whether or not the first two children are of the same sex, (iv) proximity to a clinic, (v) contraceptive use, and (vi) housing type. The probit model is used to estimate variables, while the instrumental variable (IV) approach is used to tackle the issue of endogeneity. In the first stage, the study's results give estimates of endogenous covariates separately, using the IV approach. In the second stage, the IV vector is used to show the impact of explanatory variables on the dependent variable FLFP. We find an inverse and significant relationship between FLFP and both fertility and the gender-wage gap; and a direct and significant relationship between FLFP and ownership of home appliances and co-residence.<br><br>
