		<h3>The Political Economy of Ethnic Polarization, Natural Resource Curse and Income Inequality</h3>
		<p><b>Researcher: </b>Sadia Hussain (2015) </p>
		
		<h5>Abstract</h5>
		
		<p>
		This paper analyzes the impact of ethnic polarization in triggering the natural resource curse which consequently results in higher levels of income inequality. We use a dynamic infinite time horizon model in a general equilibrium framework setting. A backward-induction process is used with three essential elements. Firstly, we decide upon the size of the elite through cost-benefit analysis. Secondly, the elite decide whether to indulge in rent-seeking or not by comparing their respective utilities. Lastly, the elite decide upon the optimal level of expropriated rents through a utility maximization problem. We are able to show that it is only in ethnically polarized societies, the economy is stuck in a high-corruption equilibrium by raising the returns from rent-seeking. Conversely, in ethnically homogeneous societies, a low-corruption equilibrium will exist conduit lower concentration of political power CREB.<br><br>
		
