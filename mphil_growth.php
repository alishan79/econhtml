		<h3>The Role of Socio-Economic Networks in Lahore for the General Elections 2013</h3>
		<p><b>Researcher: </b>Mahnoor Asif (2014)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This study creates a map of the political and non-political networks of the main parties of Lahore to see how politicians are affiliated to each other. General Elections are crucial in framing a country's future. As all consequent policies, internal and external, are products of those who secure a seat in the Provincial and National Assemblies. But, before a candidate contests in the General Elections, he must win the party ticket. From a pool of applicants the one who is chosen to represent his party at a given constituency, is a prominent and connected leader, as such a representative has a higher probability of securing a win for the party. This study looks at such connections, which exist among politicians in the shape of a network. Using political and non-political variables, networks were generated among the major leaders/politicians of Lahore. Each common variable among the politicians created a connection, and all these connections together produced a network. On the basis of such networks, centrally located politicians were identified using a simple Centrality Index. Following this, empirical analysis was conducted to establish the determinants of centrality, and to see how being centrally located in a network impacts a politician's probability of securing party ticket and subsequently winning the General Elections.<br><br>
