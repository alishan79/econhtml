		<h3>Endogenous Patience in a Model of Economic Growth with Human and Unproductive Social Capitals</h3>
		<p><b>Researcher: </b>Umair Ayaz (2014)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This research explores the relationship between size of the government and economic growth in an endogenous growth model with productive human capital and unproductive social capital. We model discount factor as an endogenous function of inherited human capital. We find that with low endowment of human capital, government intervention of any size is growth depressing, for high endowment of human capital, government intervention is not associated with the accumulation of unproductive social capital and there is high growth. Whereas for intermediate levels of inherited human capital, whether government intervention is growth depressing or not depends on the size of intervention and level of inherited human capital. In this scenario, bigger size of the government is growth depressing and higher levels of inherited human capital dilute this effect by promoting investment in productive human capital. <br><br>
