		<h3>Pakistan's Experience with the 2006 Pak-China FTA and Lessons for CPEC</h3><br>

		<p>The relationship between Pakistan and China has a long history and over the last few decades this relationship has been accompanied by significant economic interactions which include the 2006 Free Trade Agreement (FTA) between Pakistan and China as well as China-Pakistani Economic Corridor (CPEC).<br><br>

There is a growing realization that this relationship can have a significant economic impact for both countries though the impact of CPEC has still to be determined. In order to address the prevailing uncertainty regarding the economic impact of this major collaboration on the industrial sector of Pakistan, senior researchers from the Lahore School of Economics looked at the consequences of the 2006 Pakistan-China Free Trade Agreement (FTA) to ensure that CPEC related initiatives yield the maximum benefits. Researchers found that although Pakistan-China FTA has had a significant impact on the amount of trade between both countries, but this trade has led to movement in Pakistan from higher productivity to lower productivity firms, which was far from optimal in the context of a Pakistani growth strategy. <br><br>

In particular, the research found that first, Pakistani tariffs on Chinese goods has negatively affected productivity in those sectors that have become more vulnerable to Chinese imports and at the same time, there has been significant decrease in the value added and value added per worker in those sectors that have become more vulnerable. Also, there has been a decrease in the number of firms and the level of employment in these sectors that have become more vulnerable to Chinese imports because of lower Pakistani tariffs.<br><br>

The researchers also found that lower Chinese tariffs on Pakistani goods has negatively affected productivity in those sectors that could have potentially benefited because of higher potential access to the Chinese markets. At the same time, there has been significant decrease in the value added in those sectors as compared to other sectors. Also, there has been a significant increase the level of employment in the potentially benefitting sectors as well as in the total Pakistani exports to China in these potentially benefiting sectors due to lower Chinese tariffs on Pakistani exports.<br><br>

The researchers concluded that in the context of results drawn on the impact of Chinese tariff concessions to Pakistani exports, it is critical that Pakistan gain the same level of tariff concessions from China as received by the ASEAN countries. Only with equal access will Pakistani manufacturers have a chance to move out of the cycle of low productivity firms producing and exporting low value added goods to China and into higher productivity firms producing and exporting higher value added goods to China.<br><br>

The researchers also concluded that since there is a very central industrial cooperation component to CPEC, it is critical that Chinese industrial initiatives yield the maximum economic benefits for local stakeholders in Pakistan. Some ways of achieving this can be first, ensuring that CPEC-related industrial activities must have well-defined local stakeholders who help maximize the local benefits for firms. This can be achieved by promoting joint ventures between the Pakistanis and the Chinese which contain a minimum requirement for local partner involvement in each project as well as guarantee that each is allocated a minimum financial share of each project. The researchers also recommended that the government make pragmatic decisions right now which sectors to focus on. The decision on which sectors to focus on should take into account which sectors can most benefit from greater productivity, which sector can lead to the greatest increase in value added and which sectors have the greatest potential to increase exports. <br><br>

Finally, the researchers recommended that in order to increase productivity, employment, value added and exports, there has to be a conscious decision by the policy makers that CPEC related industrial projects should lead to a move up the technology ladder by firms. This can be achieved by: first, creating firm level incentives for investment in advanced machinery based on the technological sophistication of output. Second, placing a minimum local content requirement (i.e. minimum percentage of locally sourced inputs) for all goods created in CPEC industrial zones. Third, ensuring that Pakistan's technology is upgraded through technology transfers from China by making it mandatory for a minimum level of technology transfer to take place over the life of all CPEC initiatives. Finally, the only way to ensure higher firm level productivity, higher wages and a move towards higher value added output is to develop a CPEC related labour policy that enables the manufacturing sector to switch from low-skilled to high-skilled labour. <br><br>

The researchers working on this study were Dr. Azam Chaudhry, Professor & Dean, Faculty of Economics, Lahore School of Economics; Dr. Theresa Thompson Chaudhry, Professor of Economics, Lahore School of Economics;  and Ms. Nida Jamil, PhD Economics candidate at the Lahore School of Economics
</p>
			
			
							
