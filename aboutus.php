	<LI>Produce world-class research in economics, finance, business administration and related disciplines. </LI>
	<LI>Enable students, researchers, faculty members, and professionals to pursue excellence through emphasis on intellectual rigor, creativity, and critical thinking in a collaborative and enabling environment. </LI>
