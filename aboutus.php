<H3>CAMPUSES: </H3>
<P>
The Lahore School of Economics has two campuses- a City Campus in the heart of Lahore and the Main Campus near the Lahore Airport. The City Campus is located in the picturesque setting of Gulberg close to the central Liberty Market area and hosts the Graduate Institute of Development Studies and the admissions and information office for all programs offered at the Lahore School. There is also a specialized Development Studies Library at this campus.
The Lahore School's Main Campus near the Lahore Airport is spread over 35 acres adjacent to Main Boulevard, Phase VI, DHA on Burki Road Lahore. All academic and research programs of the Lahore School excluding those undertaken by the Graduate Institute of Development Studies are offered at the Main Campus. This campus in addition to Administration and Academic Centres also has a substantial number of other facilities including libraries, computer learning and media studies centres, sport facilities and a cricket ground. In addition, there are large landscaped areas where the students can sit in the open air and relax or study. There are three major dining cafeterias and multiple smaller food facilities where students can have their lunch, tea, cold drinks or snacks.

