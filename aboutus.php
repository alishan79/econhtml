<H3>ACHIEVEMENTS 1997-2017 </H3>
<P>
<B>Institution Building: </B>Lahore School of Economics is located on 35 acres of land in Burki, Lahore with more than 200,000 square feet of excellent teaching and research facilities. The School's faculty comprises of more than 130 full-time PhD and MPhil faculty members and 50 part-time faculty members. Academic activities are supported by 100 MBA / MS/BS (honors) equivalent full time administrative staff. Currently about 400 graduate students and 2,400 undergraduate students are enrolled in different degree programs. <BR>
The Lahore School, in just 20 years (1997 - 2017), has already achieved the status of a leading business school of Pakistan. It has established well documented academic governance systems; and has developed agreed upon vision, mission and values statements. The composition of its academic and research programs are reviewed and revised each year through its Boards of Studies, Boards of Faculties and Academic Committee; and in this process in addition to the faculty of the school, outside academics, industry experts and alumni are deeply involved.<BR> <BR>
<B>Teaching and Learning:</B> The Lahore School's four-year undergraduate (honors) and graduate programs are taught at international standards with generous scholarship programs to ensure equal opportunity to all sections of society. The current 100% scholarship MPhil and PhD research degree programs are aimed at developing teaching faculty and researchers for Pakistani and international universities. The Lahore School's undergraduate degree programs in economics, business and related disciplines comprise degrees with double majors, and degrees with a major and a minor; which is feature unique to the Lahore School of Economics; and which intends to develop well rounded individuals sensitive to working in teams. The Lahore School provides excellent platforms for more than 20 extra- curricular programs including sports, dramatics, media and music.<BR> <BR>
<B>Placement of Graduates: </B>The undergraduates, MBA and MPHIL graduates of Lahore School are considered amongst the top job market candidates in Pakistan, and have been employed in the Civil Service of Pakistan, the State Bank of Pakistan, MNCs (Multinational Companies), national companies, international financial institutions (like the World Bank and International Monetary Fund) and NGOs. Lahore School graduates are also setting up an increasing number of successful businesses.<BR> <BR>



<B>Research and Innovation: </B>The Lahore School is committed to continuing world class research that is being currently undertaken at the Lahore School through the following venues: <BR>
(i) Internationally collaborative research with production of 20-30 impact factor publications a year published in the world's leading journals including the Quarterly Journal of Economics; <BR>
(ii) Internationally published books (including by the Cambridge University Press) on the Pakistan Economy;<BR> 
(iii) Three leading internationally recognized academic journals viz. the Lahore Journal of Economics (HEC X Category Journal), the Lahore Journal of Business (HEC Y Category Journal) and the Lahore Journal of Policy Studies; <BR>
(iv) Two annual international conferences including one on the Management of the Pakistan Economy (14th Annual Conference in 2017) and the other on Managing Business in Pakistan (5th Annual Conference in 2017): <BR>
(v) One international conference on Micro Finance (2nd Annual Conference in 2017) <BR>
(vi) Three annual national conferences: one each in the area of Mathematics, Environment Sciences and Policy, and Social Sciences; <BR>
(vii) A regular conference on the Pakistani Diaspora hosted by the Graduate Institute of Development Studies. <BR>
(viii) Twenty to thirty research paper presentations by the faculty members per year at international conferences; <BR>
(ix) International faculty exchange and international training for 6-10 faculty members per year; <BR>
(x) Faculty research linkages with Pakistani industry, chambers of commerce and government departments, leading to contributions including analysis and policy recommendations for the development of Pakistan economy. <BR>
These research related activities are coordinated by the Lahore School's academic departments, Centre for Research in Economics and Business (CREB), Graduate Institute of Development Studies (GIDS) and the Innovation Technology Centre (ITC); and the research output is made available to the public through CREB, GIDS, ITC and publication of Departmental Working Papers, Case Studies and newspaper articles.<BR> <BR>

<B>Knowledge Exchange Partnerships:</B> The Lahore School of Economics is engaged in multiple international and local partnerships which focus on knowledge exchange. These international partnerships include: <BR>
(i) International research partnerships with top level foreign universities (such as Columbia University, MIT, Yale University, Oxford University and University of Warwick) and research institutions (such as the International Growth Centre (UK), Private Enterprise Development in Low Income Countries (UK), the National Science Foundation (US) and the British Academy (UK)).<BR> 
(ii) International student and faculty exchange programs through MOUs (with institutions such as University of St. Gallen (Switzerland), University of Colombo (Sri Lanka), Bahcesehir University (Turkey) and Macquarie University (Australia). <BR>
(iii) Hosting national level student competitions (such as National Debating Competitions, Model United Nations and Econothons).<BR> <BR>

<B>Faculty, Staff and Alumni Development: </B>Each department of the Lahore School focuses on developing faculty and staff capacity by conducting Faculty Development Workshops on a semesterly basis. Faculty, staff, alumni and students are also offered summer workshops for capacity development. Additionally, faculty and staff are sent to international conferences, training workshops and seminars in order to develop their teaching, research and administrative skills. The Lahore School's Alumni Office serves as a focal point for alumni related activities which include both alumni involvement in the Lahore School's academic and research programs and alumni development through the following specialized associations:<BR> (i) Independent Business Leaders; <BR>(ii) Bankers and Financial Analysts;<BR> (iii) Professionals in Corporate Sector;<BR> (iv) Educationists and Public Officers; <BR>(v) Telecom & IT Professionals and Consultants; <BR>(vi) Trade Specialists and <BR>(vii) Advertising and Media Professionals.<BR> <BR>

 <B>Social and Environmental Responsibility:</B> The Lahore School focuses on reinforcing environmentally sustainable and responsible economic and business activities both within and outside the Lahore School including helping local communities develop both socially and economically. To this end the Lahore School has a green campus with more than 80% of the area devoted to lawns and trees, use of 100% energy efficient inverter air-conditioning and LED lighting, and currently generating more than 15% of its energy requirements through solar panels (current capacity 120 KVA). <BR>
All its faculty and staff are covered by life insurance, medical insurance and provident fund including voluntary pension schemes. The Lahore School's portfolio of corporate social responsibility activities includes free medical camps, school feeding and support programs for orphanages and for persons with disabilities.<BR>
Symbol of Excellence: The Lahore School is a symbol of excellence and this is reflected in the ranking of the Lahore School of Economics as number two in the 2015 ranking of Business Schools of Pakistan by the Higher Education Commission.<BR>



