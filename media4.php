		<h3>Dear incoming Prime Minister Khan</h3><br>

		
		
		<p>Unless the country's mandate and I have got it wrong, you have done a remarkable thing. You have put welfare of the poor, as your primary economic agenda. That we have not heard, leave alone seen effected, since Zulfikar Ali Bhutto.<br><br>

A pithy parable from my fading undergraduate notes needs retelling for Pakistan. Professor Isiah Berlin recounted the Greek parable of the Hedgehog and the Fox. The fox is quick and clever, perhaps too much so, and knows many clever things. The hedgehog is ponderous and slow but knows one big thing. So, the primary economic agenda for Pakistan, the welfare of the poor.<br><br>

To affect this agenda, we will need three economic policies.<br><br>

We will need a policy to solve the current balance of payments (BoP) crisis. There will be no longer run tomorrow unless we get through today's short run crisis of meeting external payments. And in a way that cuts the periodic cycle of recurrence.<br><br>

We will need a growth policy, to grow the per capita economy, by more than the needs of the people, thereby raising their incomes and saving something to further invest and grow more.<br><br>

And we will need a policy of inclusive growth, of growth that is poverty reducing, by raising the incomes of the poor by more than the incomes of the non-poor. No economy can afford welfare by just transfers from the rich to the poor. The poor have to be given the opportunity to be able to earn their higher incomes, those that can, saving transfers for those that can't, like children and the elderly, and the disadvantaged.<br><br>

Let's focus today on the pithy economics of the first two policies, to solve the BoP crisis and raise growth. There is a standard practice solution for the short run problem of BoPs. Devaluation combined with an unregulated capital market. But I fear that will only further compound the problem it is designed to solve and compromise the longer-term agenda of economic growth. So, we will have to be more inventive.<br><br>

The BoP crisis amounts to a deficit on the Current Account (CA) of $18 billion for the year ending June 2018. This is largely due to a trade deficit, with our imports exceeding our exports by $32 billion. And despite record remittances into the country of $20 billion.<br><br>

The standard solution to such a staggering CA deficit of near 7% of our GDP, is to devalue the exchange rate. The argument being that this will make exports cheaper raising them, and imports dearer, lowering them. But there is a serious problem with this purely market-based solution. The problem is that the falling exchange rate may not on its own settle at an equilibrium level which cuts the CA deficit down to zero.<br><br>

This problem arises because the CA deficit cannot be treated on its own as a secular problem. It involves an allied balance on the Capital Account (KA). For the simple reason that the deficit in transactions on the CA, has to be paid for by transactions in assets on the KA. But when the exchange rate depreciates on the CA side, it worsens matters on the KA side, because it raises net outflows. More capital leaves the country, than comes in. Why? Because everyone bets against the depreciating Rupee, which is not holding its value, and bets on the foreign currency, say the US dollar (USD). So, the country is left with less assets to pay for the CA deficit. But the CA deficit still has to be paid for. So, we borrow. And borrow. And borrow. And run down our Reserves.<br><br>

Some numbers to support this argument. In theory, the deficit on the CA side of $18 billion, should be paid for by a surplus in ownership of assets on the KA side. Instead it is now being paid for by a negative Financial Account of $12 billion, comprising largely borrowing and running down our reserves by $7bn. Our borrowing (called liabilities) in the last year to June 2018 is coming in at $7bn.<br><br>

This BoP problem has accentuated gravely over the last five years, because of our reliance on the standard market solution of devaluation to counter a trade deficit. In 2011-12, our trade imbalance was a half of todays, at $16 billion, with exports at $25 billion and imports at $40 billion. Since then clearly exports have flatlined, while imports have shot through the roof. So, we can't afford these burgeoning imports of transport and electrical goods and consumer goods and have to lower them, until we can afford them. By raising our exports. But rather than doing this by increasing our productivity and competitiveness, we have relied on depreciating the exchange rate. And clearly it has not worked. For reasons being investigated by research at our Lahore School of Economics.<br><br>

But while devaluing the exchange rate has not done the job is was meant to do, pick up exports, it has worked perversely on the KA side. As our trade deficit has doubled, and had to be paid for, our Financial Account has paid for it by going from near balance to the present level of a negative $12 billion, in the past five years. Which of course has been based on borrowing going from near balance five years ago, to its current levels of $9 billion last year, and $7 billion this year and counting. And since this borrowing was not enough on the Financial account side to pay for the mounting CA deficit, we have been running down our reserves at an increasing rate. $3bn in 2011-12, to $7 billion now and counting. Of course, with all this borrowing, our total debt has mounted in five years, from $46 billion to $65 billion currently. Whose repayment comes in at near 30% of exports.<br><br>

But if the market-based solution of devaluation has not helped the economy on the export side, nor impeded imports, and increased our reliance on borrowing and running down reserves, so increasing our debt, then who has it helped. I am an economist and not a politician, so I will not point at individuals, but at a class of people. Devaluation has helped, people who bet against the Rupee and the economy, by investing abroad. And that is where the short run BoP crisis compounds into a longer run problem of growth.<br><br>

Pakistan's problem of growth can be captured by weakness in one variable, its abysmally low investment rate, supported by an even weaker savings rate. An investment rate of 16% and a savings rate of 12% are more befitting of a Least Developed Country, than a Lower-Middle Income country that we purport to be. The reason in the last two decades is credibly the Afghan conflict and terrorism.<br><br>

But the low investment and savings rate is also arguably a result of the policy of devaluation over the last two decades. Investment logically flows to a higher rate of return, a higher rate of interest. With devaluation, the rate of return abroad increases in Dollar terms, relative to the rate of return at home. So, devaluation increases the incentive to invest abroad, betting against the Rupee and the economy. And as capital outflows increase, the Capital Account weakens, and reliance on borrowing and running down reserves increases.<br><br>

The impact of devaluation on BoPs in the short run, and on investment and growth in the long run, is not uniquely Pakistan's. Nor is the behavior of such a class of people investing abroad unpatriotic. This is simply rational behavior of a class of investors responding to the investment environment created by managers of the economy and legislated by politicians. Take our two thriving neighbors, China with $3.5 trillion in reserves, and India with half a trillion dollars, both do not have open capital accounts and regulate their capital markets. While Pakistan with $20 billion reserves at their height can afford to experiment with open unregulated capital accounts combined with a depreciating exchange rate.<br><br>

It is the policy that is simply wrong, not the people.<br><br>

This article has been written by Dr. Moazam Mahmood, Professor in Economics, Lahore School of Economics.<br><br>
</p>
		
		
