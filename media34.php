		<h3>Connections and Elections in Lahore: How Centrality Affects Electoral Politics in Pakistan</h3><br>
		
		<p>The choice of candidates in electoral politics can be a long, difficult process, as illustrated by the primary system that exists in countries such as the United States. In Pakistan, the selection process is different as the candidates are required to appeal to members of their own party for party tickets and then appeal to a majority of voters in order to win the seat. </p>
		
		<p>Recent research by the Lahore School of Economic focused on politicians and electoral outcomes in Lahore in the 2013 Elections and revealed that party affiliation appeared to matter more than the individuals to win a provincial assembly seat. However, to win a National Assembly seat, the opposite holds since voters tend to choose more prominent and politically connected politicians who are expected to bring about policy changes and reforms to their constituencies.</p>
		
		<p>The survey, based on 142 key politicians in Lahore from Pakistan's three main parties-the PML-N, the PPP and PTI, evaluated candidates by testing the impact of their centrality on the likelihood of being allotted a competitive constituency, being nominated by the party and eventually winning the election. Political centrality was based on certain factors such as whether politicians themselves or their relatives have been members of a legislative body or the party and the number of years the politicians represented the party and been contesting elections. Nonpolitical centrality was based on politician's baradari (caste), level of education, own profession, family profession and membership of any professional or social clubs.</p>
		
		
		
		<p>The researchers found that parties preferred to field more central candidates from constituencies where previous election was a close contest. The results also revealed that parties give election tickets to more central politicians in order to increase their odds of winning. At the provincial level, tickets were given to candidates who were politically well connected both within and across parties; sound political connections within the party ensures that sufficient campaign funds are generated, while strong political connections outside the party can determine a larger vote bank. Whereas at the national level, tickets were given to politicians who were socially well connected within their own party and across parties; the party leadership presumes that strong social connections will generate a larger vote bank because the electorate is more likely to recognize the candidate's name. </p>
		
		<p>This research was funded by the Lahore School of Economics. Researchers included Dr. Azam Chaudhry, Professor and Dean, Lahore School of Economics and Ms. Mahnoor Asif, Lahore School of Economics.
