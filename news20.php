	<h3>The Lahore School of Economics' Fourteenth International Annual Conference on Management of the Pakistan Economy:  Accelerating Economic Growth in Pakistan: Key Macro and Sectoral Drivers (Day 2)  </h3><br>
	
	<p>The second day of the Conference opened with a session determining an industrial strategy and optimal locations for industrial clusters and special economic zones. The session chair was Dr. Mujtaba Paracha (Secretary Industries, Government of the Punjab). Dr. Azam Chaudhry (Professor and Dean of the Economics Faculty, Lahore School of Economics) provided a brief overview of the sessions on industrial policy. The aim of these sessions was to explore the current industrial environment of Punjab to identify synergies and opportunities arising from China-Pakistan Economic Corridor (CPEC) and establishment of special economic zones. </p>
	
	<div align="center"><img src="images/drv1.png"></div><br>
	
	<p>Dr. Azam Chaudhry proposed a systematic step-wise approach towards the formulation of an effective industrial policy which entailed: (1) identification of the right sectors for targeting, (2) deciding the optimal location of special economic zones and industry specific clusters, (3) maximizing benefits from CPEC related infrastructure development, (4) devising a strategic trade policy that supports industrial policy, (5) determining if lack of borrowing is simply a supply issue or does demand play a role, (6) defining the key role of innovation and technology in an industrial strategy, (7) realizing the importance of women and the environment, and finally (8) widely using firm level data as a key input for drawing coherent policy lessons. Subsequent presenters in following sessions discussed each of these dimensions in greater detail.

</p>
	
		<div align="center"><img src="images/drv2.png"></div><br>

	<p>The first paper of the first session by Maryiam Haroon (Assistant Professor and Research Fellow at CREB, Lahore School of Economics) investigated the relationship between agglomeration and firm level productivity in Punjab, Pakistan. The paper concluded that spatial concentration of firms and sectors is not uniform, with heavy concentration in central Punjab. The second paper in the session by Umair Ayaz (Research Fellow, Technology Centre, Lahore School of Economics) analyzed the pattern of firm location for a set of identified sectors in Punjab. Using recent demographic and industry statistics and spatial data on planned national highways and motorways under CPEC, the paper identified suitable regions for locating special economic zones. The third paper in the session by Zunia Tirmazee (Assistant Professor and Research Fellow, Lahore School of Economics) studied whether differences between the wages of non-production and production workers can explain patterns of industrial production in Punjab.</p>
	
		<div align="center"><img src="images/drv3.png"></div><br>
	
	
	<p>Papers presented in the second session of day two focused on the role of trade policy, finance and innovation in an optimal industrial strategy. The chairperson for the session was Dr. Theresa Chaudhry (Professor of Economics at the Lahore School of Economics). The first paper in this session by Nida Jamil (Assistant Professor, Lahore School of Economics) and Rabia Arif (Assistant Professor, Lahore School of Economics) identified a potential export gap for Pakistan; sector specific products where the world demand is high but Pakistan's export share is low. The authors proposed an industrial strategy with tariff reductions on intermediate inputs as a potential mechanism for overcoming the export gap. The next paper in this session presented by Dr. Waqar Wadho (Assistant Professor and Senior Research Fellow, Lahore School of Economics) and coauthored with Dr. Azam Chaudhry (Professor, Lahore School of Economics) investigated distinguishing features of fast growing firms in the Pakistani Textile and apparel sectors. The authors showed that small, young and innovative firms grow faster and generate higher employment. 

</p>
	
			<div align="center"><img src="images/drv4.png"></div><br>
			
			
			<p>The last paper in the session by Tariq Raza (Research Associate, Lahore School of Economics) and Amar Shuja (Teaching Fellow, Lahore School of Economics) investigated the extent to which Sialkot-based small and medium enterprises (SMEs) in the manufacturing sector are credit constrained. The study provided valuable insights on optimal financing terms for SMEs, their financial needs and easing access to credit to improve financial inclusion of SMEs in the country. Dr. Theresa Chaudhry wrapped up the session by providing a brief summary of main findings from each paper presented in this session. </p>
 
		 <div align="center"><img src="images/drv5.png"></div><br>
		 
		 <p>The third session of day 2 focused on implications of innovation and change in the financial services industry for economic growth. The session was chaired by Dr. Moazam Mahmood (Professor, Lahore School of Economics). The first paper in the session by Dr. Jamshed Uppal (Associate Professor of Finance, Catholic University of America) and Dr. Inayat Mangla (Professor of Finance, Lahore School of Economics) provided an overview of the linkage between financial services and economic growth, evaluated the development of Pakistan's financial markets and concluded with a discussion of the challenges and policy recommendations for making Pakistan's financial services sector an effective driver of economic growth. </p>



		 <div align="center"><img src="images/drv6.png"></div><br>
		 
		 <p>The second paper in this session presented by Dr. Kumail Abbas Rizvi (Associate Professor, Lahore School of Economics) and coauthored with Bushra Naqvi and Fatima Tanveer provided a qualitative assessment of economic, demographic and technological factors that could be conducive for the penetration and growth of Fintech in Pakistan. The paper also explored the regulatory framework governing Fintech and its potential role in making the segment an active or dormant player in the financial services industry. On the basis of global best practices, the paper concluded with a set of forward-looking policy recommendations aimed at industry players and regulators. Dr. Ayesha Afzal (Assistant Professor, Lahore School of Economics) presented the final paper in this session. This paper reviewed risks surrounding the use of crypto currencies. It also provided various regulatory solutions to improve management and create a safer environment for financial innovation.

</p>
		 
		 

		 <div align="center"><img src="images/drv7.png"></div><br>
		 
		 <p>The last session of day 2 highlighted the importance of reviving agricultural growth in Pakistan. The chairperson for the session was Mr. Fazal Abbas Maken (Secretary, Ministry National Food Security and Research, Government of Pakistan). The first paper in this session by Dr. Naved Hamid (Director, Centre for Research in Economics and Business (CREB) and Professor at the Lahore School of Economics) and Mahniya Zafar (MPhil Student, Lahore School of Economics) discussed government's price policies and failure to reform the regulatory framework for agricultural markets as important constraints to agricultural growth in Pakistan. Focusing on the crop subsector, which accounts for about 40 percent of the value added in the agriculture sector, the paper showed that due to government policies, or the lack thereof, the profitability in this sub-sector is extremely low and farmers have neither the resources nor the incentive to invest in increasing agricultural productivity. Low profitability was attributed to (i) trade and exchange rate policies that discriminate against the agriculture sector and (ii) dysfunctional agricultural markets with a wide gap between prices received by farmers and those paid by consumers, or received by exporters. </p>
		 
		 
		 <div align="center"><img src="images/drv8.png"></div><br>
		 
		 <p>The second paper in this session by Dr. Johannes (Hans) Jansen (Agriculture Economist, The World Bank) discussed an ambitious transformation program to restore agriculture sector's competitiveness and dynamism in Punjab given (1) the rapid shift in domestic demand away from food grains to high-value agricultural products; (2) the significant yet largely unexploited comparative advantage for products, which can provide opportunities to generate export revenue and jobs; (3) the exhaustion of options for new sources of irrigation water; and (4) the need for production systems to become more resilient as climate change progresses.</p>
		 
		 
		 
		 <div align="center"><img src="images/drv9.png"></div><br>
		 
		 <p>The last paper in this session by Dr. Talat Naseer Pasha (Professor, University of Veterinary and Animal Sciences) provided an in-depth discussion of existing constraints and potential policy reforms for promoting growth in the livestock sector. Reliance on traditional marketing systems, little value addition in the livestock value chains, and high processing costs were identified as major constraints to growth in this sector.</p>
		 
		 
		 <div align="center"><img src="images/drv10.png"></div><br>
		 
		 <p>In the end, a brief summary of key messages that emerged during the conference was provided. This marked the end of the 14th International conference on management of the Pakistan economy, organized and hosted by the Lahore School of Economics. </p>
		 
		 
		 <div align="center"><img src="images/drv11.png"></div><br>
		 
		 <p>Day 2 of the conference ended with concluding remarks from Dr. Rashid Amjad (Professor of Economics and Director, Graduate Institute of Development Studies, Lahore School of Economics) and Dr. Azam Chaudhry (Professor of Economics at the Lahore School and the Dean of the Economics Faculty).</p>
		 
