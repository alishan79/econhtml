		<h3>Manufacturers of Pakistani Garments and Fans Struggle with Quality, Management, Worker Absenteeism and Entrenched Piece-Rate Wages</h3><br>

		<p>Textiles account for over half of Pakistan's exports, reaching US $ 3.72 billion in 2011/12.  On the other hand, the electric fan sector in Pakistan has historically focused on production for the domestic market, but has diversified into export markets over the last 10-15 years, with exports reaching US$ 40 million in 2012.</p>
		
		<p>Surveys conducted by researchers at the Lahore School of Economics in both these sectors identified problems that firms face in middle management, monitoring of quality, worker absenteeism, and wage practices.  Academic studies carried out on firms in India and other countries have shown a profound and positive impact of sound management practices on firm-level performance.  Firms in Pakistan are working to improve, but have a long way to go before they achieve international levels of management quality. </p>
		
		
		<p>Ready-made garment (RMG) producers in Punjab are concentrated in and around Lahore.  Garments are produced along an assembly line similarly to other producers around the world.   On the other hand, fan manufacturers in Punjab are clustered in the Gujrat and Gujranwala districts. Fans are produced using a traditional batch system here, even though internationally, large producers such as China manufacture fans along an assembly line. In the batch system, production takes place in stages in individual workshops by teams of workers, under the supervision of a team leader known as an ustad.  </p>
		
		
		<p>In both sectors, much of the responsibility for the day-to-day management of production and quality is delegated to supervisors on the factory floor who have little or no formal training.  These are the ustads and a few foremen in the fan sector and production and quality supervisors in the garment sector.  Both sectors cite worker absenteeism as a major problem that they face as it creates bottlenecks in the production process. <br>	
		
		<p>In response to weak middle management (of maybe because of it), both sectors pay wages that are piece-rates; that is, the worker gets paid based on the number of units produced.  This is not considered best practice in either sector for a variety of reasons.  Insiders believe that piece-rates are responsible for quality issues and therefore both sectors hire a large number of quality inspectors.  But managers face a quantity-quality trade-off: they do not think that they can get workers to complete export orders on time for foreign buyers unless they use piece-rates to incentivize the workers.   Beyond the quality quantity tradeoff, piece rates may have other consequences, such as higher costs and hindering the adoption of new technologies.</p>
		
		<p>Workers are also very comfortable with the piece-rate system and resist changes to it. For example, when one large fan manufacturer attempted to move from the outdated batch manufacturing process to an assembly line to reduce both costs and defects workers refused to adapt to the new system.  One reason was it meant they would be shifted to salaries from piece-rates.  The combination of piece-rates and batch production made it easier for workers to take breaks during the day or even take days off for family commitments, odd jobs or seasonal agricultural work in the villages.</p>
		
		<p>Firms in both sectors believe that if more women entered the workface and took salaried jobs, the quality problems would be resolved. Under these circumstances, international buyers can also play a role in mandating changes in labor or wage practices as the firms in Pakistan have been responsive to buyers' rules and regulations.<br>
This IGC funded research study was conducted by Dr. Theresa Chaudhry, Professor, Lahore School of Economics and Ms. Mahvesh Faran, Lahore School of Economics.
</p>
		
