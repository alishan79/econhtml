		<div class="timely">  Nobel laureate, who showed that for a country maintaining a fixed exchange rate, an open capital account and an independent monetary policy at the same time was just not possible - the impossible trinity as it came to be called in the economic literature.</div>
		<br>
		
		<p>In Pakistan, too we face a version of the impossible trinity as is shown through analysing the country's recurring stop-go cycles of economic growth. The impossible trinity the country's economic managers currently face is how to balance the conflicting demands of maintaining the current high fiscal deficit, the exchange rate and the pursuit of higher economic growth.<br><br>
An overdue step that we took after prolonged stubborn resistance by the Ministry of Finance was the adjustment in the overvalued Pakistani rupee, which has since been devalued by 10 per cent. This has had some positive impact on exports which have grown by around 15pc. But imports still continue to increase unabashedly so that the current account deficit remains unsustainably high and the reserves continue to dwindle.<br><br>
Unfortunately the recently announced budget has done little to address this fundamental macroeconomic imbalance and, indeed, in all probability will lead to a worsening of the macroeconomic situation. The budget tries to get the best of all worlds - please the electorate with unaffordable concessions, postpone much-needed economic reforms as they come at a cost, and wave the flag of high growth. The underlying strategy in support of their policy stance is that by boosting consumption they would spur higher economic growth, and resulting increases in incomes would generate the higher revenues even at the lower tax rates they had introduced.<br><br>
A classical supply-side economic solution may have worked for president Reagan but what our well-intentioned and bright policymakers overlooked is the very real possibility that in the process the growth engine on which they are banking will run out of fuel ie foreign exchange and the economy will then come to a grinding halt, with or without the IMF. They may not have quite understood that the Budget Strategy Paper is based on a growth accounting model which even under the most unrealistic assumptions always churns out an equilibrium.<br><br>
So what must now be done to retrieve the economy from this unfortunate milieu? Not surprisingly, there is a strong school which advocates that we should not change course and ride out the storm. If we face a blow-out then our friends who have recently invested so much in our economy through CPEC will bail us out. They further argue that our current economic problems are temporary resulting from the high burst of imported machinery for our infrastructure and badly needed energy projects. Once they come on stream and the much-awaited foreign investment in the new export-processing and tax free industrial zones materialises we can laugh our way to the bank which would have no shortage of foreign exchange.<br><br>
There is, however, another, more realistic school which has seen the economy cra¬shing downwards many a time and acc¬or¬¬ding to which it would be much more prudent to slow the economy down and keep a firm grip over it rather than be held hostage or indebted to even our most reliable friends (no free lunches in the world of hard finance).<br><br>
This policy stance would entail a mixture of reducing drastically the fiscal deficit to which all stakeholders including the provinces contribute, and if exports lose momentum, be prepared to make some needed exchange rate adjustments. Most importantly, it will entail undertaking important reforms that will ensure sustainable and high long-term growth.<br><br>
There are no prizes for those who can predict which option we will choose. And one can only sympathise with the interim and new finance minister who take over the reins of the economy when the current government leaves and hands over as the previous two governments did before, an unsustainable and highly unstable economy.<br><br>

