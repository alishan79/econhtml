		<td valign="top"><p>IT is now a settled axiom in economics that a precondition for sustainable economic growth is macroeconomic stability. Over the years, Pakistan has learnt this the hard way. Most recently, after crawling back up to what should be its normal 'Pakistani' growth rate of six per cent, it now finds even this target threatened with an unsustainable current account deficit and mounting debt repayments.<br>
		It is also by now an accepted economic axiom that Pakistan's problems are not just cyclical but stem from severe structural economic imbalances, which, unless remedied, make sustainable growth impossible. That these can be solved by just adjusting the exchange rate or opening the economy further is part of a neoliberal fairy tale that only
