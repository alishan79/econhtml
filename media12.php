		<br>
		
		<p>The hard truth is that, while some price adjustments are needed periodically and should be made in a timely fashion rather than forever delayed (as in the case of the current exchange rate), for fundamental structural imbalances to be corrected we would need far-reaching changes in our current economic thinking, policies and development plans for the future. Given that the Planning Commission has now embarked upon drawing the Outline Paper for the 12th Five-Year Plan (2018-23), it is an opportune time to start this process.<br><br>
Any such exercise must start by accepting the harsh reality that, despite a not unimpressive growth performance (5pc over 70 years), an emerging middle class and significant reduction in extreme poverty, most people in Pakistan believe that there has been little improvement in their living standards and that poverty is rampant. There is also an overwhelming belief that Pakistan's economy is controlled and run by 'mafias', both private and public, who not only dominate the economy but can steamroll policies to their advantage largely through corrupt and illegal means.<br><br>
Even if this perception is exaggerated, it is still a damaging indictment of our existing economic system and past economic performance. It means that most people believe that the system is unfair and rigged in favour of a few - and that it is near impossible to actively participate in, and gain from, the growth and development process.<br><br>
Clearly, it is not just Pakistan's economic planners who must confront this problem but, more importantly, its political and economic elite. Is this an issue that either one can tackle? Yes, if a progressive political leadership were to gain power and undertake some of the following important measures.<br><br>
First, break the unfair monopoly of certain groups and institutions - whether in land, real estate or product markets or services - by coming down hard on unfair and corrupt market practices. This will require a much more active and empowered anti-monopoly authority than the present Competition Commission. At the same time, barriers to new entrants must be removed. For this, the banking system needs to be reformed, including breaking the current strong linkages between financial and industrial capital.<br><br>
The second is to reverse the economy's premature de-industrialisation, which is responsible for Pakistan's low total factor productivity and low levels of manufactured exports. We need to cut through the current debate between the neoliberals, supported by the World Bank, who claim that the economy is amongst the most protected, and the structuralists who argue that we have foolishly opened up the economy by slashing tariffs, entered into free-trade agreements and accepted smuggling, all of which have combined to destroy our industrial capacity and throttle future growth.<br><br>
The truth is, there are sectors that have been starved of imported raw materials and there are sectors that have been trampled by unfair foreign competition. What we need is an industrial policy that tackles both problems together. It has been done by the East Asian economies with time-bound and conditional support to steer industry to higher value-added products. Technology upgradation will be the key to our industrial revival.<br><br>
Third, we must prioritise the creation of more and better 'decent jobs' to meet the aspirations of a young, fast-growing labour force and reduce poverty by instituting an efficient, equitable and rights-based labour market. Job generation must be embedded in the 12th Five Year Plan with policies and sectors clearly identified that will generate much-needed additional employment given our continuing high growth rate of population.<br><br>
Employability here would be the key to ensure productive and remunerative employment through investment in education, skills development and acquisition of necessary social skills for teamwork. There is a growing sense of alienation amongst the working people, and this can only be overcome by providing them a voice at work through their democratically elected representatives, and rebuilding labour market institutions that provide necessary flexibility to employers as well as ensure fair wages and stable jobs to workers.<br><br>
Fourth, we must urgently stop the unfettered freedom that we have allowed nationals to transfer foreign exchange abroad, whether acquired legally or otherwise, and hollow out the economy through siphoning off much of the re-investible surplus. Of course, this will not be an easy task. Pakistan has run a virtually open capital account for almost 25 years, which no other developing country, including India, has done. Although it will take some deft handling by the State Bank, we must gradually institute measures to ensure that at least ill-gotten wealth and illegal transfers are curtailed.<br><br>
Finally, and most importantly to carry out these reforms, we would need in tandem strong judicial action to ensure that those who break the law and corrupt the system to build their wealth are justly and timely punished.<br><br>
The policy and systemic changes suggested here are undoubtedly challenging. But simply reviving growth without questioning its content, direction and distribution will only rebuild the same economic structure that is being strongly questioned - and rejected - by the people of this country. Giving more of the same will not work this time.

