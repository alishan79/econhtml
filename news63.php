		<h3>Thirteenth International Conference on Management of the Pakistan Economy
"Igniting Technology led Growth in Pakistan: Role of Monetary,
Fiscal and Investment Policies"
30 March, 2017 (Day 2)
</h3><br>
		<div align="center"><img src="images/thirteen.png" width="450px"></div>		
		
		<p>The second day of the Conference opened with a session titled "Openness, Economic Growth and Firm Level Productivity". The session was chaired by Matthew McCartney (Director of South Asian Studies; Associate Professor in the Political Economy and Human Development of India, University of Oxford, UK).</p>
		
		<div align="center"><img src="images/thirteen2.png" width="450px"></div>	
		<p>The session started with the paper titled, "Pakistan's Experience with the Pakistan-Chine Free Trade Agreement: Lessons for CPEC". In this paper, Dr. Azam Chaudhry (Professor of Economics at the Lahore School and the Dean of the Economics Faculty) and his co-authors Dr. Theresa Chaudhry (Professor of Economics, Lahore School of Economics) and Nida Jamil (Teaching Fellow, Lahore School of Economics), provided fresh insights on Pakistan's experience with the Pakistan-China Free Trade Agreement (FTA) to draw policy related conclusions for CPEC related initiatives.  They tested the impact of the last major economic agreement between the two countries, which was the 2006 Pakistan-China Free Trade Agreement (FTA). The study found a significant impact of this trade agreement on the amount of trade between two countries however it also pointed out its suboptimal consequences in the context of Pakistan's growth strategy. </p>
		
<div align="center"><img src="images/thirteen3.png" width="450px"></div>	
		<p>More specifically, the authors found a positive relation between Pakistan-China FTA and imports from China and a negative relation of this trade agreement with the productivity of the firms and value added in these sectors with adverse consequences for the economy especially on the incentives of the producers in the country.</p>
		
		<div align="center"><img src="images/thirteen4.png" width="450px"></div>	
		<p>The authors also pointed out that though Pakistan has still not been given the same level of market access to China as the ASEAN countries  (since Chinese tariffs on Pakistani goods tend to be higher than those on goods from ASEAN countries), the level of exports in the sectors benefiting from lower Chinese tariffs is increasing (though it has not increased as a percentage of Chinese imports which means that as Chinese imports have grown, Pakistani exports have not been able to significantly benefit from this trend). Consequently, the higher level of exports in these potentially benefitting sectors has led to an increase in employment in these sectors.<br>
The rigorous analysis was followed by some interesting policy recommendations for CPEC related industrial initiatives such as gaining the same level of tariff concessions from China as received by the ASEAN countries, promoting joint ventures between the Pakistani and Chinese investors, making the conscious decisions which sector can lead to the greatest increase in value added and which sectors have the greatest potential to increase exports. And finally, developing a labour policy that enables the manufacturing sector to switch from low skilled to high-skilled labour. 
</p>

<div align="center"><img src="images/thirteen5.png" width="450px"></div>	
		<p>The second paper in this session by Dr. Tayyab Shabbir (Professor of Finance at Wharton School, University of Pennsylvania) presented its finding on the effects of foreign capital inflows on economic growth in Pakistan. In order to build the argument, it started with a discussion of endogenous growth models to conceptualize the impact of FDI on economic growth. It stressed that subjects of foreign capital inflows and economic growth require tailor made solutions within the context of Pakistan's economy. The author identified some insightful areas for future research for example, relationship between micro level (firm level) and macro level variables in order to boost foreign capital flows in the country.<br>
The second session titled "Investment, Technology Upgradation and Job Creation" was chaired by, Dr Pervaiz Tahir. 
</p>
		
		<div align="center"><img src="images/thirteen6.png" width="450px"></div>	
		<p>The first paper in this session by Dr. Rajah Rasiah (Professor of International Development at the Faculty of Economics and Administration, University of Malaya) and Shujaat Mubarik (Associate Dean and Associate Professor, Faculty of Business Administration and Social Sciences, University of Malaya) focused on some main lessons from the experience of five east Asian countries namely South Korea, Indonesia, Malaysia, the Philippines and Thailand with regards to financing of technological up gradation in their economies. The analytical framework of the study emphasized sources and targets of investment, including changes in interest rates, government's role in investment promotion as well as technological upgradation. Five of the East Asian countries examined showed similarities and differences in how technological upgradation was financed over the period 1960-2014.The most successful, which is South Korea, relied heavily on financing capital (mainly infrastructure development) and technology accumulation. Technology accumulation in South Korea was heavily regulated in the formative years of chaebols operations to prevent rent dissipation. Such regulation was minimal in Malaysia. South Korean industrial firms have successfully experienced technological catch up and leapfrogging while others still lie behind the globe's technology frontier.
</p>

<div align="center"><img src="images/thirteen7.png" width="450px"></div>	
		<p>Dr. Hanns Pichler (Professor at University of Economics & Business Administration, Vienna) presented the second paper in this session, which studied investment behavior in Austria as a kind of regional central European case. The study showed that the structural change reflecting increased dynamics of immaterial investments with repercussions on otherwise overall sluggishness of investment (due to prevailing economic uncertainties, generally less conducive business climate with unfavorable framework conditions and overwhelming bureaucratic / administrative hurdles.) Furthermore, the analysis hinted the preference for business friendly tax policies / instruments. The study recommended a strong plea for business friendly economic policies / measures such as placing emphasis on structural tax reforms and reducing bureaucratic / administrative burdens.  
</p>
		
		<div align="center"><img src="images/thirteen8.png" width="450px"></div>	
		<p>Ms. Sadia Hussain (Teaching and Research Fellow at Lahore School of Economics) concluded the session with a presentation on her co-authored work with Ms. Farah Said (Assistant Professor and Research Fellow, Lahore School of Economics). This paper examined and identified the determinants necessary to survive in agriculture for small and marginal farmers across all four provinces of the country. By employing Pakistan Panel Household Survey (2001, 2010), the paper empirically investigated the difference between the welfare indicators in the diversified households and specialized households. The results showed that the diversified households experience a significant increase in consumption and income in contrast to specialized households. Furthermore, an interesting aspect of their findings was pointing out the role of public investment in promoting synergies across agricultural and non-agricultural sectors. The authors suggested that the agricultural policies should be geared towards promoting non-farm activities through commerce, growth in services and manufacturing.<br>
The third session of day 2, which was the last technical session of the conference, titled "Strengthening Institutions and Improving Governance for Equitable Growth"  was chaired by, Dr. Rajah Rasiah.

</p>

<div align="center"><img src="images/thirteen9.png" width="450px"></div>	
		<p>The first paper in this session was presented by Dr. Mehak Ejaz (Assistant Professor of Social Sciences Department at SZABIST). Her work with Dr Kalim Hyder (Senior Economist, Monetary Policy department, State Bank of Pakistan) analyzed the presumption whether some groups are more vulnerable to business cycle shocks than others, and found that relative to males, females face higher risks when exposed to shocks. The index-based analysis revealed that females are the most vulnerable part of the society. Further, the new entrants and about to retire workers are more vulnerable to economic shocks. Similarly, while low educated females are less vulnerable to the economic shocks, graduate and higher educated females are more vulnerable. Male earners residing in Baluchistan province are the most vulnerable segment in case of males. The vulnerability computed by income class wise indicates the similar type of groups. However, high-income earning females are more vulnerable as compared to low income earning females in case of Sindh province. In case of employment status, higher income class female earners in the category of paid employment are more vulnerable than lower income earner in the same category. Age cohort wise analysis suggests that vulnerability of the earners is higher in low income earner young females but with the higher age cohort the vulnerability situation reversed. The study suggested that the best solution to tackle this issue is to work on the financial innovations in the transfer payment system. 
</p>
		
		<div align="center"><img src="images/thirteen10.png" width="450px"></div>	
		<p>Dr. Kumail Rizvi (Associate Professor of Finance at Lahore School of Economics) presented his co-authored paper with Bushra Naqvi (Assistant Professor, Suleman Dawood School of Business, Lahore University of Management Sciences) and Fatima Tanveer (Research Associate, Lahore School of Economics). The authors suggested the importance of understanding the mobile banking success story for promoting financial inclusion and growth in Pakistan. Their study attempted to trace and compile the history of mobile banking in Pakistan. The study documented various mobile models and assesses its current state using available data to understand how the segment has been evolving overtime and transforming the conventional banking structure in the country. Finally, the paper touched upon the so called ecosystem that needs to be built in Pakistan to utilize the full potential of mobile technology and makes some recommendations on how to move forward in that direction. 
</p>
		
<div align="center"><img src="images/thirteen11.png" width="450px"></div>	
		<p>Day 2 of the conference ended with concluding remarks from Dr. Rashid Amjad (Professor of Economics and Director, Graduate Institute of Development Studies, Lahore School of Economics) and Dr. Azam Chaudhry (Professor of Economics at the Lahore School and the Dean of the Economics Faculty). Dr. Rashid Amjad provided a comprehensive summary of the key messages, which emerged during the two-day conference. He said that the conference has come up with several policy relevant ideas that can help policymakers to devise measures for propelling the economy onto a higher growth trajectory through modern technology. Dr. Azam Chaudhry thanked the participants for their valuable contributions which have enlightened the academic community as well as policy practitioners. 
</p>
		
		
		
