	<h3>Book launch - The Pakistani Diaspora</h3><br>
	
	<div align="center"><img src="images/book1.png"></div><br>
	
	<p>Center on International Migration, Remittances, and Diaspora (CIMRAD) organized a Book launch 'The Pakistani Diaspora: Corridors of Opportunity and Uncertainty' on 8th of December, 2017 at the Lahore School of Economics. <br><br>
Rector Lahore School of Economics Dr. Shahid Amjad Chaudhry, Professor of Economics, Director CIMRAD Dr. Rashid Amjad, Professor Dr. Nasra Shah from University of the Kuwait, and Professor Dr Fareeha Zafar from LSE, Representative of the World Bank Dr Abdul Waheed and chief guest former State Bank governor Dr Ishrat Husain were present on the occasion.
</p>
	
	<div align="center"><img src="images/book2.png"></div><br>
	
		<p>While addressing the ceremony, Dr Ishrat Hussain said that the book was being published at a time of mounting debate over the future of international migration and ensuring anxiety among migrants. He added that the book was just the beginning of an era where we should review our policies regarding diaspora. He said that the second generation of Pakistanis in US were just like Americans. However, they are still giving charity to the social projects like Shakat Khanum Memorial Cancer Hospital or they are buying property for their families. <br><br>
While sharing some facts, he said that in 1980s, on average 7,000 Pakistani students resided in the US in contrast to 28,000 Indians. However, post 9/11, these number are 5,000 Pakistanis and 13,000 Indians. The Indians hold key jobs in media industry, IT industry and the Silicon Valley. They have their lobby in US Congress as well and hat they also influenced US policies for South Asia.<br><br>
Dr. Ishrat also appreciated that Lahore School of Economics was a pioneer in bringing migration and diaspora studies into light. He concluded by encouraging academics to focus panel and primary data surveys to get more deeper insights into the scope of the migration and diaspora studies. 


</p>
			<div align="center"><img src="images/book3.png"></div><br>
			
			<p>Dr. Rashid Amjad, in his address, said that the book was unique in many respects. It does not merely analyze the growth of diaspora in a historical context, but also provides valuable insights into the cost and benefits of migration, social remittances, and engagement of their families in the new host country and the families they leave behind. He also said besides migration towards other countries, migration had been constant in 56 districts of the country, especially Karachi, central Punjab and few districts of Khyber Pakhtunkhwa and Baluchistan.<br><br>
A large number of people face losses in the struggle to migrate to foreign countries. Dozens are killed on their way to Greece. They imprisoned in different countries usually celebrate diaspora day and discuss issues being faced by migrants but no one in our country thinks about this. Dr. Amjad also highlighted that the State Bank of Pakistan had written to the Finance Ministry that it wanted to start saving bonds for overseas Pakistanis.<br><br>
Dr. Fareeha Zafar in her speech reflected upon the social side of diaspora represents a close interaction between the host and the home country. Dr. Zafar added that due to better transport facilities, third and fourth generation Pakistani diaspora was visiting their mother country.<br><br>
Dr. Nasra Shah said that the trends of migration were changing and a downturn had already begun in the Gulf. She suggested that policies should be devised for easing re-absorption of returnees in the home country.
</p>
	
		
			<div align="center"><img src="images/book4.png"></div><br>
			
			<p>Dr. Shahid Amjad Chaudhry, rector of the Lahore School of Economics, in his concluding remarks, suggested that Pakistan must develop safe and secure mechanisms to foster foreign investments and trade in Pakistan.</p>
			
