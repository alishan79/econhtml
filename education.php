		<br>
		
		<h3>Higher education, languages, and the persistence of inequitable structures for working-class women in Pakistan</h3>
		<p><b>Researcher: </b>Tayyaba Tamim (Lahore School)</p>
		
		<p>
		Higher education has been much emphasised as a source of empowerment by the government of Pakistan, following international development agendas. However, this paper argues that the empowering effect of higher education on women in Pakistan (see Malik and Courtney 2011Malik, S. and Courtney, K. 2011. Higher education and women's empowerment in Pakistan. Gender and Education, 23(1): 29-45. (doi:10.1080/09540251003674071)[Taylor & Francis Online], [Web of Science ®], [Google Scholar]; Sameena 2005Sameena, Y. 2005. Impact of higher education in understanding of social recognition in women. PhD thesis, University of Arid Agriculture. <br><br>Pakistan research repository Rawalpindi.http://.hec.gov.pk/98/(accessed October 1, 2011) [Google Scholar]) might pertain to only middle-class or a fraction of lower-middle-class women. A capability-based evaluation of the higher education experiences of female participants in Pakistan, belonging to the middle, lower middle and working classes (a division based primarily on household income differentials), entering higher education from private and government schools reveals that the language used to impart higher education along with language-based practices act as gatekeepers which exclude and marginalise those who do not possess the dominant valued language. In this the most disadvantaged of all emerge to be the working-class females who are the most severely excluded.<br><br>
Developing the argument from a multidisciplinary educational research in the context of Pakistan, this paper contends that gender and social class are intrinsically interwoven and best understood in relation to each other (Moore 2004Moore, R. 2004. Education and society: Issues and explanations in the sociology of education, Cambridge: Polity. [Google Scholar]). Unless we reject an essentialist approach to gender, intra-group differences remain concealed and 'matriarchal inequities', i.e. the unjust treatment 'perpetuated by women against women' (Stone 1994Stone, L. 1994. "Introducing education feminism". In Education feminism reader, Edited by: Stone, L. 1-16. London: Routledge. [Google Scholar], 5), remain hidden. Following Bourdieu (1991Bourdieu, P. 1991. Language and symbolic power, Cambridge, MA: Harvard University Press. [Google Scholar]), the study dismisses the cultural/linguistic deficit theories of educational disadvantage and questions the hierarchies of value, legitimised by institutional authority, which are structured to privilege the dominant class. The paper focuses on issues faced by working-class women in higher education when their access to English has been limited due to discriminatory schooling on the one hand and their institutionally acquired linguistic capital is devalued on the other. Higher education in this paper refers both to college and university education after the completion of secondary school in grade 10.<br><br>
This research is based on the findings of a 3-year, qualitative study funded by the Research Consortium on Educational Outcomes for Poverty. It uses Sen's [1985. Well-being agency and freedom. Journal of Philosophy 82, no. 4: 169-221] capability approach and Bourdieu's [1991. Language and symbolic power. Cambridge, MA: Harvard University Press] critical theory to argue that access participation and the empowering outcomes of higher education are contingent on learners' familiarity with the languages used. If there is a discrepancy between the languages used in higher education and the linguistic capital that learners have acquired during schooling without any appropriate measures to fill the gap, participation is bound to be limited. <br><br>
Findings of this qualitative multiple case study involving eight participants entering higher education from government and private schools in Pakistan reveal that, although the working-class women in the study may have achieved the same functioning as their middle-class counterparts in terms of completing at least 2 years of education, there was a vast difference in terms of their capabilities. Where the middle-class women learnt to unleash their potential, engage with knowledge structures, participate fully, and prepare for professional careers of their choosing because they possessed the linguistic capital valued at the higher education level, the working-class women remained marginalised both within and outside the classroom. They were deselected from the community of learning and forced to accept their low position through a reinforced sense of low self-worth and ability, which culminated in their delayed elimination from higher education. Since the working-class women were denied the use of the language that held the key to their identity, experience, knowledge, imagination, and creativity, they felt dismissed and devalued. The very institution of higher education that had promised them empowerment authorised their invisibility instead.<br><br>
This interdisciplinary study highlights the value of using capability approach which emphasises that despite the 'equality' of everyone being offered the same resources, text, class, and lectures in higher education, or the same 'functioning', i.e. completion of 2 years of college education the situation remains inequitable because of inequality in the space of 'capabilities' or 'freedom of opportunities' to access education and achieve valued goals. In this language choice in education- and language-based practices become both a site and a tool of perpetuating discrimination. This paper highlights this significance of language in structuring inequality and the need for detailed capability-based case studies along with numerical estimations to account for the inequities embedded within educational contexts that may affect working-class women's access to higher education. The paper also draws attention to the importance of exploring the intra-gender differences intertwined with social class in order to understand the issues of inequality.<br><br>
Although there are pragmatic constraints that necessitate the use of English in higher education in Pakistan's given situation, this paper highlights the need for adequate support systems for those from government school backgrounds, specifically working-class women who have the least resources available to them to learn the valued language because they tend to be restricted to their homes. However, unless the higher education language policy and practices accept and reflect the linguistic diversity of the given context, participation at this level will be limited. A capability approach-based evaluation emphasises that the notion of equality must be grounded in the reality of human diversity for developing more inclusive policies.<br><br>
Published Paper: <br> 
<a href="https://www.tandfonline.com/doi/full/10.1080/09540253.2012.752793?scroll=top&needAccess=true" target="_blank">Gender and Education, Volume 25, No. 2, pp 155-169, 2013.</a>


		</p>
	
