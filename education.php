		<h3>Determinants of School Choice: Evidence from Rural Punjab, Pakistan </h3>
		<p><b>Researcher: </b>Hamna Ahmed (Lahore School of Economics) and Sahar Amjad Sheikh (Lahore University of Management Sciences)</p>
		
		<p>
		The objective of this study is to understand why parents in rural areas of Punjab, Pakistan, choose to send their children to private schools when free public schools are available. The study utilizes the Privatization in Education Research Initiative (PERI) school choice dataset compiled by the Lahore School of Economics in collaboration with the Punjab Bureau of Statistics. These data provide rich information on parents' perception of their child's school relative to alternative schools he or she could have attended. The findings suggest that parents' perceptions play an important role in school choice. In particular, their perceptions of school quality and employment opportunities emerge as key determinants of private school choice. Additionally, expenditure on and access to private schooling relative to public schooling as well as the socioeconomic status of the household have a significant impact on parents' probability of choosing a private school for their child.<br><br>
Published Article: <br>
<a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%201/Ahmed%20and%20Sheikh.pdf" target="_blank">The Lahore Journal of Economics, Volume 19: 1 (Summer 2014): pp. 1-30.</a>


		</p>
	
