		<br>
		
		<h3>Education, Rent seeking and the Curse of Natural Resources</h3>
		<p><b>Researcher: </b>Waqar Ahmed Wadho (Lahore School)</p>
		
		<p>
		Recent empirical evidence suggests that relatively resource rich countries tend to have lower economic growth, higher corruption and lower level of education. In this article, we provide a theory behind this evidence where abundance of natural resources affects both corruption and education, which in turn determines the rate of growth. We have developed an endogenous growth model for an economy divided in two classes; the elite and workers. The former is a privileged class who has access to both education and the political process. Apart from the industrial sector, there is a natural resource sector that creates rents. The rents from natural resource sector accrue to the government, which attracts rent seeking. Profitable rent seeking requires time investment in political capital accumulation, which crowds out time invested in education.<br><br>
Our predictions in this article are in line with the empirical findings that the natural resources curse operates through the crowding out of productive capital and rent seeking. Furthermore, the relationship between abundance of natural resource and the resource curse is non-monotonic. We find that depending on the natural resource endowments, there can be three different growth regimes. There are endogenous thresholds of natural resource endowments that demarcate different equilibria. For low endowment of natural resources, there is a unique high-growth equilibrium with faster growth, higher education attainment, and no corruption, while for high endowment of natural resources, there is a unique poverty-trap equilibrium with no growth, no education attainment and very high corruption. In the intermediate ranges, there are multiple equilibria.<br><br>
The thresholds are endogenous and crucially depend on inequality in access to human and political capital accumulation and on the monetary cost associated with corruption. Increasing access to education and political participation would increase the ranges of resource endowments where the high-growth equilibrium exists.<br><br> Institutions play pivotal role for the determination of different growth regimes. For better quality institutions (i.e., higher cost of political participation), the range of natural resource abundance where high-growth equilibrium exists would be higher. Thus, for higher cost of political participation there are multiple equilibria, where for high abundance of natural resources, high-growth and poverty-trap equilibria coexist, and for the intermediate ranges of natural resource abundance, high-growth and low-growth equilibria coexist. This implies that the high abundance of natural resources can be consistent with the high growth equilibrium that is observed in the case of Norway, Botswana, etc. Whereas decrease in inequality in access to education and political process increases the range of natural resources where the high-growth is in equilibrium and decreases the rage of natural resources where the poverty-trap is an equilibrium.<br><br>
There is an important limitation in our model concerning the technology of natural resource sector. The assumption that the natural resource sector employs only unskilled workers can be suitable for some types of natural resources but may not be for the others. Second, this limits our analysis to see the impact of natural resources on income inequality. Clearly, when the society is segmented in two classes, abundance of natural resources may have redistributive implications as well. <br><br>Allowing for more flexible settings may help to see the redistributive implications of natural resources. Another possible extension of the model would be to allow for the possibility of unskilled workers protest and fight against rent seeker elites. This could potentially represent the conflicts observed in different resource rich countries and could bring some important insights about open or class conflicts. One possibility would be to model the workers coordination problem with some information sharing mechanism and the associated costs to unite and fight against corrupt elites.<br><br>
Published Paper: <br>

<a href="SSRN: https://ssrn.com/abstract=2382313 or http://dx.doi.org/10.1111/ecpo.12029" target="_blank">Wadho, Waqar Ahmed, Education, Rent Seeking and the Curse of Natural Resources (March 2014). Economics & Politics, Vol. 26, Issue 1, pp. 128-156, 2014. </a>

		</p>
