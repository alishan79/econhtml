		<h3>Public Vs Private Schooling Choice at the Household Level </h3>
		<p><b>Researcher: </b>Masooma Habib, Hamna Ahmad and Sahar Amjad (Lahore School) </p>
		
		<p>
		With a grant from Open Society Institute (OSI) Privatization in Education Initiative (PERI), researchers undertook a study titled Determinants of School Choice - Evidence from Punjab, Pakistan. The study addressed the following overarching question: Why do parents choose low cost private schools when free public schools are available? The main emphasis of the study in answering this question was on the role of parents' perceptions in school choice while controlling for a range of child, household and school specific characteristics. The study was based on a sample of 1024 rural households in 64 clusters spanning over 8 tehsils and 7 districts of the Punjab. This project was completed in 2012. <br><br>
Findings of the study have been published in:
Ahmed, H., Amjad, S., & Habib, M. "Private Schooling: Determinants and Implications for Social Justice in Rural Punjab, Pakistan". I. Macpherson, S. Robertson, & G. Walford (Eds.), Education, Privatisation and Social Justice: Case studies from Africa, South Asia and South East Asia, Oxford: Symposium Books Ltd (2014).<br><br>

Ahmed, H., Amjad, S., Habib, M & Shah., S. A. "Determinants of School Choice: Evidence from Rural Punjab, Pakistan", CREB Working Paper No. 01-13, (2013) Centre for Research in Economics and Business, The Lahore School of Economics, revised 2013. 

		</p>
	
