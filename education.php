		<h3>Conditional-Cash Transfers for Girls' Secondary Schooling in Punjab: Outcomes</h3>
		<p><b>Researcher: </b>Theresa Chaudhry (Lahore School) and Mushfiq Mobarak (Yale University)</p>
		
		<p>
		The government of Punjab, starting in 2003, offered a conditional cash transfer (CCT) of Rs 200 per month, to girls in class 6 to 8 with minimum 80 percent attendance, in order to increase the educational attainment of girls in districts with less than 40 percent literacy.  The program was later was extended up to grade 10. <br><br>
Using data collected a survey of rural households in Punjab (supported by the British Academy and Lahore School of Economics), Dr. Theresa Chaudhry and Anam Masood (Lahore School) are analyzing the impacts of the Female Secondary School Stipend Program (FSSSP), a component of Punjab Education Sector Reform Program (PERSP) on school enrollment, middle and high school completion, marriage and fertility outcomes for eligible girls in rural areas of stipend recipient districts. <br><br>
This paper uses triple difference-in-difference analyses to show the effects of program after ten years of implementation. The relevant control groups in this study include elder sisters and/or cousins in stipend districts, girls of similar age, their elder sisters and/or cousins in non-stipend districts.<br><br>
Girls exposed to the program were more likely to remain in school if we look at a short (2003-2006) or medium (2003-2009) span of time, but there was no statistically significant impact on completion of middle or high school. There is suggestive evidence that girls exposed to the program were more likely to be engaged in early marriages and subsequently are younger at the birth of first child.<br><br>
These surprising findings will be explored further with the addition of a second household survey to the current sample.

		</p>
		
