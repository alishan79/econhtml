		<h3>Impact of Female Secondary School Stipend Program on Enrollment, Marriage and Fertility Outcomes in Rural Punjab</h3>
		<p><b>Researcher: </b>Anam Masood (2015)</p>
		
		<h5>Abstract</h5>
		
		<p>
		We study the effects of Female Secondary School Stipend Program (FSSSP), a component of Punjab Education Sector Reform Program (PERSP) on school enrollment, middle and high school completion, marriage and fertility outcomes for eligible girls in rural areas of stipend recipient districts. The relevant control groups in this study include elder sisters and/or cousins in stipend districts, girls of similar age, their elder sisters and/or cousins in non-stipend districts. This paper uses triple difference-in-difference analyses to show the effects of program after ten years of implementation. Girls exposed to the program were more likely to remain in school if we look at a short (2003-2006) or medium (2003-2009) span of time, but were less likely to complete middle or high school. There is suggestive evidence that girls exposed to the program were engaged in early marriages and subsequently are younger at the birth of first child. These findings provide critical information for policy makers in assessing efficiency and effectiveness of such developmental programs.<br><br>
		
