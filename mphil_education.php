		<a name="External"></a>
		<br>
		
		<h3>Effects of External Migration on School Enrollments accumulated Schooling and Dropouts in Punjab</h3>
		<p><b>Researcher: </b>Rabia Arif (2011)</p>
		
		<h5>Abstract</h5>
		
		<p>
		Abstract In developing countries, external migration tends to ease household income constraints because of the external remittances that are generated. Using data on the Punjab, this working paper attempts to determine whether the external migration of individuals in a household has a positive effect on children's schooling outcomes as measured by school enrolments, the accumulated level of schooling, number of days spent in school, and dropouts. We use historic migration rates to instrument for migration in an analysis of school outcomes for children aged 5-17, 5- 11, and 12-17 to determine which group is most affected by external migration. The results show that external migration has a significantly positive impact on the school enrolments of younger children, whereas the accumulated level of schooling among older children increases significantly if there is an external migrant in their households.<br><br>
Published Working Paper: <a href="http://www.creb.org.pk/uploads/Working%20Paper%20No.%2003-12%20complete.pdf" target="_blank">CREB Working Paper No. 03-20.</a>
 

		</p>

