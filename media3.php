		<h3>Locating Industries Along Key CPEC Routes</h3><br>

		
		
		<p>Transportation costs are one of the major factors affecting firms' location decision. Investment in modern transportation networks under the China Pakistan Economic Corridor (CPEC) can favorably affect regions by improving accessibility and reducing transportation costs. Given the advanced stage of CPEC's infrastructure development phase of CPEC, Pakistani policy makers need to now focus on a CPEC-related industrial policy focused on higher value added exports. This provides an opportunity for the government to identify such regions and encourage the concentration of manufacturing activity within them. This will also help in achieving structural transformation and generating economic growth by shifting resources from low value-added to high value-added activities <br><br>
	The researchers from the Lahore School of Economics attempted to identify suitable regions for locating Industrial Development with a special emphasis on CPEC. The purpose of this analysis is to inform industrial policy for Punjab by identifying different industries based on their growth and export potential and suggest suitable regions for locating these industries, particularly based on their proximity to the CPEC route. <br><br>
	After setting a suitable criteria, nine sectors were selected including Processed Food and Beverages, Wearing Apparel, Leather Products, Pharmaceutical Products, Industrial Chemicals, Plastic Products, Non-Electrical Machinery, Electrical Machinery and Supplies, Motor Vehicles and Autoparts. However, the results found indicated that distance from National Highways and/or Motorways has a significant impact on firms' location decision in the case of only 4 sectors including Processed Food and Beverages, Industrial Chemicals, Plastic Products, Electrical Machinery and Supplies.<br><br>
	A mapping exercise showed that some of the currently under construction, planned, or proposed national highways, expressways, and motorways will immediately benefit tehsil headquarters and areas in the vicinity around them in terms of reducing travel time. The key CPEC routes that can immediately bring benefits are: (i) CPEC Western Alignment (Burhan -Dera Ismail Khan), (ii) M-3 (Lahore -Abdul Hakeem), (iii) M-4 (Pindi Bhattian-Faisalabad -Multan), (iv) E-3 (Wazirabad-Pindi Bhattian), (v) E-4 (Faisalabad -Jhang-Khanewal), (vi) E-5 (Khanewal-Lodhran).<br><br>
	The detailed analysis showed the following results: First, of the two dedicated CPEC routes passing through Punjab, the Western Alignment Route is expected to significantly improve accessibility for some of the districts through which it will pass. Therefore, it presents an opportunity for the government to promote Industrial Chemicals, and Electrical Machinery and Supplies manufacturing in the Chakwal district.<br><br>
	Second, there is a room for active encouragement by the government of processed Food and Beverages, and Plastic products manufacturers to locate in the Southern districts of Khanewal, Vehari, and Bahawalpur which are expected to benefit from the construction of E-5 expressway.<br><br>
	Third, the Faisalabad district is expected to naturally attract Processed Food and Beverages, Plastic Products, and to an extent Industrial Chemical manufacturing industries once the M-3 gets completed and becomes fully operational.<br><br>
	An active industrial policy is needed that promotes the growth of the following industries in the mentioned areas:<br><br>

