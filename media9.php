		<h3>Strategic Tariff Reduction to Increase Pakistan Exports: Leveraging Pakistan's Trade Policy with respect to China</h3><br>

		
		<p>Pakistan has been facing a chronic trade deficit, with an urgent need to focus on boosting its exports. Thus, there is an urgent need for Pakistan to climb up the export ladder given the worsening trade deficit being faced by the country. The main problem for Pakistan in terms of exports has been its dependence on low value added agricultural and manufacturing goods. The Lahore School of Economics did a cross-country comparison of countries including Pakistan, Sri Lanka, India, Turkey and Bangladesh and found ways for the Pakistan government to reduce certain tariff (but not all tariffs) in order help Pakistan gain the most out of the Pakistan-China collaboration as well as to boost its exports.<br><br>
Even though Pakistan has signed many bilateral agreements in an attempt to grow its export market there will be very little impact on exports until it upgrades the products being exported and moves up the value chain by exporting higher value products. The comparison of the top export products for Pakistan and India in 2016 revealed that while low value products like textiles, clothing, cotton and fruits remain the top exporting products for Pakistan, India has significantly increased its exports in high value products. <br><br>
While Pakistan has reduced its tariff on the import of the intermediate goods, what remains striking is the massive reduction in the tariff rates on intermediate by other textile exporting countries in South Asia. Moreover, the import of intermediate inputs in these other countries has grown as compared to Pakistan. So, while the import of intermediate goods in Pakistan has increased over the last few years, the growth is significantly less than other south Asian countries that export textiles. In Pakistan, textiles, wood, rubber and plastic, glass, chemicals, electrical appliances and metal are specific sectors which are significantly affected by lower intermediate input tariffs. <br><br>
In the textile sector, there are at least 10 intermediate goods which are extremely important for tariff reductions. At the same time there are 22 goods made in Pakistan that are used as intermediate goods for the textile sector which are higher quality and where there is no need to reduce tariffs.<br><br>
Overall, tariff reductions on imported intermediate inputs from China are a key channel through high quality and low priced intermediate goods can be made available to the Pakistani exporters. So what is needed is a policy where tariff rare reduced only on the higher quality imported intermediate goods and that the government should focus specifically on reducing intermediate input tariffs in certain sectors (textile, wood, rubber and plastic, glass, chemicals, electrical appliances).
<br><br> 
This research was carried out by Ms. Rabia Arif, Assistant Professor & Research Fellow and Ms. Nida Jamil, Assistant Professor & Research Fellow, Lahore School of Economics.


