		<h3>Innovation Needed to Help Struggling Pakistani Textile Sector</h3><br>

		<p>The textile sector is the largest manufacturing sector in Pakistan, contributing one-fourth to total industrial value added, employing 40 percent of the industrial labor force, and making up 56 percent of national exports. Pakistani textile exports increased from US$ 10.7 billion in 2005 to US$ 14 billion in 2014. However, as compared to the rest of the world, the textile sector in Pakistan has stagnated and its share in world textiles exports has been falling throughout the last decade. Thus, in order to sustain its market share in World trade, the sector needs a continuous flow of innovation, according to research by the Lahore School of Economics. </p>
		
		<p>A recent analysis of the dynamics of firm level innovation activities conducted by the Lahore School of Economics, used a sample of 431 Pakistani textile and apparel manufacturers, and found that 56 percent of the surveyed firms introduced technological and/or non-technological innovations during this period. Also, 38 percent of firms introduced new products through these were incremental in nature since the majority of those products were only new to the firm and not new to the market. There were also six enterprises who introduced products that were first in the world and all six firms were from Sialkot.</p>
		<p>The researchers also found that innovation increases with firm size; large firms have an innovation rate of 83 percent, followed by medium sized firms (68 percent) and small sized firms (39 percent). Technologically innovative firms were found to be spending 10 percent of their turnover in 2015 on innovation mainly for acquiring newer vintages of capital with the purpose of introducing new/improved products and processes. Lack of availability of funds within the enterprise was found to be the single most important cost factor hampering innovation. Also, firms were found to be more focused on innovation than promoting growth. The economic importance of innovations seems very high as the research findings showed that 67 percent of the turnover of innovative firms in 2015 resulted from product innovations that were either new to the market or new to the firm. </p>
		<p>This study was funded by the Lahore School of Economics and the International Growth Centre (IGC). Researchers included Dr. Azam Chaudhry, Professor and Dean, Lahore School of Economics and Dr. Waqar Wadho, Assistant Professor, Lahore School of Economics. The researchers explored the dynamics of firm's innovation activities by analyzing the innovation behavior, the extent and types of innovation, the resources devoted to innovation activities, sources of knowledge spillovers, factors hampering technological innovation, and the returns to innovation for three years 2013-2015.<br><br>	
		
