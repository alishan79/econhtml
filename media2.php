		<h3>The BoP crisis </h3><br>

		
		
		<p>I argued two weeks back, addressing the incoming Prime Minister, because I thought it was a matter of pith and urgency that Pakistan's BOP crisis was structural and could not be solved through a price fix of devaluation as past advice and economic management had committed us to. And will no doubt attempt to again.
<br><br>
The pithy problem lies in understanding that there is an impossibility between two macro policies, the one relying on depreciation of the exchange rate to fix the BoP crisis, and the second maintaining open unregulated capital accounts allowing free flow of capital in and out of the country. They work against each other.<br><br>

The urgency lies in estimates we have made of capital outflows at the Lahore School of Economics, which are exceedingly high, and must be regulated. Or else the BoP crisis will worsen, and without which short run fix, there will be no long run economic agenda to implement.<br><br>

Restating the pithy problem first. Essentially, over the past five years, Pakistan's exports have flatlined, inching up to barely $31billion this year ending in June 2018. While imports have exploded, coming up to $63 billion this year. Which gives a trade gap of $32 billion, compounded further by deficits in services and income accounts. Record remittances struggle to bring this vast deficit in our Current Account (C/A) down to $18 billion this year.<br><br>

One solution to this mounting trade and services gap, is considered to be devaluation of the exchange rate, making our exports cheaper and raising them, while making our imports dearer and lowering them. We have had a 20% depreciation of the Rupee in the past five years, with no obvious impact on either exports or imports. Again, our research shows that Pakistan's exports are extremely sluggish to such price signals. Therefore logically, the policy to raise them should be based on raising competitiveness through enhancing productivity.<br><br>

But much expert opinion and most multilateral advice persists in arguing for further devaluation. That meets Einstein's oft-quoted criteria of madness - repeating the same experiment but expecting different results.<br><br>

However, my problem with devaluation lies on the other side of BoP accounting, of immediately being able to pay for the current deficit. The C/A deficit of $18 billion has to be paid for through the Capital Account (KA). If current payments cannot be met, assets must be used to pay for them, so that the BoPs always balance.<br><br>

The KA comprises three large sources to repay for the C/A deficit. The largest source should be assets owned abroad, but Pakistan has gone from a near zero balance on these assets five years ago, to liabilities, that is having to borrow $9 billion last year and $7 billion this year and counting. Raising our cumulated borrowing or debt, by near $20 billion over the past five years, to its current level of $64 billion.<br><br>

The second largest source of repayments for the C/A deficit has unfortunately come to be running down our reserves. This reliance too has risen, from using up $3 billion five years ago, to $7 billion this year and counting. Lowering our current reserves from $18 billion last year to $10 billion now, worth only two months of our import bill.<br><br>

Since these two sources of repayments, borrowings and running down reserves are negatives to be minimized, policy has to focus on a third source of repayments, net flows of capital in and out of the country.<br><br>

Net inflows for 2016-17 for which there is full data, have been about $2.5 billion. Gross outflows, are less easily available from the State Bank's standard accounting. But with their great help, we estimate these as $5.5billion for 2016-17.So with an unregulated capital market, called an open capital account, we have half the money coming in as goes out. Two years of these outflows would be worth an IMF SBA loan.<br><br>

So why is our open capital account hemorrhaging such huge outflows. Two reasons.<br><br>

One, devaluation of the Rupee versus say the US Dollar (USD), increases the rate of return on investment abroad in USD terms. Making it perfectly rational behavior to bet against the Rupee and invest abroad. Which of course increases capital outflows, and further weakens the Rupee, weakening our Capital Account and ability to pay for our mounting Current Account Deficit.<br><br>

But the ability to increase outflows at will, is only enabled by an unregulated open capital account. So if there has to be a policy of depreciation, it cannot be accompanied by an open capital account, allowing unrestricted capital outflows. Ask China, whose policy of depreciation led to such huge capital outflows paid for by a loss in reserves of $1 trillion, until it clamped down on the outflows.<br><br>

As Pakistan must urgently regulate its open account capital market to reduce capital outflows. An open capital account is an economic impossibility if accompanying a policy of devaluation.<br><br>

There is a second contributory factor leading to such large and increasing outflows over time. Capital will flow to its highest rate of return, given open capital accounts. A pegged exchange rate like Pakistan's, with a weakening BoP position, is difficult to maintain, requiring a running down of reserves, selling Dollars to buy Rupees, to bolster demand for the Rupee and the peg. The alternative to running down reserves is to raise the domestic interest rate. As the domestic rate of return rises relative to the foreign rate of return, capital inflows should increase and outflows decrease, strengthening the pegged exchange rate. That policy option has not been followed. Over the last five years our inter-bank lending rate, Kibor has halved, from 12.4% in 2011 to 6.5% in 2017, or 7.5% currently.<br><br>

I am not in disagreement with keeping domestic interest rates and cost of borrowing low, to raise Pakistan's very low investment rate above its current 16%. But then we will have to rely more on regulating our open capital accounts.<br><br>

So in fact there is a double impossibility bind on Pakistan's exchange rate. If it is to be devalued, it will not find an equilibrium value and keep falling, because it will not close the trade gap on the Current Account side, and weaken payability on the Capital Account side by raising outflows. So the solution has to be to regulate outflows and open capital accounts.<br><br>

Alternatively if the pegged exchange rate has to be maintained, it will require increasing interest rates to reduce outflows, jeopardizing investment and growth. So the solution again has to be to regulate outflows and open capital accounts.<br><br>

Ergo, policymakers please 'heed the impossibility of devaluation fixes without capital control fixes'. The electoral mandate says the people are fed up of paying the price of, at best bad economics, or at worst venality led economics of elites able to enact policies that bet against the Rupee and the economy. The people earn their hard won income in this economy and consume and invest their little all here. The elites earn their income in this economy and consume and invest much of it abroad. That kind of criminal macroeconomics even countries with trillion Dollar reserves and 40% investment rates cannot afford. How can Pakistan? Welcome to the real crisis.<br>

</p>
	
	
