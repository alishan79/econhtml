		<h3>Lahore School of Economics and World Bank Conference on Poverty</h3>	<br>	
		<p>The Lahore School of Economics and the World Bank jointly organized a brainstorming session on poverty measurement and policy at the Main Campus on 17 April 2015. The session was intended to get an open discussion initiated on issues in the measurement of poverty, and its implications for policy, and to facilitate the formation of an independent expert group to guide the process of arriving at a credible methodology and process for measuring and monitoring poverty.</p>

		<div align="center"><img src="images/n3.jpg" width="450px"></div>

		<p>Opening comments were made by Dr. Shahid Amjad Chaudhry, Rector, Lahore School of Economics; Mr. Asif Bajwa, Chief Statistician, Pakistan Bureau of Statistics; and Dr. Naeemuz Zafar, Ministry of Planning Development and Reform. Following that, Dr. Ghazala Mansuri, Lead Research Economist at The World Bank gave a presentation on "Poverty debate in Pakistan: What accounts for the disconnect between the data and perceptions?" This raised issues for discussion on lack of confidence in the quality of official surveys such as the Pakistan Social and Living Standards Measurement Survey (PSLM) and the Household Income and Expenditure Survey (HIES), lack of confidence in the official consumer price index as a measure of inflation faced by the poor, especially in rural areas, concerns about the basket of goods and the calorie threshold used for estimating the poverty line, and the method used for tracking trends.</p>
		<p>In response to Dr. Mansuri's presentation, Dr. Naved Hamid, Professor and Director, Center for Research in Economics and Business (CREB), Lahore School of Economics, moderated the discussion that welcomed expert opinion and recommendations from distinguished poverty experts, academics and policy makers.</p>
		
