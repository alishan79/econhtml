	<h3>The Lahore School of Economics' Fourteenth International Annual Conference on Management of the Pakistan Economy:  Accelerating Economic Growth in Pakistan: Key Macro and Sectoral Drivers (Day 1)</h3><br>
	
	<p>The Lahore School of Economics' Fourteenth International Annual Conference on Management of the Pakistan Economy started at Burki Campus on the 28 March, 2018. The theme of this year's Conference is "Accelerating Economic Growth in Pakistan: Key Macro and Sectoral Drivers". In addition to keynote lectures by renowned international scholars, the conference includes eight sessions highlighting some of the most important areas of research pertinent to Pakistan's Economy. The regular sessions during the conference feature twenty five papers along with commentary from distinguished economists detailing the cutting edge research centered around macroeconomic stability, poverty, CPEC, industrial strategy, role of trade, change in the financial services industry and agriculture growth in Pakistan.</p>
	
	<div align="center"><img src="images/drivers1.png"></div><br>
	
	<p>The conference opened with welcome remarks by Dr. Shahid Amjad Chaudhry (Rector, Lahore School of Economics). He mentioned that the two-day event will include scholarly discourse on key macro and sectoral drivers of economic growth that will generate insights on the short run initiatives as well as structural and institutional measures designed to boost long run economic growth in Pakistan. He remarked that the issues the conference aims to address are very relevant to the challenges confronted by Pakistan's economy at present. This conference will be an important contribution to the debate over key issues faced by the country and what can be done to address these. Highlighting the conference presentations, Dr. Chaudhry said that the papers on day one of the conference will provide the analytical basis for addressing burning issues faced by the country based on rigorous research on macroeconomic stability, poverty and CPEC. The day two of the conference will feature the industrial strategy, role of trade policy innovation and agriculture sector.

</p>
	
		<div align="center"><img src="images/drivers2.png"></div><br>

	<p>He added that more concerted efforts are needed to leverage the opportunities under CPEC by partnering in regional value chains with Chinese industries. By instituting these first steps, the government can begin the arduous task of reversing Pakistan's growth decline. He further stressed the need to study the 7th NFC award, 18th Amendment and foreign illegal assets and to provide recommendations on how to restructure tax amnesty, as well as how to reduce the tax leakages. Also the elections are around the corner, and in this regard short run policies are needed to revive economic growth.<br>
	With these welcoming remarks of Dr. Chaudhry the first session began with a fascinating discussion on "Macroeconomic Stability and Sustainable Growth, which was chaired by Shamas Ul Hassan (Deputy Governor IB, DFG & IT).
	</p>
	<a name="drrashid"></a>
		<div align="center"><img src="images/drivers3.png"></div><br>
	
	
	<p>Dr Rashid Amjad (Professor of Economics and Director, Graduate Institute of Development Studies, Lahore School of Economics) began the session with his presentation on "Redesigning Macroeconomic Policy: Breaking out of Pakistan's Impossible Trinity". This important research provided an explanation about the impossible trinity and challenge of maintaining a balance between the fiscal deficit, the rate of economic growth and the external account deficit. The paper addressed and identified policy measures needed in the short, medium and long term for Pakistan to break out of the impossible trinity and to move to a sustainable and higher growth path. The discussion hinted that essentially, to move the economy from low growth equilibrium to a higher growth rate, the country's economic managers face the challenge to prioritize between balancing the current fiscal deficit, maintaining a fixed exchange rate and achieving a rising economic growth rate. </p>
	
			<div align="center"><img src="images/drivers4.png"></div><br>
			
			<p>The second paper in the session was presented by Sakib Sherani (CEO Macro Economic Insights (Pvt) Ltd and Ex-Principal Economic Adviser, Ministry of Finance). His discussion centred on the country's declining export receipts, which has been compounded by a deep-seated anti-export bias in policies as well as attitudes. Overall, and barring a few areas, Pakistan fares poorly with regard to productivity, especially in comparison to many of its regional competitors. He remarked that there are few channels via which politics - domestic, regional as well as global - have undermined exports from Pakistan. For the past few decades, 'capture' of Pakistan's trade policy framework by a segment of low value-added exports has been a significant factor in preventing a diversification of export products as well as markets. The author suggested exchange rate flexibility as a viable policy tool.</p>
			
			<div align="center"><img src="images/drivers5.png"></div><br>
			
			<p>Almazia Shahzad (Assistant Director in the Development Finance Support Department) concluded the first session with her presentation titled "Capital Account Liberlization and Development in Pakistan". This paper explores Pakistan's experience of capital account liberalization and tries to assess how it has affected country's economic performance. A multivariate VAR model applied on quarterly macroeconomic data from 1990-2015, finds no significant impact of the policy on the economy. The paper identifies the substantial share of external debt, insufficient reforms in domestic financial sector and impact of capital flows through informal channels of money transfer as the probable reason for this outcome.</p>
			
			<div align="center"><img src="images/drivers6.png"></div><br>
			
			<p>The second session of the Conference chaired by Dr Rashid Amjad focused on "Poverty and Economic Growth in Pakistan". The first presentation, by Dr. Moazam Mahmood (Professor of Economics at the Lahore School of Economics) co-authored with Aimal Tanvir (Research Associate at Lahore School of Economics) entitled "The Macro Determinants of Poverty in Pakistan". The authors examined poverty and correlated it to the trajectory of macro policy variables. In their study they matched some macro variables with the long run poverty trends. In the study GDP doesn't consistently explain poverty reduction nor does the Gini index. Also remittances and inflation do not explain poverty rather inclusive growth such as growth which is poverty reducing explains poverty trends in the country.
 </p>
 
		 <div align="center"><img src="images/drivers7.png"></div><br>
		 
		 <p>Dr. Nasir Iqbal (Director Research at the Benazir Income Support Program BISP) presented his co-authored work with Dr. Saima Nawaz (Assistant Professor at the COMSATS Institute of Information Technology, Islamabad) titled "Social Safety Net Program - BISP in Pakistan: Effectiveness, Challenges and Future Agenda". The paper focused on impact assessment of cash transfers (CT) programs on poverty, education, health and productive assets. The paper evaluated the performance of BISP with reference to other renowned social safety nets in the world. The authors also explored the future plans of BISP in the light of Sustainable Development Goals (SDGs) 2030 agenda by utilizing desk review of impact evaluation and annual reports, payment and enrollment data, National Socio Economic Registry (NSER) and a structured questionnaire. </p>
		 
		 
		 <div align="center"><img src="images/drivers8.png"></div><br>
		 
		 <p>The second session was concluded by Dr. Ahmed M. Khalid (Professor of Economics, Unversiti Brunei Darussalam) with the presentation on "Informal Economy; Size (sectorial distribution), Trends and Implications for Growth". This study aimed to estimate the size of informal economy in the context of Pakistan. The paper provides a detailed review of the existing empirical literature on the subject of informal economy, especially in the context of Pakistan. Furthermore, the study investigates sectorial distribution of informal economy to get a better understanding of its impact on economic growth. The study used a variety of estimation techniques to check the robustness of estimates. </p>
		 
		 
		 <div align="center"><img src="images/drivers9.png"></div><br>
		 
		 <p>The third session of the Conference chaired by Dr Rashid Amjad focused on "Leveraging CPEC for Balanced Growth I". The session started with the lively and exciting discussion of Dr. Matthew McCartney (Director of Contemporary South Asian Studies Programme, Associate Professor in the Political Economy and Human Development of India) entitled "The China-Pakistan Economic Corridor (CPEC): Considering contemporary Pakistan through old-fashioned Economics and historical case studies".</p>
<p>
The discussion centered on the debate that there is a long tradition of research in South Asia that has criticised large infrastructure projects funded from overseas as being incompatible with the economic development of South Asia. This paper looks in detail at the $46 billion CPEC package of transport, energy and manufacturing projects and asks how we can analyse the impact of a transformative expansion of infrastructure. The paper argues that traditional methods based on social saving are inadequate for such an evaluation. Instead this paper uses the theoretical frameworks of the 'Leading Sector' and 'Unbalanced Growth' and the historical case studies of transformative infrastructure expansion in nineteenth century United States, Mexico, Germany and India to explore the conditions under which the CPEC could promote sustainable long-run economic development in contemporary Pakistan. 
</p>
		 
		 
		 <div align="center"><img src="images/drivers10.png"></div><br>
		 
		 <p>Dr. Sirimal Abeyratne (Professor in Economics at University of Colombo) presented his work titled "Managing Development with Chinese Investment: The Case of Sri Lanka". The paper argues that, in spite of popular rhetoric, the management of the internal economic affairs with policy and regulatory reforms has been in the heart of the issue, which needs to be addressed for improving the economic and political capacity to benefit from the country's locational advantage and emerging opportunities. </p>
		 
		 <div align="center"><img src="images/drivers11.png"></div><br>
		 
		 <p>The last paper of this session was presented by Dr Rajah Rasiah (Professor of International Development at the Asia Europe Institute, University of Malaya) entitled "How can Pakistan Benefit from the China-Pakistan Economic Corridor" co-authored with Shujaat Mubarik and Nazia Nazeer. The paper investigated the standard trade measures to examine the trends, and to assess the potential impact the highways connecting the Arabian Sea with the Chinese border in the North of Pakistan, and the industrial zones to be developed. The author concluded that while the immediate impact of CPEC is likely to deepen the existing asymmetric trade patterns between the two countries tilting further towards China, this paper focuses on how Pakistan can strategize its role in the relationship to appropriate greater economic synergies.</p>
		 
		  <div align="center"><img src="images/drivers12.png"></div><br>
		 
		 <p>The fourth session of the first day of conference focused on "Leveraging CPEC for Balanced Growth II". This session was chaired by Dr Navid Hamid (Director, CREB, Lahore School of Economics). The session started with Khalil Hamdani (Visiting Professor at the Graduate Institute of Development Studies, Lahore School of Economics and Former Director, UNCTAD) discussion on the paper titled "Maximizing the Impact of Chinese Investment in Pakistan". The discussion centered on how Pakistan, Chinese investment can accelerate economic growth in four ways. First, increased capital inflows will ease macroeconomic management and the balance of payments. Second, the infusion of investment and technology into infrastructure development will catalyse key productive sectors. Third, vibrant economic activity would attract investment from other countries, provided domestic investors lead the way. Fourth, the ensuing cross- border economic flows and enterprise supply chains will deepen integration in the global economy. The main finding of the paper hints that the impact of Chinese investment for the Pakistan economy will be positive, and could be substantial with a comprehensive and sustained policy effort. </p>
		 
		 <div align="center"><img src="images/drivers13.png"></div><br>
		 
		 <p>The second paper of the session entitled "Special Economic Zones (SEZs): A Comparative Analysis for CPEC SEZs in Pakistan" was presented by Cui Yong (Professor at Jiangsu University). The paper was co-authored with Muhammad Muzammil Zia, Hasnain Javed and Beenash Afzal Malik. The study investigated the assessment of socio-economic impacts of various Special economic zones SEZs in diverse regions via comparative analysis. The analysis indicates that overall, African SEZs have not led to significant job creation or poverty reduction because of failures in implementing proper regulations. However, Asian SEZs on the other hand, have shown many socio- economic benefits. The paper established that to underpin co-opt African experiences with SEZs in order to improve the Asian framework for the same zone-type setup as Pakistan. </p>
		 
		 
		 <div align="center"><img src="images/drivers14.png"></div><br>
		 
		 <p>The last paper of the session and day one of the conference entitled "Agriculture Development Options under China Pakistan Economic Corridor (CPEC)" was presented by Mahmood Ahmad (visiting researcher at Lahore University of Management Sciences), co-authored with Sana Khalid. The paper critically evaluated the policies and investment priorities perused in developing CPEC in general and agriculture and agro-industry in particular. The paper also highlighted and identified clusters of agriculture value chains in four corridor zones, especially central zone (Indus Basin) classified under CPEC project that carries good comparative advantage in producing a diversified crop mix that has not been fully exploited. In conclusion the authors mentioned that in order to translate this comparative advantage into competitive advantage, there is not only need to in adopting good global production and trade practices.</p>
		 
