		<h3>Pakistan to Record Highest Growth Rate in Nine Years: WB Report</h3><br>

		<p>According to the recent World Bank Report, Pakistan's Gross Domestic Product growth in Fiscal Year 2017 is expected to climb to 5.2 percent - the highest in nine years -- and the growth rate will continue to accelerate, reaching 5.5 percent in FY18 and 5.8 percent in FY19. </p>
        
        <div align="center"><img src="images/growth1.png" width="500px"></div><br>
	
    	<p>Released twice a year, the Pakistan Development Update sets out recent developments across the economy and identifies risks and opportunities in the near-term future before focusing on a handful of key development challenges. </p>
    
            <div align="center"><img src="images/growth2.png" width="500px"></div><br>

    	<p>The report was launched in Lahore on Saturday, 20th of May, 2017 in collaboration with the Lahore School of Economics.</p>
    
            <div align="center"><img src="images/growth3.png" width="500px"></div><br>


    	<p>Pakistan's growth will continue to benefit from growing consumer and investor confidence in the first half of FY17, following the successful efforts to restore macroeconomic stability during the last 4 years.  The report highlights that more recently, weakening trade and fiscal balances point toward the need to continue with the reform efforts to consolidate the hard won stability. <br>
"Pakistan's accelerating growth is good news and reflects the country's success in building confidence.  But the pace of reforms has slowed and it is important for the structural reforms to accelerate," said Illango Patchamuthu, World Bank Country Director for Pakistan. 
</p>
    
            <div align="center"><img src="images/growth4.png" width="500px"></div><br>


    	<p>A moderate increase in investment is expected to supplement growth, driven primarily by public and private consumption.  But the country's current account deficit and fiscal balances deteriorated in the first half of the financial year, which could affect the reform momentum.  The report emphasized the importance of collaboration between federal and provincial governments in delivering on the country's reform agenda. </p>
    
            <div align="center"><img src="images/growth5.png" width="500px"></div><br>


    	<p>"Since the 18th Constitutional Amendment, many important reform areas are now shared responsibility. Provinces have a significant role to play in fiscal consolidation, energy sector reforms and business environment, not to mention their role in delivering high quality health and education services," said Muhammad Waheed, World Bank Senior Economist for Pakistan. <i> "Effective collaboration between federal and provincial governments will be crucial if Pakistan has to deliver on its growth potential."</i></p>
    
            <div align="center"><img src="images/growth6.png" width="500px"></div><br>


		<p>The report discusses a number of challenges that the provincial governments are facing. Using Punjab as an example, it is highlighted that while provincial own-source revenues have significantly increased in recent years, there could have been a further significant gains, if tax policy and administration reforms were implemented. </p>
    
            <div align="center"><img src="images/growth7.png" width="500px"></div><br>



		<p>The World Bank also highlights the importance of the skilling of the youth bulge for better jobs. The Punjab Government is putting in efforts to significantly increase the scale of technical and vocational training programs in an attempt to boost job prospects for the province's 'youth bulge'.  Several next steps in this area are identified in the report including the application of quality standards and the introduction of initiatives to improve matching between jobseekers and prospective employers. </p>
        
    	
