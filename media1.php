		<h3>Handling the Balance of Payments Crisis in Pakistan </h3><br>

		
		
		<p>Once again, a serious BOP problem has arisen in Pakistan after a few years of higher growth. In this case, as in the previous crises, the problem may not simply be one of mismanagement but rather a structural issue relating to the composition of Pakistani exports. <br><br>
		
Research done by the Lahore School of Economics looks at the recent balance of payments (BOP) crisis in Pakistan. The argument presented by the researchers is that it is the higher GDP growth rate that causes the reoccurring BOP problem. The fundamental reasoning behind this is that there exists a structural weakness in the Pakistani economy in terms of the composition of Pakistani exports since higher growth is accompanied by significantly higher imports but only slightly higher exports. The reason for low export growth is that Pakistan is caught in a low value-added export production trap.<br><br>

The researchers find that there exists a BOP constrained maximum growth rate in Pakistan of approximately 5%. This means that every time the Pakistan growth rate goes above 5%, the current account significantly deteriorates. And this current account deterioration is historically caused by an unsustainable level of higher imports when the growth rates rise above the threshold level of 5%. The researchers also found that a change in the real exchange rate does not have a significant impact on the current account deficit. In terms of the economic theory behind this finding, the researchers found that the Marshall-Lerner condition is not satisfied in the Pakistani case, so a real devaluation cannot significantly reduce the current account deficit. The researchers conclude that the only way to get out of the current balance of payment (BOP) crisis in the short term is to reduce the GDP growth rate through fiscal and monetary contraction. In the long run, Pakistani policy makers need to develop an industrial strategy aimed at producing higher value added exports.<br><br>

The researchers also presented some key policy recommendations: First, with an effective exchange rate devaluation of approximately 20%, the overvaluation problem of the rupee has now been mostly resolved. So there is little economic justification for further devaluation. But they note that because of the inelasticity of imports and exports, the devaluation on its own will not solve the current balance of payments crisis. <br><br>

Second, only a sharp slowdown in the growth rate caused by a significant fiscal (and potentially monetary) contraction will stabilize the current account in the short run. Also, tariff increases on consumer goods, in particular luxury goods, should be strongly considered, to slow the currently unsustainable influx of imports.  In the long run, a well-defined industrial strategy is needed to help Pakistani manufacturers transition from lower value-added exports to higher value-added exports.<br><br>

Third, despite the fact that poverty in Pakistan has fallen over the last decade, the impending fiscal contraction has the potential to lead to a significant increase in poverty. The previous IMF program (which Pakistan entered into in 2013) included a substantial increase in social sector spending that focused on a sharp increase in spending on the Benazir Income Support program (BISP). The BISP allocation is currently at 0.4% of GDP with coverage across all of the provinces and the researchers strongly recommended that any homegrown or foreign-led stabilization program should have a significant social safety net component built into it.<br><br>

Finally, the researchers emphasized that any international borrowing done by the government be focused on lower rate interest borrowing sources as well as sources that support spending on social sector development. The Poverty Reduction and Growth Facility (PRGF) Program of the IMF, aimed at reducing poverty, is one possible option.<br><br>

This research was carried out by Dr. Azam Amjad Chaudhry, Professor and Dean, Faculty of Economics, Lahore School of Economics.
