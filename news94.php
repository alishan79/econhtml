	<h3>Talk on The West and the Rest in the World Economy by Dr. Deepak Nayyar</h3><br>
		<p>As part of the Distinguished Economist Seminar Series, the Lahore School Economics Society (LSES) organized a seminar on February 26, 2015. The guest speaker was a renowned Indian economist Dr. Deepak Nayyar who talked about "The West and the Rest in the World Economy: The Next Transformation" based on his recent book 'Catch Up: Developing Countries in the World Economy'. The seminar was attended by distinguished professors and economists including Dr. Shahid Amjad Chaudhry (Rector, Lahore School of Economics), Dr. Rashid Amjad (Director the Graduate Institute of Development Studies (GIDS)), Dr. Azam Chaudhry (Dean, Department of Economics, Lahore School of Economics), Dr. Naved Hamid (Director, Center for Research in Economics and Business) and Dr. Irfan-ul-Haque (Special Advisor for Financing for Development, South Centre, Geneva), faculty members of Lahore School and a large number of both graduate and undergraduate students of Lahore School.</p>		
		
		<div align="center"><img src="images/n15.jpg" width="450px"></div>		
		
		<p>Dr. Deepak in his talk provided a comprehensive overview of the evolution of developing countries in the world economy, situated in a long term historical perspective, from the onset of the second millennium but with a focus on the second half of the twentieth century and the first decade of the twenty-first century. He highlighted the overwhelming significance of what are now developing countries in the world until 200 years ago to trace their decline and fall from 1820 to 1950. Dr. Deepak emphasized that there is no single formula for growth and different sets of policies seem to have worked well in different settings, but economies relying on market as driving force have minimized market failures and those relying on government as central have minimized government failures. Also, growth has often not been transformed into meaningful development that improves the wellbeing of people. However, he optimistically asserted that the beginnings of a shift in the balance of power in the world economy are discernible and developing countries can sustain this rise only if they can transform themselves into inclusive societies where economic growth, human development and social progress move in tandem.

</p>

