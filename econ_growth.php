		<a name="Understanding"></a>
		<br>
		
		<h3>Understanding and Addressing Rural Poverty in Pakistan: Oxford University- Lahore School Collaboration </h3>
		<p><b>Researcher: </b>Naved Hamid, Azam Chaudhry, Theresa Chaudhry, Hamna Ahmed, Asha Gul, Uzma Afzal and Farah Said (Lahore School), Marcel Fafchamps (University of Oxford), Simon Quinn (University of Oxford), Kate Vyborny (Duke University), and Muhammad Haseeb (University of Warwick) </p>
		
		<p>
With funding from the British Academy and the Lahore School of Economics, researchers at Oxford and CREB have completed the 3-year collaborative program on the Economics of Rural Poverty in Punjab. The collaboration has led to several joint academic projects between the two institutions. The collaboration has now far exceeded the scope originally planned, leading to new joint projects that have continued beyond the end of the grant. This project was completed in 2014. <br><br>
The collaborative work includes research on the following topics: <br><br>

		<b>a. Patron-Client Relationships and Rural Development in Pakistan </b><br>
		
		How do social networks and local "patrons" affect household wellbeing and access to public services? Patron-client relationships, or vertical social relationships in which individual powerful "patrons" have multifaceted relationships with "clients", are said to be key features of both markets and politics in many developing country settings, including rural Pakistan. But how do these relationships work in practice and how do they affect the delivery of government goods and services? <br><br>
These questions were explored by Azam Chaudhry (Lahore School), Muhammad Haseeb (Lahore School/University of Warwick) and Kate Vyborny (Duke University/Lahore School) initially with data collected from the Lahore School's Privatization in Education Initiative (PERI) survey and then with the new survey under this project. The researchers have developed several research papers analyzing these data, which examine questions including how local context affects the patron-client relationship, how political connections affect targeting of government programs, and whether different types of program design make programs for the poor more robust to the influence of patronage. The researchers have presented this work in Lahore at the Lahore Economic Development Research Seminar Series (LEDRS) and at International Food Policy Research Institute (IFPRI), AIMS-Manila and Duke University as well as at seminars and conferences in Oxford, Ottawa and Paris and will submit papers for publication soon. <br><br>

		<b>b. Learning about Flood Risk: Evidence from a Field Experiment in Pakistan </b><br>
		
		Farah Said, Uzma Afzal (Lahore School) and Ginger Turner (The Wharton School) investigated how individuals learn about flood risk through the experience and observation of flood events. The study tested the theory that individual risk perceptions and preferences could change with disaster experience, even when controlling for variation in initial asset constraints and losses. The aim was to improve on previous studies by matching game choices with survey data that included questions on expectations, asset, information sets, and timing by complementing surveys with experimental design. <br><br>
A main contribution of this study was to improve the identification of individual loss measures, to test whether variation in flood damages at an individual level can explain differences in observed risk aversion changes. This research was particularly innovative in the Pakistan context, where there has been little work to date on behavioral economics. Findings on behavioral changes have been published in the followings:<br>
Said, F; Turner, G. and Afzal, U. "Risk Taking and Risk Learning after a Rare Event: Evidence from a Field Experiment in Pakistan", Journal of Economic Behavior & Organization 118, 167 - 183, (2015). <br><br>
Turner, G., Said, F. and Afzal, U., "Microinsurance Demand after a Rare Event: Evidence from a field experiment in Pakistan". The Geneva Papers on Risk and Insurance - Issues and Practice 39, 201 - 223, (2014).<br><br>
Turner, G., Said, F., Afzal, U., Campbell, K. "The Effect of Early Flood Warnings on Mitigation and Recovery during the 2010 Pakistan Floods". A. Singh and Z. Zommers (Eds.), Preventing Disaster: Early Warning Systems for Climate Change. London: Springer (2014).<br><br>
Said, F, Turner, G. and Afzal, U. "Attitudes towards Risk in the Wake of a Rare Event: Evidence from Pakistan", CREB Working Paper No. 02-14, (2014).<br><br>

		<b>c. Evaluating the Impact of Punjab's Girls Stipend Program </b><br>

		The government of Punjab, starting in 2004, offered a conditional cash transfer (CCT) of Rs. 200 per month, to girls in class 6 to 8 with minimum 80 percent attendance, in order to increase the educational attainment of girls in districts with less than 40 percent literacy. The program was later was extended up to grade 10. <br><br>
Using two rounds of data collected, a 2009 survey of urban and rural households in Punjab (supported by NSF and the Lahore School of Economics) and a 2013 survey of only rural households (supported by the British Academy and the Lahore School of Economics), Amar Shuja and Theresa Chaudhry (Lahore School) are analyzing the impacts of this CCT on enrollment and marriage outcomes by means of triple difference-in-differences approaches, comparing eligible girls to their non-eligible elder sisters younger siblings in both stipend and non-stipend districts.


		</p>
	
