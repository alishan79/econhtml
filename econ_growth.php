		<h3>Caste, Social Exclusion and Education in Rural Punjab: World Bank and CREB joint Research Initiative </h3>
		<p><b>Researcher: </b>Tayyaba Tamim (Lahore School) and Rehan Jamil (World Bank) </p>
		
		<p>
This funded research was in collaboration with the World Bank and Lahore School of Economics. They used a multiple case study design and mixed methods approach to capture in-depth processes by which caste-based social exclusion shapes the educational opportunity for the marginalized castes in rural Punjab. Three villages were selected from the PERI 2011 data based on geographical spread (North, south and Centre of Punjab), caste and religious complexity and lowest school enrollment. <br><br>Each village was taken as a case. Amartya Sen's capability approach to human development was used to evaluate educational opportunity in the space of capabilities and opportunities, in combination with forms of social exclusion identified by (Whitley, 2005), while Pierre Bourdieu's social critical theory provided the analytical toolkit. Data from 105 interviews with low and high caste parents of (10 -16 year old children) and school heads along with questionnaires, revealed that the caste-based social exclusion stemming from socioeconomic dimension refracted into active and passive forms of institutional exclusionary processes at macro and micro levels, across temporal, spatial and social planes on the one hand; and unfavorable inclusion of the low castes in their own groups, on the other hand; forcing the poorest and most excluded castes to self-deselect themselves from accessing educational opportunity. This project was completed in 2013. <br><br>
The results of the study were published as: <br>
CREB working paper "Caste, Social Exclusion, and Opportunities for Education in Rural Punjab", CREB Working Paper No. 01-15, (2015).<br><br>
Tamim, T. and Tariq, H. "The Intersection of caste, social exclusion and educational opportunity in rural Punjab", International Journal of Educational Development, 43, 51-62 (2015). <br><br>
Two other papers from the same study are in the pipe line: 'Factors affecting low - caste parents' choices for schooling their children: case study of a village in southern Punjab'; 'Caste, social capital and education in rural Punjab'. 

