		<a name="Hamiltonian"></a>
		<br>
		
		<h3>A Partial Hamiltonian Approach for Current Value Hamiltonian Systems</h3>
		<p><b>Researcher: </b>Azam Chaudhry and Rehana Naz (Lahore School) </p>
		
		<p>
Researchers are developing a partial Hamiltonian framework to obtain reductions and closed-form solutions via first integrals of current value Hamiltonian systems of ordinary differential equations (ODEs). The approach is algorithmic and applies to many state and costate variables of the current value Hamiltonian. However, they apply the method to models with one control, one state and one costate variable to illustrate its effectiveness. They explain their approach with the help of a simple illustrative example and then apply it to two widely used economic growth models: the Ramsey model with a constant relative risk aversion (CRRA) utility function and Cobb Douglas technology and a one-sector AK model of endogenous growth are considered. They show that their approach can be used to deduce results given in the literature and also to find new solutions.<br><br>
Published Article: <a href="https://www.sciencedirect.com/science/article/pii/S100757041400149X" target="_blank">"A Partial Hamiltonian Approach for Current Value Hamiltonian Systems,"  with R. Naz and F. M. Mahomed in Communications in Nonlinear Science and Numerical Simulation, Vol. 19, Issue 10, Oct. 2014, 3600-3610.</a>


