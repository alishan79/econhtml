		<h3>Access to solar electricity in rural Sindh: Role of payment schedule and planning </h3>
		<p><b>Researcher: </b>Farah Said (Lahore School), Jacopo Bonan (Fondazione Eni Enrico Mattei), Giovanna d'Adda (University of Milan), Mahreen Mahmud (University of Oxford) and Massimo Tavoni (Politecnico di Milano)</p>
		
		<p>
This research is funded by the IGC and is in collaboration with Eco Energy Finance (EEF). EEF provides solar energy solutions to customers in on-grid areas under a pay-as-you-go monthly repayment scheme. This field experiment will use behavioral measures to investigate individual constraints to repayment and the sustainability of this business model. <br><br>
Almost 70 million people in Pakistan (35% of the population) do not have access to electricity, as they live in areas, mostly rural, not reached by the grid. Over 75 million (38%), while on-grid, receive low quality energy and experience outages for more than 12 hours per day (IFC, 2015). In spite of the significant benefits of access to electricity, the long-run feasibility of grid expansion for the provision of quality electricity to remote areas is often a concern.<br><br>
The study is a collaboration with a for-profit company supplying sustainable and efficient solar energy solutions (e.g. lights, fans, mobile chargers, TV) to small businesses in off-grid areas of rural Sindh, to evaluate one market solution to increase access to energy. The product is innovative, as it alleviates credit constraints to adoption and has strong enforcement features: customers' access energy through a pay-as-you-go monthly payment scheme and are disconnected when the credit expires. The study will investigate key determinants of the sustainability of the business model and of product take-up, by looking at the trade-off between discipline and flexibility in repayment schedule. The experiment will be able to explore behavioural underpinnings of two repayment schemes, by testing soft ways to increase the salience of repayment.<br><br>
Status: Fieldwork ongoing, expected to finish by October 2018. <br>
Expected publication: Dec 2018. 

