		<h3>Breaking the cycle. Reducing non-payment for electricity: experimental evidence from Pakistan </h3>
		<p><b>Researcher: </b>Farah Said (Lahore School), Jacopo Bonan (Fondazione Eni Enrico Mattei), Giovanna d'Adda (University of Milan), Mahreen Mahmud (University of Oxford) and Massimo Tavoni (Politecnico di Milano)</p>
		
		<p>
This research is funded by the International Growth Centre (IGC). The proposed research tests the effectiveness of behavioral interventions in fostering regular bill payment through a randomised field experiment (RCT) to be conducted in Karachi, Pakistan. The experimental design was defined in partnership with K-Electric (henceforth, KE), a private, vertically integrated electric-energy corporation serving around 23 million residential, industrial and commercial customers in Karachi. In spite of the company's efforts to reduce losses from non-payment of bills, recovery rates still range between 70 and 90%. Energy provision by KE follows a policy of selective load shedding at the feeder level, with the number of hours of outages increasing in the level of losses. Under this policy, the individual decision to pay in full and on time generates a positive externality, in terms of quality of energy provision, on clients living in the same neighborhood. Our proposed interventions will make use of the tools commonly used in tax evasion and energy efficiency studies, i.e. communication to customers in the form of letters, to deliver information on the selective load shedding policy and to make explicit the link between individual repayment decision and neighborhood level quality of energy provision. This will contribute to an exciting recent literature that combines laboratory and field experiments to study how real world outcomes can be explained by underlying preferences and norms. <br><br>
Status: Piloting expected in May 2018; fieldwork in Sept-Oct 2018. <br>
Expected publication: September 2018; March 2019. 

