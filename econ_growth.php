		<h3>Rent-seeking, Government Size and Economic Growth</h3>
		<p><b>Researcher: </b>Dr. Waqar Wadho (Lahore School) and Umair Ayaz (Lahore School)</p>
		
		<p>
The researchers explored the relationship between government size and economic growth in an endogenous growth model with human capital and an unproductive capital which facilitates rent-seeking.  <br><br>
With exogenous as well as endogenous time discounting, we find a non-monotonic relationship between the size of government and economic growth. We find that with very high (low) discounting, there is a unique low (high) growth equilibrium, regardless of the size of government. For the intermediate range of discounting, there are multiple equilibria and the growth outcome depends on the size of government.  <br><br>
With endogenous time discounting, the growth outcome is path dependent and depends on the level of inherited human capital. However, there is only one stable growth regime and the economy endogenously switches to it. When the institutional constraints on rent-seeking are not extremely high, the stable regime is the one in which there is a high-growth equilibrium for a smaller size of the government and for larger size, both the high-growth and the low-growth equilibrium coexist. 
When the institutional constraints on rent-seeking are extremely high, there exists only a unique high-growth equilibrium irrespective of the size of government. Furthermore, economies with bigger size of the government and/or with poor quality institutions will take longer to endogenously switch to this stable growth regime. <br><br>
Published Article: <a href="https://onlinelibrary.wiley.com/doi/abs/10.1111/ecpo.12105" target="_blank">W. Waqar; A. Umair. (2018). "Government size and economic growth in an endogenous growth model with rent-seeking", Wiley Journal of Economics & Politics, Volume 30, Issue 1, pages 151-179.</a>


		</p>
	
