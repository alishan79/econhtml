		<a name="Microinsurance"></a>
		<br>
		
		<h3>Microinsurance in Pakistan: Progress, Problems, and Prospects </h3>
		<p><b>Researcher: </b>Theresa Thompson Chaudhry (Lahore School) and Fazilda Nabeel (University of Sussex)</p>
		
		<p>
Microinsurance in Pakistan is still in its nascent stages. More than half of the current microinsurance policies in effect in Pakistan are offered through the Benazir Income Support Program (BISP), with the remainder provided in conjunction with microcredit services offered by various microfinance institutions (MFIs), microfinance banks, nongovernment organizations, and rural support programs (RSPs). <br><br>
The policies offered by the microcredit sector are mainly creditlife policies, which cover loan balances in the event of the borrower's death. In addition, some lenders-principally the RSPs-offer small health insurance policies covering the hospitalization of the borrower and (sometimes) their spouse. <br><br>
As catastrophic health expenses and deaths in the family are among the most important economic stressors that households face, it makes sense that microinsurance should first make inroads in these areas. It is difficult to say what impact microinsurance has had in Pakistan, since few rigorous evaluations have been undertaken to date. <br><br>
What we do know is that utilization has been low, explained by providers as limited client awareness of the benefits and coverage. In the short to medium term, microinsurance outreach can be expanded by offering health microinsurance (HMI) coverage to microcredit borrowers' entire households, and by offering HMI to all community members within an RSP, rather than only microloan borrowers and their spouses.<br><br>
 Partnering with mobile phone operators for automated, digital payments can also significantly expand potential customer volume while reducing transaction costs. HMIs might also be combined with health savings accounts that households can use to pay for medications and outpatient services not covered by HMI plans. Provinces could also leverage the existing database of poverty scorecards implemented by BISP to channel partially government-subsidized microinsurance policies toward poor households just above the BISP threshold.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2018/Issue%20SP/15%20Thompson%20and%20Nabeel.pdf" target="_blank">The Lahore Journal of Economics, Volume 18: SE (September 2013): pp. 335-374.</a>


