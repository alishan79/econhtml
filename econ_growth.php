		<a name="Future"></a>
		<br>
		
		<h3>An Impact Assessment of Expected Future Turmoil Risk on FDI: A Panel Data Analysis of Developing Countries </h3>
		<p><b>Researcher: </b>Mahvish Faran (Lahore School; PhD candidate Sheffield Univertsity)</p>
		
		<p>
This paper uses foreign direct investment (FDI) data from 39 developing countries for the period 2002-11 to explore whether the expected future turmoil risk of a country plays a significant role in determining FDI. It concludes that countries for which the expected future turmoil risk is very high are likely to have lower FDI inflows than countries for which the expected future turmoil risk is low, keeping all other factors constant. The results also illustrate that GDP per capita, democratic accountability, religious tension, and FDI inflows in the previous period are important determinants of FDI in developing countries.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%202/04%20Faran.pdf" target="_blank">The Lahore Journal of Economics, Volume 19: 2 (Winter 2014): pp. 101-128.</a>


		</p>
	
