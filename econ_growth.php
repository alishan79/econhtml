		<h3>Incentivizing Development: A Field Experiment on Third Tier Organizations in Pakistan</h3>
		<p><b>Researcher: </b>Hamna Ahmed (Lahore School), Asha Gul (University of New South Wales), Kate Vyborny (Duke University) and Simon Quinn (University of Oxford)</p>
		
		<p>
Donors and developing country governments have experimented with alternative ways to deliver services and assistance to communities and to individuals.  One such approach is to deliver funding for basic services through non-governmental organisations.  Incentive issues have been studied extensively in government bureaucracies.  However, similar issues also arise when public spending is channelled through the non-government sector, which may reduce state effectiveness in policy delivery; yet there is very little research on these issues.  <br><br>
In addition, we know very little about how public spending through non-government organisations affects service delivery through the traditional bureaucracy.  Under what circumstances does it complement or bolster mainstream public service delivery, providing information or exerting citizen pressure on the government?  Under what circumstances does it "let government off the hook," substituting for services that would have been provided by the state? <br><br>
 In this project, we are collaborating with the Pakistan Poverty Alleviation Fund to study these issues.  PPAF receives support from both donor and Pakistani government sources, and funds local volunteer-run NGOs across Pakistan to provide public services in a wide range of sectors.  We are collaborating with PPAF on a Randomized Controlled Trial with 836 volunteer local NGOs across Pakistan.<br><br>  
As a part of the trial, PPAF is randomly varying the reporting obligations and incentives for those NGOs.  The results of the RCT and related analysis on a rich panel dataset on these NGOs, their communities and local government offices will help shed light on how monitoring and incentives can be improved in the NGO sector; how the staff of a complex organization (PPAF and its partners, who disburse the funds and support the local NGOs) respond to and manage new information; and on the relationship between publicly-funded NGO sector and traditional state institutions. 

		</p>
