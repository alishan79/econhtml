		<a name="Analyzing"></a>
		<br>
		
		<h3>Analyzing Pakistan's Trade Opportunity with Turkey </h3>
		<p><b>Researcher: </b>Asha Gul (Lahore School; PhD. candidate, University of New South Wales)</p>
		
		<p>
Pakistan and Turkey undoubtedly have a strategically important and economically lucrative relationship. While Turkey could become a bridge between Asia and Europe, Pakistan shares borders with two large, densely populated economies and could become a gateway to energy-rich Central Asia. <br><br>
However, given their present trade structures and volumes, Pakistan needs Turkey more than the other way around. Growing economic cooperation between the Pakistani and Turkish governments-manifested in the recently proposed preferential trade agreement (PTA)-has served to strengthen the historically good relations between both countries. This paper explores the trade relationship between Pakistan and Turkey in an attempt to analyze the potential gains for Pakistan under the proposed PTA. <br><br>
We evaluate potential trade opportunities using descriptive statistics and three trade indices: a trade complementarity index, export similarity index, and intra industry index. Our findings suggest that Pakistan's trade surplus with Turkey, strong export similarities, and intra-industry trade would allow greater opportunities for firm synergies between the two countries. This, in turn, would help Pakistan achieve greater value addition and a broader market base for its exports. The Government of Pakistan should, therefore, lobby strongly for the proposed PTA (which might later evolve into a free trade agreement) and leverage the agreement in such a way that Pakistan can maximize its potential benefits.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%20SP/15%20Asha%20Gul%20Final.pdf" target="_blank">The Lahore Journal of Economics, Volume 19: SE (September 2014): pp. 349-370.</a>


		</p>
	
