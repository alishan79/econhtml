		<h3>Exchange Rate Management and Economic Growth: A Brewing Crisis in Pakistan </h3>
		<p><b>Researcher: </b>Naved Hamid (Lahore School) and Azka Sarosh Mir (Lahore School)</p>
		
		<p>
In this research it is argued that Pakistan has had a consistently overvalued exchange rate and the policy with regards to management of the exchange rate has undergone a significant change in recent years. It was showed that prior to March 2013, the policy target of the exchange management was stability of the real effective exchange rate. However, during the tenure of the current government, the policy target for exchange rate management seems to have been stability of the nominal exchange rate against the US dollar. As the currencies of Pakistan's major trading partners (UK, Europe and China) have depreciated against the dollar during this period, the real effective exchange rate has appreciated by over 20 percent since the time that the current policy makers took office. Overvaluation in general and the recent reversal in the exchange rate management policy in particular have had an adverse impact on exports and the manufacturing sector. This not only has serious negative consequences for the long term, growth of the economy, but has greatly increased the short-term risk of a balance of payments crisis.<br><br>
