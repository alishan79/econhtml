		<a name="Corruption"></a>
		<br>
		
		<h3>Corruption, Tax Evasion And The Role Of Wage Incentives With Endogenous Monitoring Technology</h3>
		<p><b>Researcher: </b>Waqar Ahmerd Wadho (Lahore School)</p>
		
		<p>
The importance of high salaries to circumvent bureaucratic corruption has been widely recognized in the policy debate. Yet, there appears to be much reluctance when it comes to the implementation. In this paper, we evaluate the common conviction that efficiency wage can be used as an incentive device to eliminate malfeasance in the government and its cost-effectiveness. Our focus remains on one branch of the government, that is, tax department, which may comprise corruptible tax inspectors who cause tax compliance problems. We explore for a budget balancing government that when it will launch anti-corruption strategy and would it be cost effective to offer wage incentives to combat corruption. Apart from efficiency wage, we add reservation wage (outside option) and capitulation wage (a wage less than outside option) in the basket of wage strategies for the government. We have an endogenous monitoring technology, which depends on government's audit intensity and the number of corrupt tax inspectors. Furthermore, we add the role of rule of law through the cost of laundering bribe income.<br><br>
Endogenous monitoring technology implies that equilibrium level of corruption not only depends on public remuneration scheme but also on the level of audit intensity and the number of other corrupt tax inspectors. For wage incentives to be a viable anti-corruption strategy, it must be accompanied by a non-zero audit intensity. When government offers efficiency wages and capitulation wages, there is a unique equilibrium where all tax inspectors stay honest and all are corrupt, respectively. For reservation wages, the equilibrium depends on the monitoring technology. For a higher audit intensity, all tax inspectors stay honest, and for a lower audit intensity, all corruptible tax inspectors are corrupt. For the intermediate levels of audit intensity, there are multiple equilibria. Depending on the proportion of corrupt tax inspectors, corruptible tax inspectors can be corrupt or can stay honest.<br><br>
Since auditing is costly and the effectiveness of monitoring depends on the number of other corrupt tax inspectors, there are situations where government is eventually better-off (in terms of revenues) giving up against corruption. Our results suggest that government is better-off either completely eliminating corruption or accepting corruption by offering wages lower than the market wage. This implies that offering tax inspectors a wage premium or even equal to private sector wage and not solving the moral hazard problem is sub optimal in terms of net revenues. Governments can increase their net revenues either by reducing government wages to a level below the outside option and not auditing tax inspectors or by raising audit intensity up to the level where no one has incentive to be corrupt. This suggests that Ukraine's wage strategy of capitulation wages seems optimal than those of for example Pakistan, India, or Latin America where there are public wage premiums but corruption levels are as high as those of Ukraine and tax to GDP ratio is low. When it is optimal to deter bribery, the government can do this either through incentive-based strategy (carrot/stick mix) or through monitoring. Which of these two is optimal depends on cost of auditing, level of dishonesty, tax base, and reservation wage. The role of efficiency wages decreases in societies with higher proportion of dishonest agents. In these societies, wage incentive schemes become prohibitively expensive and the government would opt for a monitoring-based anti-corruption strategy when auditing is less costly, and a capitulation wage policy when auditing is more expensive.<br><br>
Published Article: <a href="https://onlinelibrary.wiley.com/doi/pdf/10.1111/ecin.12243" target="_blank">Economic Inquiry (ISSN 0095-2583) Vol. 54, No. 1, January 2016, 391-407.</a>


		</p>
	
