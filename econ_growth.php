		<h3>A Partial Langrangian Approach to Solving Dynamic Models of Economic Growth</h3>
		<p><b>Researcher: </b>Azam Chaudhry and Rehana Naz (Lahore School) </p>
		
		<p>
Researchers are developing a new approach termed as a discount free or partial Lagrangian method for construction of first integrals for dynamical systems of ordinary differential equations (ODEs). It is shown how one can utilize the Legendre transformation in a more general setting to provide the equivalence between a current value Hamiltonian and a partial or discount free Lagrangian when it exists. As a consequence, they develop a discount factor free Lagrangian framework to deduce reductions and closed-form solutions via first integrals for ODEs arising from economics by proving three important propositions. The approach is algorithmic and applies to many state variables of the Lagrangian. In order to show its effectiveness, they apply the method to models, one linear and two nonlinear, with one state variable. They obtain new exact solutions for the last model. The discount free Lagrangian naturally arises in economic growth theory and many other economic models when the control variables can be eliminated at the outset which is not always possible in optimal control theory applications of economics.<br><br>

Article Published: <a href="https://link.springer.com/article/10.1007/s11071-016-2605-8" target="_blank">"A partial Lagrangian method for dynamical systems, "  A. Chaudhry, R. Naz and F. M. Mahomed, Nonlinear Dynamics, May 2016, Vol. 84, Issue 3, 1783-1794.</a>


		</p>
	
