		<a name="Diversification"></a>
		<br>
		
		<h3>The Diversification and Sophistication of Pakistan's Exports: The Need for Structural Transformation </h3>
		<p><b>Researcher: </b>Uzma Afzal (Lahore School, current PhD. candidate, University of Nottingham), Maha Khan (Lahore School; current PhD. candidate, The University of York)</p>
		
		<p>
While export diversification is considered to foster export growth and enhance GDP growth rates, this diversification has not translated into higher exports for Pakistan. In addition to diversification, the country must undergo a structural transformation of its exports to upgrade to a more sophisticated export basket. This entails shifting its comparative advantage from primary to manufactured exports and, further, from a labor-intensive to a more capital-intensive productive structure.<br><br>
 In order to explain Pakistan's paradoxical situation, this paper analyzes Pakistan's orientation in the 'product space' as it affects the process and rate of structural transformation. In addition, we assess the sophistication of Pakistan's exports based on their complexity and technological sophistication. 
Our analysis refutes the traditional argument that diversification leads to greater exports and faster economic development. It also shows that the bulk of the country's productive capabilities are concentrated in the periphery of the product space, which is very weakly connected to the tightly packed industrial core. The export basket is neither complex nor technologically sophisticated, producing low-tech undifferentiated products. It seems that Pakistan is left with few nearby options for structural transformation, leaving it without a path to other, more sophisticated areas in the core of the product space. <br><br>
We argue that accelerating the process of structural transformation will require revisiting industrial policy, strengthening the country's institutions and strategic collaboration between the public and private sectors.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2021/Issue%20SP/05%20Khan%20and%20Afzal.pdf" target="_blank">The Lahore Journal of Economics, Volume 21: SE (September 2016): pp. 99-127.</a>


		</p>
	
