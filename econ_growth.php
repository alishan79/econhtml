		<a name="Patterns"></a>
		<br>
		
		<h3>Patterns of Export Diversification: Evidence from Pakistan </h3>
		<p><b>Researcher: </b>Hamna Ahmed (Lahore School) and Naved Hamid (Lahore School)</p>
		
		<p>
This research examines historical trends in the diversification of exports in Pakistan, using the Hirschman index to quantify the degree of export diversification. 
We analyze the structure of exports through the lens of 'traditionality' for which we construct industry-specific, average cumulative export experience functions, i.e., a traditionality index of all 2-digit export industries in Pakistan from 1972 to 2012. This is useful in distinguishing between traditional and nontraditional export industries. <br><br>
We also study the degree of structural change in the export sector since 1972 by recalculating the traditionality index based on five-year interval periods. The cross-industry variance of this index is then used to calculate the structural change index. Periods for which the index values are low are interpreted as periods during which the export industries experienced uniform patterns of export growth (and thereby no structural change). Periods for which the index values are high are interpreted as periods during which the export industries experienced varied patterns of growth, thus undergoing structural change. <br><br>
Finally, we explore the determinants of structural change in exports by looking at variables such as GDP growth, export growth, the real exchange rate, the growth rate of world trade, trade liberalization, and the degree of product concentration in the country's export base.<br><br>
The objective of this study was to assess the degree of traditionality across all export industries in Pakistan, to analyze the degree of structural change that has occurred in the export sector since 1972, and to explore the determinants of medium-term structural change in the export sector.<br><br>
 Our results suggest that the current export base continues to be fairly traditional as is evident from the fact that the least traditional industries accounted for less than a tenth of total exports in 2012. Moreover, the results highlight two main factors that may be critical for the export sector to undertake structural change and become more dynamic in the future. The first of these pertains to a more liberal trade policy stance by the government; the second relates to the country's ability (as given by GDP growth) to successfully expand in the future. <br><br>
However, the effectiveness of both measures-a more liberal trade policy regime and a sustained drive toward growth-will likely also depend on the availability of complementary fundamentals such as a supportive business environment, a trade-enabling regulatory framework, good governance with the rule of law, and political and macroeconomic stability.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%20SP/13%20Ahmed%20and%20Hamid%20Final.pdf" target="_blank">The Lahore Journal of Economics, Volume 19: SE (September 2014): pp. 307-326.</a>


