		<h3>
Patronage in Rural Punjab: Evidence from a New Household Survey Dataset 
</h3>
		<p><b>Researcher: </b>Azam Chaudhry (Lahore School) and Kate Vyborny (Duke University)</p>
		
		<p>
The intervention of local elites is often cited as an impediment to policy implementation in many developing countries. In this research, we present initial results from an original primary household dataset from eight tehsils of rural Punjab, Pakistan. We examine descriptive statistics on patron-client interaction and correlations between household characteristics and that relationship. <br><br>
The study raises some key findings. First, households report connections with a range of officials; they interact most heavily with local officials, but a large number of households also report interacting with their provincial and national politicians. <br><br>
Second, many households report receiving active assistance both from local officials and from provincial and national politicians in accessing certain state services, in particular in applying for national identity cards.<br><br>
Third, households report links with many patrons outside their own biraderi or clan. Fourth, vulnerable households, such as landless and female-headed households, appear less likely to interact with and less likely to receive assistance from patrons, suggesting that patronage activity could increase the inequality of outcomes. <br><br>
Fifth, better-off households appear more likely to assist patrons in a range of areas. Finally, local officials and politicians had tended to recommend candidates in the last election, and rural households were strongly convinced that their vote was not secret from their patrons or officials. This is possibly consistent with patronage-based politics and bloc voting.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2018/Issue%20SP/08%20Azam%20and%20Vyborny.pdf" target="_blank">The Lahore Journal of Economics 18: SE (September 2013): pp. 183-209.</a>




		</p>
	
