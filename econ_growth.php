		<h3>Impact assessment of startup loans to female run micro-enterprises </h3>
		<p><b>Researcher: </b>Farah Said (Lahore School), Azam Chaudhry (Lahore School), Giovanna d'Adda (University of Milan), Naved Hamid (Lahore School) and Mahreen Mahmud (University of Oxford)</p>
		<div align="center"><img src="images/startup.png"></div>
		
		<p>
This study is a Randomized Control Trial to evaluate the socio-economic and welfare impacts of micro-loans to female micro-entrepreneurs. This study looks at whether access to finance leads to female setting up their business and attempts to measure household dynamics faced by borrowers that may act as a constraint on the decision to set up a business. This study was conducted in collaboration with Kashf and the fieldwork completed in October 2016. The project was funded by the International Growth Centre (IGC).<br><br>
There was a positive, but transitory, impact of the microfinance product on the likelihood of setting up a business but no improvement in household asset holdings or in women's independence when making ordinary household decisions. To provide measures of social and household dynamics these borrowers operate in, this study also made use of lab-in-the-field experiments at the time of the first follow up survey in 2015, with 267 randomly selected RCT participants and male members of their households. Experimental results show that in pairs where women were entrepreneurs, men and women did not hide experiment money from each other. However, husbands of housewives chose to keep more for themselves when this allocation could be kept hidden from their partners. In literature, this is taken as evidence of 'hiding' of resources from the spouse. In addition, men and self-employed women exhibit entitlement over their earnings, whereas housewives do not. These results point towards possibly adversarial dynamics in households where the woman is not working.<br><br>
Status: Fieldwork completed, data analysis under way, preliminary results presented at SEEDEC 2016, RES 2017, CSAE 2017, CSAE 2018, RES 2018, SEEDEC 2018 (expected). <br><br>
Dissertation Submission: September 2018 <br><br>
Expected publication: <a href="https://www.theigc.org/wp-content/uploads/2017/03/Said-et-al-2017-Final-report.pdf" target="_blank">December 2018; March 2019.</a>


