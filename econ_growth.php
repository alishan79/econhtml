		<a name="Reviewing"></a>
		<br>
		
		<h3>Reviewing Pakistan's Import Demand Function: A Time-Series Analysis, 1970-2010 </h3>
		<p><b>Researcher: </b>Zunia Saif Tirmazee Lahore School) and Resham Naveed (Lahore School)</p>
		
		<p>
There has been a tremendous rise in Pakistan's level of imports over the last few years, especially following the liberalization regime of the 1990s. In addition, the economy has experienced a rise in foreign exchange reserves and economic growth. However, as a result of its limited export profile, Pakistan's trade deficit has worsened. In light of these circumstances, it has become essential to determine which factors explain the abrupt variations and continuous growth in imports for Pakistan. This paper has tried to do so by empirically estimating the determinants of imports in the long run and short run using a VECM. Our main findings indicate that there is a long-run equilibrium relationship between import demand and real GDP, relative prices, the terms of trade, and foreign exchange reserves availability, signifying the relevance of additional determinants for the conventional import demand equation. <br><br>
These findings obviously have implications for the trade balance. The import volume would rise significantly if real income were to increase, and that too at a rate higher than the rate of growth of real income, thus causing the trade balance to deteriorate. To prevent this, exports must grow in tandem with imports. With a stagnant export growth rate and a very high import growth rate, the trade balance will keep worsening. Foreign exchange reserves are also statistically significant, affecting import demand both in the short run and the long run. Moreover, import demand is elastic with respect to foreign exchange reserves availability. Omitting such an important variable can cause the misspecification of the model and overemphasize the influence of the other variables included. <br><br>
We have shown this empirically by estimating two separate models for import demand: a conventional import demand function, to which we then added other variables of import demand. Import demand is inelastic with respect to relative prices, even though relative prices have a negative impact on import demand. The small coefficient implies that Pakistan's export market is noncompetitive: despite the rising prices of imports, this has not led to a significant substitution of exports for imports. This effect marks the inability of the domestic market to provide substitutes that can compete with imports. Finally, the coefficient of the terms of trade is positive and greater that 1. This means that a favorable change in the terms of trade (i.e., an increase in the price of exports) should help domestic consumers buy more imports for each unit of exports. However, this may also imply a worsening trade balance if exports fall because they have become relatively more expensive. <br><br>
Our estimates of the elasticity of import demand with respect to the terms of trade are greater than 1, suggesting that an increase in the price of imports should lead to a fall in imports at a rate greater than the rate at which the terms of trade improve. For Pakistan, however, even with the constant devaluation of the rupee, a fall in the price of exports and a corresponding rise in the price of imports have not improved the trade balance. <br><br>
Again, this has to do with the nature of imports. Without suitable substitutes to control import growth, the trade deficit will not improve. Our post-1980s analysis reveals that Pakistan's imports-to-GDP ratio was falling at a time when those of other rapidly growing developing countries (which are now far ahead) were increasing. Import growth is an essential development that takes place alongside economic growth. Pakistan's trade balance is the mirror image of its imports, implying that any variation in the balance of trade comes mostly from imports. Policy propositions to reduce imports and improve the trade balance are, therefore, common. <br><br>
The real concern, however, is to improve the trade balance without having to reduce imports. If recovery is to come from anywhere, it must come from the export side, given the nature of Pakistan's imports and the fact that it is not self-sufficient in producing what it currently has to import.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%20SP/16%20Zunia%20Resham%20Final.pdf" target="_blank">The Lahore Journal of Economics, 19: SE (September 2014): pp. 371-393.</a>


		</p>
	
