		<a name="Growth"></a>
		<br>
		
		<h3>Pakistan's Growth Spurts and Reversals: A Historical Perspective </h3>
		<p><b>Researcher: </b>Rashid Amjad (Lahore School)</p>
		
		<p>
This research takes a historical perspective to search for the major causes of Pakistan's stop-go growth cycles and come to the conclusion that, to varying degrees, the foreign exchange constraint provides a major explanation for these cycles of irregular economic growth in the country, particularly since the 1990s. <br><br>
The main conclusion of the paper is that the Pakistan economy has not performed to its potential and this reflects both poor short- to medium term macro-economic management and the emergence of serious structural constraints which have resulted amongst others in recurring balance of payment crisis. The country has been fortunate in that favorable external developments in the form of a manifold increase in remittances and some positive internal developments have kept the economy afloat and in the process generated sufficient dynamism such that extreme poverty has continued to decline.<br><br>
 A glaring message which does emerge from our historical analysis is that a binding constraint has been the foreign exchange constraint which especially in the last two and a half decades has been responsible for the economy's poor performance. While the manifold increase in remittances in the last decade and a half need to be welcomed they cannot substitute for Pakistan's continuing poor export performance. This again is an area which has been analyzed many times earlier and with hindsight on what has worked and not worked. This conference, with its focus on improving Pakistan's export performance, offers us an opportunity to come up with workable and doable policy solutions to help remove and initially ease the foreign exchange constraint which continues to throttle economic growth.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%20SP/05%20Rashid%20Amjad%20Final.pdf" target="_blank">The Lahore Journal of Economics, Volume 19: SE (September 2014): pp. 91-104</a>


		</p>
	
