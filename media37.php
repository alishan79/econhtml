		<h3>Readymade Garment Exports Should Be Government Focus</h3><br>
		<p>Developing countries have sought to promote exports as a growth strategy since they are a source of both higher demand and of coveted foreign exchange. Proponents of trade liberalization argue that there is a positive relationship between openness of economy and productivity of its firms. However, the mechanism through which this works is by the introduction of imports which reduce the markups that the firms charge from consumers due to greater competition which in turn lowers the average cost of production due to the exit of low-productivity firms. In Pakistan, exporting firms use more imported inputs, are more productive and capital intensive and have higher growth potential reveals recent research conducted by the Lahore School of Economics.</p>
		
		<p>Researchers from the Lahore School of Economics showed that the average sales from exports, among exporters, was approximately 51% of total sales in Punjab and that many exporters in Pakistan don't have a significant domestic presence. Exporting firms have been found to have 29% higher revenues and 150% more output even after controlling for firm level characteristics such as geographical location and ownership status. The labor productivity of exporting firms was found to be 2-3 times higher than that of non-exporters, and exporters were outperforming in terms of larger firm sizes, more employment, higher compensation and greater productivity.</p>
		
		
		<p>The researchers from the Lahore School of Economics found that apparel producers are doing well and exporting nearly 93% of their output. This sector employs on average 400 workers per organization and offers significantly higher compensation which makes the sector favorable for exports. Therefore, the government's recent emphasis on developing the readymade garments sector is well placed. </p>
		<p>The researchers also found generally that the capital-labor ratio among exporters was twice than that of non-exporters. Exploring some additional dimensions by which the exporting and non-exporting firms differ, it was found that the average exporting firms tended to use a larger share of imported material in their input mixes than non-exporting firms. Also, looking at the number of days a factory is in operation, it was found that exporters' factories operates, on average, more days than the non-exporting factories. </p>						
		
		<p>This research was conducted by Dr. Theresa Chaudhry, Associate Professor, Lahore School of Economics and Muhammad Haseeb, University of Warwick.
