		<h3>Lahore Journal of Economics</h3><br>
	
	<p>We are pleased to announce the launch of the latest issue of the Lahore Journal of Economics. This issue contains the following papers that are on the cutting edge of economic research in Pakistan:</p>
	
	<div align="center"><img src="images/news_res8.png" width="450px"></div>
	
	<p>Tehseen Ahmed Qureshi and Zafar Mahmood, in their paper "The Magnitude of Trade Misinvoicing and Resulting Revenue Loss in Pakistan," analyze the magnitude of trade misinvoicing in Pakistan with 21 of its developed trading partners during 1972-2013 and find that the total volume of trade misinvoicing for this period exceeds US$92.7 billion.</p>
	
		<p>Mirajul Haq, Syed Kafait Hussain Naqvi, and Muhammad Luqman in their paper "Is the Value Addition in Services and Manufacturing Complementary? Empirical Evidence from SAARC," test whether the rapid growth and value addition of services presents an opportunity or threat for value addition in manufacturing in SAARC countries.</p>
		
	<p>Iftikhar Ahmad in his paper "Assessing the Effects of Fiscal Decentralization on the Education Sector: A Cross-Country Analysis," examines the effects of fiscal decentralization on the education sector across countries and find that while subnational governments that are financed through own-tax revenues are more likely to increase the funds allocated to education, they also seem less concerned with maintaining teaching quality.</p>
		
		
		<p>Mushtaq Hussain Khan, Ahmad Fraz and Arshad Hassan in their paper "The Diversification Puzzle: The Role of Asymmetric Information and Insider Trading in Pakistan," study of the corporate diversification-value relationship for Pakistani firms and show that, in cases of asymmetric information, insiders increase the purchase of their firms' shares in the open market when diversification is high.</p>
		
		<p>Mobeen Ur Rehman in his paper "Financial Contagion in EFA Markets in Crisis Periods: A Multivariate GARCH Dynamic Conditional Correlation Framework," investigates the relationship between stock markets in developed markets and emerging and frontier Asian (EFA) markets and find a stronger relationship between EFA and developed markets, especially during the 2008 financial crisis.</p>
		<p>Naeem Akram in his paper, "Do Financial Sector Activities Affect Tax Revenue in Pakistan?" analyzes the role of financial markets in generating tax revenue in Pakistan and finds that, in the long run, the number of bank branches and market capitalization have a positive and significant impact on tax revenue.</p>
		
