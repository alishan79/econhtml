			<h3>Child Nutrition, Education & Child Labor: Impact on Human Capital Accumulation</h3><br>
			
			<p><b>Sara Khan</b></p>
			
			<h3>Abstract</h3>			
			
			<p>This study constructs a two-period overlapping generation's model which incorporates both child nutrition as a measure of health and education to observe the complementing nature and nexus between them and how it alters parent's fertility decisions. The results of the model show that in the intermediate phase, the economy experiences a demographic transition. In this interval the child quantity-quality trade-off is observed but at a later stage the continuing process of increasing human capital allows agents to generate adequate resources to rear more children and simultaneously endowing them with the capacity of providing the children with education and nutrition. Minimum level of fertility in the model is attained when maximum child nutrition and time devoted towards education are attained. When human capital exceeds the maximum threshold level time devoted to education becomes a constant i.e. further increases in human capital has no effect on the time devoted to education pertaining that maximum capacity to learn has been reached. Moreover, the model also proposes insights on child labor policies which has implications for both human capital and economic growth.</p>
			
			<br>

			<h3>Birth Order Effects on Child Labor</h3><br>
			
			<p><b>Maida Basharat</b></p>
			
			<h3>Abstract</h3>			
			
			<p>Birth order has impacted household resource allocation decisions in the past in terms of schooling, labor and nutrition. This paper attempts to study in-depth the impact birth order has on the schooling and child labor outcomes in a household. It also looks at how birth order might have a gender specific impact on these outcomes. For this purpose, the bivariate probit model is employed on the sample data given by Punjab MICS 2011. The results of this study imply that both first-born male and female children are more likely to work and latest-born male and female children are less likely to go to school. First born females are more likely to work in all types of work (market work + family work+ any positive hours worked domestically). First born males more likely to work in market and family work. A latest born male child is less likely to work in market and family work. First born female children are discriminated against in terms of domestic and all types of work which is indicative of our society. Latest Born male children are more likely to work in both market and family work and domestic work >20 hours and less likely to work otherwise. Latest born female children are less likely to work domestically (when even <20hrs per week considered) and in small families.</p>
			
			<br>
			<h3>Multiple equilibria and stability analysis of an economic growth model with Human capital externalities and endogenous time preferences
</h3><br>
			
			<p><b>Ammara Riaz</b></p>
			
			<h3>Abstract</h3>			
			
			<p>We would be developing a model to study the growth and fiscal policy implications under the assumption that the public policy would create an externality through the aggregate human capital in individual rate of time preference. Human capital is taken as main driver of economic growth. We would investigate the behavior of the government size and the growth rate to the sensitivity of time preference to physical capital and human capital considering the magnitude of human capital externality. There would be multiple equilibria in the economy. We would perform stability analysis to look at stable equilibria along the balanced growth path. Finally, we would look at how this high and low growth gap could possibly shrink by fiscal implication.</p>





