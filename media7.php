		<h3>Are All Pakistani Textile Firms Innovative?</h3><br>
		
		
		<p>In the wake of an extraordinary increase in access to information and new markets in recent years (primarily due to advances in information technology and globalization), firms in developing countries experience a constantly changing landscape in the market for their products. This on one hand is providing much needed knowledge flows into developing economies, while also forcing firms to improve their competitiveness on the other. In such a situation, one would expect firms (especially those which export) to invest in new technology and also introduce new and improved products in their markets. However, there is still a greater need to expand our understanding of innovation and its economic impact when it comes to developing countries like Pakistan. <br><br>
Research by the Lahore School of Economics looks at the decision of firms to innovate, its innovation investment, product innovation, and labor productivity using firm level data of 377 firms from the highly export oriented textile sectors, which included firms from the unstitched textile sector and also firms from the stitched apparel sector of Pakistan. The findings reveal some interesting insights into innovation behavior at the firm level that have the potential to serve as inputs to national policy making. <br><br>
On average, firms in the apparel sector are larger than firms in the unstitched textile sector, they spend more on innovation, have a higher percentage of innovative sales, and are more innovative. Overall, on average firms spent 3.6 percent of turnover in 2015 on innovation, and on average approximately one-third of turnover in 2015 is attributed to innovative products. Another, important observation is that there is a high dispersion in the distribution of firms in terms of their size, innovation expenditure, and innovative sales. Around one third of all firms in the Pakistani textile sector are innovative, however, there are striking differences between the unstitched textile and apparel sectors in Pakistan. The percentage of innovative firms in the apparel sector is more than two times greater than the unstitched textile sector.   On average innovative firms spent more than 7 percent of their turnover in 2015 on innovation, of which around two-thirds of the expenditure was on the acquisition of machinery, equipment, and software.<br><br>
The researchers also found that product innovation leads to increased labor productivity as well as higher labor productivity growth. In particular, a 10 percent increase in innovative sales per worker yields a greater than 10 percent increase in labor productivity and labor productivity growth. Firms located in the province of Sindh and from the unstitched textile sector experience labor productivity growth as compared to the apparel sector. There is no significant impact of firm size and sources of information and cooperation on a firm's productivity or its growth.<br><br>
Competition seems to have a different impact depending on the location of the competitor. Results suggest that firms with medium or large sized foreign competitors are less likely to engage in innovation.<br><br>
On the constraints side, firms reporting cost factors as a highly important constraint to innovation are less likely to engage in innovation, but they invest more in R&D.<br><br>
The results show that vertical knowledge flows are very important determinants of a firm's decision to engage in innovation. Especially, firms considering foreign clients and foreign suppliers as an important source of information and cooperation have a higher probability to innovate.  Larger firms are more likely to engage in innovation and the impact of competition depends on the location of the competitors: foreign competition negatively affects a firm's decision to innovate, whereas, local competition positively affects a firm's level of innovation investment in Pakistan. <br><br>
Also, exporting is found to be positively associated with innovation performance in Pakistani unstitched textile and apparel firms. The results also point to learning by exporting: Pakistani firms exporting to Europe and the US are more likely to engage in innovation, and the investment in innovation increases with exports. There is also evidence in support of the crowding out effect of national subsidies since Pakistani firms receiving national subsides invest less in innovation. <br><br>
Finally, higher investment in innovation, and higher labor productivity leads to higher innovative sales per worker in the Pakistani unstitched textile and apparel sector and there is also evidence of complementarity in that firms which engage in product innovations tend to have greater management and organizational innovations. Also, firms are able to achieve higher sales per worker from new products by introducing organizational innovations. These are the firms who target achieving an increase in the range of goods that they produce, and/or entering new markets or increasing market share in the existing market, and/or improving quality of goods. <br><br>
Overall, the results point to the role of a coordinated industrial strategy that targets firms that export to the US and Europe and also face local competition. Also the results suggest that an industrial strategy by the government that helps reduce firms' costs may lead to greater innovation than policies simply aimed at increasing firm level Research and Development.<br><br>
This research has been conducted by Dr. Azam Chaudhry, Professor & Dean, Faculty of Economics, Lahore School of Economics and Dr. Waqar Ahmed Wadho, Assistant Professor of Economics & Senior Research Fellow at the Center for Research in Economics & Business (CREB), Lahore School of Economics.

