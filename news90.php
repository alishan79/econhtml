		<h3>Lahore School of Economics and Oxford University Conference on Microfinance and Enterprise Development</h3><br>
		<p>The Lahore School of Economics in partnership with Oxford University hosted a conference on Microfinance and Enterprise Development. The objective of the conference was to bring together speakers from microfinance institutions, donors, academics and practitioners from Pakistan and abroad. During this one-day event there was a detailed discussion on current and future research on microfinance with the purpose to better understand the role of research  to help the microfinance achieve its goals of poverty alleviation, financial inclusion and sustainable enterprise growth. The conference was conducted as part of an on-going project on a comparison of Demand for Microcredit and Microsaving in a Framed Field Experiment in Rural Pakistan, which is funded by Economic and Social Research Council-Department of International Development (ESRC-DFID).</p>
		
		<div align="center"><img src="images/n4.jpg" width="450px"></div><br>

		<p>The conference started with welcoming remarks by Dr. Shahid Amjad Chaudhry (Rector, Lahore School of Economics). The keynote speech was delivered by Dr. Rashid Bajwa who showed that the microfinance sector in Pakistan has witnessed a tremendous growth - from 164 thousand borrowers in 2003 to 3.3 million borrowers in 2015. At present, there are 50 microfinance providers operating in the country. Moreover, the sector continues to attract foreign investment.</p>
		
	
		
		<p>The first session of the conference focused on the current state and future directions of the microfinance sector. The chairperson for the session was Dr. Rashid Bajwa (Chief Executive Officer, NRSP). The session was conducted jointly by, Dr. Amjad Saqib (Founder and Executive Director, Akhuwat), Syed Mohsin Ahmed (Chief Executive Officer, Microfinance Network), Kamran Azim (Chief Operating Officer, Kashf Foundation), and Shahid Maqsood (Chief Operating Officer, FINCA Microfinance Bank Limited). Dr. Amjad Saqib gave a brief overview of the unique model of microfinance that Akhuwat is following, based on interest free loans. Syed Mohsin Ahmed discussed an impact assessment study, which PMN is planning to conduct in the near future in order to investigate the impact of microfinance loans on employment and job creation. Kamran Azimgave an overview of the current approach that is being followed by Kashf Foundation. The strategy focuses on (i) credit programs with existing businesses as well as start-ups (ii) A large scale financial literacy program (iii) a health insurance program as well as support for schools in low income areas and (iv) microsaving products.</p>
		
	
		
		<p>The second session of the day served to disseminate and invite feedback on the current research being conducted by Lahore School on microfinance. The chairperson for the session was Kamran Azim (Chief Operating Officer, Kashf Foundation).</p>
		
		<p>Simon Quinn (Associate Professor and Deputy Director, Centre for the Study of African Economies, University of Oxford) presented the first paper in the session (written jointly with Uzma Afzal, Giovanna d'Adda, Marcel Fafchamps, and Farah Said), while the discussant for the paper was Tahir Waqar (Senior Program Manager, National Rural Support Programme (NRSP).</p>
		

<p>The paper is based on a recent pilot study conducted in Sargodha, which directly tested between the demand for microcredit and demand for micro-savings using a framed field experiment amongst women borrowers of NRSP participating in group lending arrangements in rural parts of Sargodha. The experiment involved randomly offering credit products and savings products to the same subject pool. The study concluded that a high demand exists for both credit products and for savings products implying that the distinction between micro-lending and micro-saving may be largely illusory; participants value a mechanism for regular deposits and lump-sum payments, whether that is structured in the credit or the debt domain. Fieldwork for the study is currently underway. A scaled up version of the study will be carried out later this year.</p>

		
		<p>The session ended with a presentation by Uzma Afzal (Assistant Professor and Research Fellow, Centre for Research in Economics and Business, Lahore School of Economics) with Ghazala Mansuri (Lead Economist, World Bank) as the discussant. It was a preliminary presentation discussing the methodology for a project (to be conducted jointly with Giovanna d'Adda, Marcel Fafchamps, Simon Quinn and Farah Said) which aims to use behavioural games to understand how self-control problems and peer pressure affect take-up and outcomes of a financial product and secondly how access to saving and/or credit affects self-control problems and ability to resist peer pressures?</p>	
		<p>The third session of the day also focused on recent research that has been conducted in the microfinance sector in Pakistan. The first paper in this session was presented by Ghazala Mansuri (Lead Economist, World Bank), which was written jointly with Xavier Giné. The discussant for this paper was Ayesha Afzal (Assistant Professor, Lahore School of Economics). The main objective of the paper was to identify the relative importance of human and physical capital for entrepreneurship. Microfinance clients were offered business training and a loan lottery of up to seven times the average loan size. The paper concluded that business training increased business knowledge, reduced business failure, improved business practices and increased household expenditures by $46 per year. It also improved financial and laboral location decisions. Access to larger loans, in contrast, had little effect. Despite the positive impact on clients, business training was not cost-effective for the microfinance institution, explaining why few institutions offer training voluntarily.</p>
		
		
		<p>Farah Said (Assistant Professor and Research Fellow, Centre for Research in Economics and Business, Lahore School of Economics) presented the second paper in the session (written jointly with Azam Chaudhry, Naved Hamid and Mahreen Mahmud). The discussant for this paper was Ommara Raza (Senior Manager Research, Kashf Foundation). The paper is based on a baseline survey for a Randomized Control Trial to evaluate the socio-economic and welfare impacts of micro-loans to female micro-entrepreneurs. The project is being conducted in collaboration with Kashf and the fieldwork is currently underway. </p>
		
		<p>The final paper in this session was presented by Mahreen Mahmud (PhD Candidate, University of Kent). The discussant for the paper was Sajjad Akhtar (Member, Pakistan Microfinance Network Research Committee). The paper explored a unique microfinance model based on zero interest loans and voluntary contributions instead of one with a fixed interest charge. This model provides more scope for signaling to the borrowers especially in the case of joint liability contracts. Thus, the author investigated if patterns of giving and repeat borrowing are consistent with what would be expected of signaling behaviour.</p>
		
		<p>The conference ended with a detailed panel discussion on the role of research in helping the microfinance sector achieve its objectives and in shaping its future. The chairperson for this session was Qazi Azmat Isa (Chief Executive Officer, Pakistan Poverty Alleviation Fund). The panel session was conducted jointly by Akbar Zaidi (Member Board of Directors, Pakistan Microfinance Network), Waqas-ul-Hasan (Private and Financial Sector Development Adviser, Department for International Development), Abanul Haq (Head Education Innovation Fund, Ilm Ideas), Mathew Titus (Former Founder Executive Director, Sa-Dhan) and Sanjay Sinha (Managing Director, Micro-Credit Ratings International Limited).

</p>
