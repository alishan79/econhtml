		<h3>Children in Punjab Fall below International Health Standards and Almost 90% of People Use Private Health Facilities</h3><br>
		<p>Eight per cent of Pakistani children die before they reach the age of five, giving Pakistan the highest child mortality rate in South Asia. Height and weight for age are also low which further increase the susceptibility to disease. However, current surveys in Pakistan are lacking information on the leading causes of health status. Collecting more precise evidence on the key determinants of poor health outcomes in Pakistan, particularly those that can be influenced by policy, is of utmost importance for policy makers for informing government action, reveals research by the Lahore School of Economics.</p>
		
		<p>In order to address these issues, Lahore School researchers evaluated heights, weights and mortality rates of children in Pakistan and found that factors such as income, education, sanitation, water and access to primary care facilities are key determinants of child health. The researchers found that the average four year old Punjabi boy is 6.3 centimeters (2.48 inches) shorter than the international norm. </p>
		<p>A closer look at the author's results found that mother's education is found to be a prime determinant of health status and found that the mother is considered to be the "first line of defense" against illness in her family, particularly among children. The results showed that mothers with some education reduce child mortality rate to 3.3% from a mean of 8% for mothers with no education, which is an enormous difference.</p>
		<p>The research also found that the effect of the family's wealth/financial status was very large in determining child health and appears to be getting stronger over time. This shows that an important role of the government would be to mitigate inequalities in income by forming a public policy that equalizes outcomes between social classes and flattens the relationship between income and health.</p>
		<p>Interestingly, the presence of a public curative health facility was not found to be correlated with better health in this research. According to the results, 88% people who seek medical help go to private sector, while only 7% go to a public hospital, 2% go to primary facilities, and 16% do not seek any care for health problems. The findings also showed that firstly, wealthier families are more likely to do something rather than nothing when a child gets ill. Secondly, girls are less likely to be taken to private health facility as compared to boys.</p>
		<p>On the whole, the research shows that the key measures of health status of children are critically affected by financial status of the family, education of mother, sanitation, water and access to primary care facilities. Thus the government needs to give high priority for policy interventions that addresses these issues. </p>
		<p>The IGC funded study was conducted by CDPR and the researchers included Ms. Uzma Afzal, Research Fellow, Centre for Research in Economics & Business, Lahore School of Economics along with fellow researchers including Usman Ghaus (Teaching Fellow, LUMS) and Dr. Jeffrey S. Hammer (Woodrow Wilson School of International Affairs, Princeton University, USA).<br>										
		
