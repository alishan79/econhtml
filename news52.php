		<p>Mehreen Zahid presented her research work on "Endogenous time preferences and Environmental quality: Multiple equilibria and Fiscal implications," and Mahniya Zafar presented on "The Impact of School Facilities on Drop outs in Pakistan." in the Economics Faculty Departmental Meeting held on 7th September, 2017, at 10:00a.m in TRC-1.<br><br>
</p>
		
			
			<div align="center"><img src="images/MP2.png" width="450px"></div><br>
			
			<p>Mehreen's thesis analyzes a model of growth with endogenous time preferences. Economic growth is determined by factors like consumption, income, savings etc., and this thesis will include environmental quality as a factor in the production function. The analysis will focus on how incorporating endogenous discounting into individual preferences effects economic growth. Her study will hypothesize the presence of multiple equilibria, one low growth and one high growth. Also, the requisite stability analysis will be done to look at stable equilibria along the balanced growth path. Furthermore, her study will analyze how fiscal policy can be used to change a low equilibrium growth path to a high equilibrium growth path.<br><br>
The study by Mahniya Zafar explored the impact of attributes of school facilities on primary school dropouts in Pakistan. She used the Annual Status of Education Report (ASER) data set from 2013, 2014 and 2015 to study the determinants of dropout at the primary level within a household, and the impact of school facilities (both public and private schools) on proportion of dropouts in a village. She also drew a statistical comparison of the impact of school facilities on primary and secondary level drop-outs. The author found that parent's education, asset ownership and gender have a significant impact on whether an individual drops out of school at the primary level. Further, the educational qualifications of private schools teacher quality significantly reduce the proportion of dropouts in a village. However, once the student proceeds to the secondary level, the effect of household, school and teacher quality indicators diminishes. These results indicate the need for policy makers and educators to emphasize school and teacher quality to retain students in school. 
</p>

