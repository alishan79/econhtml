		<h3>An undervalued economy</h3><br>

		<table border="0">
		<tr><td valign="top"><p><img src="images/undeliverd_economy.png"></p></td>
		<td valign="top"><p>THE voices telling us that Pakistan stands on the threshold of a major economic upturn are growing louder. Be it PricewaterhouseCoopers or Bloomberg, scores of renowned economic analysts are pointing to Pakistan as a country where there is money to be made.
Do forgive the ordinary, already troubled Pakistani who is bewildered by all this news. The country has just managed to complete a stringent IMF programme, finally hoping that, with a semblance of macroeconomic stability restored, economic growth might pick up from its dismal low of three to four per cent over the last decade. Decent jobs are scarce. Child malnutrition is rampant. Yet here we are, evidently sitting on a goldmine unawares!
</p>
		</td>
		</tr></table>
		
		<p>What is being said of the economy's prospects is not without merit. The fact remains that Pakistan is, today, a grossly undervalued economy - a view put forward not just by many analysts, but also by a growing number of foreign and domestic investors in search of opportunities to earn high profits in an otherwise anaemic global economy.</p>
		
		<p><b>Why did Pakistan become an undervalued economy? Why the sudden interest in taking advantage of the situation?</b></p>
		
		<p>What, then, is an 'undervalued' economy? Why and when did Pakistan become one? Most important, why the sudden interest in taking advantage of the situation?</p>
		
		<p>An undervalued economy is one that generates high returns primarily because its assets are considerably undervalued. To give some concrete examples, Pakistan's stock market would be classified as 'undervalued' because, even after accounting for the KSE index having grown by leaps and bounds, the returns on stocks and shares are abnormally high even in relation to their substantially appreciated market value. Similarly, the dollar value of the housing and real estate markets (despite some slumps) has grown faster than that of properties purchased in Dubai or most parts of the world. Our visiting neighbours from New Delhi are amazed at how relatively low house prices are in Lahore.</p>		
		
		<p>As to the second question of why this situation persists - not just for selected sectors, but for a large part of the economy - the answer has two parts. The first is that the undervaluation of our assets or capital stock is primarily a result of non-economic factors. The second is that, despite the economy's weak performance, it still generates high profits or returns on investment.</p>
		
		<p>Let us examine the first point. Over the last 30 years, the economy has underperformed grossly, at half its earlier growth rate. There are different explanations for this prolonged slowdown, ranging from poor economic management, IMF adjustment programmes that did not work, rampant corruption and, most important, the lack of political will to undertake fundamental structural reforms that could make the economy more competitive.</p>
		
		<p>Yet, underlying these factors was a deeper malaise: the gradual erosion of investors' confidence. This was triggered initially by the aftermath of the Soviet invasion of Afghanistan and the civil war that followed in the 1990s. Most important, post-9/11, terrorist attacks spilled over into Pakistan and the security situation became extremely fragile. A combination of deteriorating national security as well as policies adopted in the 1980s pulled the city of Karachi - the nerve-centre of Pakistan's economy - into chaos. Investor confidence nosedived. Overall investment levels fell sharply.</p>
		
		<p>What has now led investors' confidence to turn around? Again, there is no dearth of contenders, although one must admit that prudent economic management has, together with the decline in oil prices, helped restore macroeconomic stability. But only the very optimistic investor would pin their hopes on this factor alone, given Pakistan's history of stop-go economic cycles.</p>
		
<p>The real reason for this turnaround (even after accounting for CPEC) is that Pakistan's most powerful institution, which has a decisive say in running the country, chose to concentrate on restoring internal security and taking on all such militant forces - including those that were, earlier, viewed differently. Additionally, the determination of the military leadership that launched this operation and the resolve of the new leadership that has taken over have, to a large measure, sparked this turnaround in investors' confidence.</p>						
		
		<p>Let us now turn to explaining the high returns or profits the economy continues to generate - on average, between 10pc and 15pc, which many businesspersons would consider to be on the low side. Here, the task is made more difficult as official data sources do not capture either the true size of the economy or movements of key economic variables.</p>
		
		<p>Part of this economic dynamism can be explained by the working of the informal economy, most of which remains undocumented. While large investors shied away, smaller (and sharper) investors stepped into the breach, bent on exploiting a market where others feared to tread.</p>
		
		<p>A central factor has been the flow of remittances, which have increased from $1.5 billion in 2000 to around $18bn in 2016. This represents about 7pc to 8pc of Pakistan's GDP. The other factor has been a fast-growing population and the rapid urbanisation and growth of mega cities and services.</p>
		
		<p>It is these economic forces and the prospects of high returns that eagle-eyed investors (both domestic and foreign) are viewing with increasing interest. To ensure that the benefits of this investment are enjoyed by local firms and translate into more and better jobs and pro-poor economic growth will need appropriate policies and prudent economic management. The key lesson is this: to realise the benefits of an undervalued economy will require an unrelenting commitment to, and action by, both the military and the government at all levels to continue improving the security situation.</p>
		
		<p>This article is written by Dr. Rashid Amjad, professor & Head Graduate Institute of Development Studies (GIDS) at the Lahore School of Economics and former VC of the Pakistan Institute of Development Economics.</p>
								
		
