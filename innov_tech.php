		<h3>Innovation in the Textiles Sector: A Firm-Level Analysis of Technological and Nontechnological Innovation </h3>
		<p><b>Researcher: </b>Waqar Wadho (Lahore School) and Azam Chaudhry (Lahore School)</p>
		
		<p>
		In a knowledge-based economy, it has become increasingly important to better understand critical aspects of the innovation process such as innovation activities beyond R&D, the interaction among different actors in the market and the relevant knowledge flows. <br><br>
Using a sample of 431 textiles and apparel manufacturers, this paper explores the dynamics of firms' innovation activities by analyzing their innovation behavior, the extent and types of innovation, the resources devoted to innovation, sources of knowledge spillovers, the factors hampering technological innovation and the returns to innovation for three years, 2013-15. <br><br>
Our results show that 56 percent of the surveyed firms introduced technological and/or nontechnological innovations, while 38 percent introduced new products, these innovations were generally incremental as the majority of innovations were new only to the firm. Furthermore, the innovation rate increases with firm size; large firms have an innovation rate of 83 percent, followed by medium firms (68 percent) and small firms (39 percent). Technologically innovative firms spent, on average, 10 percent of their turnover on innovation expenditure in 2015. Acquisition of machinery and equipment is the main innovation activity, accounting for 56 percent of innovation expenditures. Large firms consider foreign market sources (clients and suppliers) and small firms consider local market sources their key source of information and cooperation. 63 percent of technological innovators cite improving the quality of goods as their most important objective. Lack of available funds within the enterprise is the single most important cost factor hampering innovation, followed by the high cost of innovation. Our results show that 67 percent of the turnover among product innovators in 2015 resulted from product innovations that were either new to the market or new to the firm.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2021/Issue%20SP/06%20Wadho%20and%20Chaudhry.pdf" target="_blank">The Lahore Journal of Economics, Volume 21: SE (September 2016): pp. 129-166.</a>


		</p>
		
