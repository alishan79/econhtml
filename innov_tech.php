		<h3>Barriers to Product Upgradation in the Sialkot Surgical Goods Sector</h3>
		<p><b>Researcher: </b>Azam Chaudhry (Lahore School of Economics), Shamyla Chaudhary (Lahore School of Economics), Amit Khandelwal (Columbia University), Eric Verhoogen (Columbia University) and David Atkin (MIT)</p>
		
		<p>
		The study investigates the extent to which fixed costs of innovation are a barrier to upgrading in the surgical-goods sector in Sialkot. <br><br>
The project has been developed under the International Growth Centre (IGC) and the Private Enterprise in Developing Countries (PEDL) initiative. In this project, they investigate the extent to which fixed costs of innovation are a barrier to upgrading in the surgical-goods sector in Sialkot. If there are externalities in the process of innovation, there may be an important role for governments in subsidizing the costs of innovation. A leading policy idea is to provide matching grants, in which governments provide supplemental funding for projects chosen by individual firms. However, there has never been a rigorous randomized evaluation of such a program (Campos et al, 2012). They are conducting such an evaluation in the surgical-goods industry in Sialkot and plan to offer matching grants for innovations that increase exports.<br><br>
The team aims to develop a randomized trial to incentivize firms to enter new product lines. We have developed a census of producers in the surgical goods sector in Sialkot using a short survey.<br><br>  
The team aims to launch the intervention by December 2018 and complete the follow up surveys by October 2020. 

		</p>
		
