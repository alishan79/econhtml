		<h3>Technology and Innovation Mapping Project</h3>
		<p><b>Researcher: </b>Azam Chaudhry (Lahore School), Theresa Chaudhry (Lahore School) and Mahvish Faran (Lahore School; PhD. candidate, Sheffield University)</p>
		
		<p>
		The aim of this initiative is to map the production process and the technologies used in the soccer ball and readymade garments sector. Then using this map, compare the technologies in these sectors with the forefront of international technologies to see how far behind firms in Pakistan are. Finally, the research team will see what incentives can be given to Pakistani firms to adopt new technologies (like financial incentives, supplying information about these technologies, providing assistance of adoption of these new technologies, etc.).<br><br>
		
Collaborators include Rajah Rasiah (University of Malaya).

