		<h3>Innovation and firm performance in developing countries: The case of
Pakistani textile and apparel manufacturers
</h3>
		<p><b>Researcher: </b>Waqar Wadho (Lahore School), Azam Chaudhry (Lahore School)</p>
		
		<div align="center"><img src="images/firm.png"></div><br />
		<p>
		The project has been developed under the International Growth Centre (IGC). Innovation and competitiveness are considered to be the backbone of private sector led economic growth and its sustainability. However, in Pakistan, very little is known about the nature and the extent of innovation as well as its determinants and obstacles. In the absence of such evidence, policies can be misdirected and ineffective. In this project, the researchers surveyed a sample of 500 textile firms in Punjab and Sindh. The survey measured both the technological (product and process) and the non-technological (organizational and marketing) aspects of innovation where an innovation is the introduction of a new or significantly improved product, process, organizational method, or marketing method by an enterprise. Through this survey, the researchers measure 'innovation' in at least four different ways: (1) Innovation rate (based on the response of enterprise if they had innovated during 2011-13, which is further be divided into four types of innovation; i.e. product, process, organizational and marketing), (2) Innovation input (given by the ratio of R&D expenditure to sales), (3) Innovation output (given by the ratio of new products sales to total sales), and (4) Innovation efficiency (given by the ratio of new product sales to R&D expenditure).<br>
Through this study, the researchers answers the following research questions:

		</p>
		<ul>
			<li>How innovative is the textiles sector of Pakistan (measured in four different ways as described above)?</li>
			<li>What are the correlates (both in terms of promoting and hampering) of innovation? (such as firm type and size, market structure, product market i.e. local or international, government regulations and facilitations, sources and type of cooperation, management patterns, skill profile, ICT, access to finance and source of finances, information on technology and markets, and demand for innovations etc.).</li>
			
		</ul>
		
		<p>
		Using unique innovation survey data collected from a homogenous sample of firms in Pakistan, this paper presents an analysis of the firm level determinants of product innovation and its impact on firm performance. We employ a multi-stage structural model linking the decision of a firm to innovate, its innovation investment, product innovation, and firm performance using primary data from the textile and wearing apparel sector, which is the largest export sector of Pakistan. We find that product innovation leads to increased labor productivity as well as higher labor productivity growth. A 10 percent increase in innovative sales per worker is associated with a greater than 10 percent increase in labor productivity and labor productivity growth. On the determinants of innovation, we find that vertical knowledge flows from foreign clients and suppliers are important determinants of a firm's decision to innovate. Larger firms are more likely to engage in innovation, however, there is no significant evidence that they invest more in innovation. Exporting is positively associated with innovation performance and firms exporting to Europe and America are more likely to engage in innovation. There is mixed evidence on the impact of competition: foreign competition adversely affects a firm's decision to innovate, whereas, local competition increases investment in innovation. Subsidies seem to have a crowding out effect since firms receiving national subsides invest less in innovation. Furthermore, firms that have higher investment in innovation, that are more productive, and that introduce organizational innovations have higher innovative sales per worker.<br><br>
<a href="https://reader.elsevier.com/reader/sd/C3D6F15065ABF3B85C8BFB739428E738267F04A6C2B701E08892DF72510509AE83F0D62DA069F888E8E20DBBD388EA99" target="_blank">Published in Research Policy in 2018, Volume 47, Issue 7, Pages 1283-1294.</a>


		</p>
		
