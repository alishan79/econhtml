		<h3>Analysis of Technology in the Sialkot Soccer Ball industry</h3>
		<p><b>Researcher: </b>Azam Chaudhry (Department of Economics, Lahore School), Shamyla Chaudry (Department of Economics, Lahore School), Eric Verhoogen (Department of Economics, Columbia University), Amit K. Khandewal (Graduate School of Business, Columbia University) and David Atkins (Department of Economics, MIT)</p>
		<div align="center"><img src="images/soccar.png"></div><br />
		<p>
		The project has been developed under the International Growth Centre (IGC) and the Private Enterprise in Developing Countries (PEDL) initiative. The Sialkot soccer ball cluster is made up of a large number of manufactures who export approximately 60 million hand-stitched soccer balls each year, accounting for about 70 percent of global production. But over the last few years, the amount of soccer balls produced in Sialkot has fallen. This project aims to better understand the technologies within the industry and determine the characteristics of the firms that are able to identify and implement new technologies.<br><br>
The research team developed a cost saving technology in 2012 which it has given to a number of soccer ball manufacturers in Pakistan in order to determine the factors affecting technology adoption in firms and technology spillovers between firms. The research team has also looked at how misaligned incentives between firms and employees can affect technology adoption. The preliminary data analysis was presented at the IGC Growth Week in September 2012 and September 2013 in London. The researchers have presented this work in seminars and conferences at University of Berkley, Yale University, Columbia University, Harvard University and University of Oxford. It has also been presented at the Government of Punjab in 2015. After these presentations the Secretary Finance and the Secretary Industries, Punjab Government used the project's findings to propose and initiate an Innovation Development Fund (IDF). Currently, the Punjab Government has initiated this fund and the Lahore School is represented on its executive board. The findings of this project have been published in:<br><br>
Published Article: <a href="https://academic.oup.com/qje/article/132/3/1101/3064351" target="_blank">Atkin, A., Chaudhry, A., Chaudry, S., Khandelwal, A. K and Verhoogan, E. "Organizational Barriers to Technology Adoption: Evidence from Soccer-Ball Producers in Pakistan". Quarterly Journal of Economics (2017)</a>
<br><br> 

Published Article: <a href="http://www.columbia.edu/~ev2124/research/AtkinChaudhryChaudryKhandelwalRaza&VerhoogenWBER2016_advaccess.pdf" target="_blank">Atkin, A., Chaudhry, A., Chaudry, S., Khandelwal, A. K., Raza, T., and Verhoogan, E. "On the Origins and Development of Pakistan's Soccer-Ball Cluster". World Bank Economic Review (2016)</a>

<br><br>

Published Article: <a href="http://www.columbia.edu/~ev2124/research/AtkinChaudhryChaudryKhandelwal&VerhoogenAERPP2015.pdf" target="_blank">Atkin, A., Chaudhry, A., Chaudry, S., Khandelwal, A. K and Verhoogan, E. "Markup and Cost Dispersion across Firms: Direct Evidence from Producer Surveys in Pakistan". American Economic Review, Papers and Proceedings (2015).</a>


