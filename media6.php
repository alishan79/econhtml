		<p>The economic problem we now face cannot be traced solely to the previous government's stubborn refusal to adjust the overvalued exchange rate. Our economic managers appear to have lost the plot over the last two years. For one, they were unable to keep track of CPEC-funded investment flows, whose exact form of financing has never been made transparent. The second and perhaps more important reason for our plight is that the federal and some provincial governments decided to go on a spending spree - launching projects, oblivious to their cost and foreign exchange implications. This is not new: the last two governments were equally guilty.<br><br>
The current state of economic affairs cannot be allowed to continue. Some important decisions may need to be taken in the crunch, even by the interim government in the national interest. The simple reason for this is that, unless some immediate measures are taken to restore business confidence and, most importantly, to calm the foreign exchange market, the exchange rate will continue to fall. In the extreme scenario, we could enter a freefall situation. Given this uncertainty, anybody with some staying power will not be willing to part with their US dollars, betting that the rupee will fall even more. Those wanting foreign exchange will be chasing less and less available in the market.<br><br>
Yet, nobody will bail us out, whether it is the IMF or anyone else, without imposing 'conditionalities' - primarily to ensure that they get their money back. Here, our team of negotiators (from the finance ministry and State Bank) must learn some lessons from the past. The last two governments entered into agreements with the IMF almost immediately upon coming into power. The 2008 agreement with the IMF was an unmitigated disaster in terms of its impact on growth, which fell from near 6pc to less than 1pc. The economy never quite recovered after that. The 2013 agreement, partly due to the groundwork done by the interim finance team, was able to avoid this shock through a more gradual decline in the fiscal deficit. However, agreeing almost immediately to a reforms agenda was unwise. To the extent possible, the new government should seek some time to finalise the content and sequencing of economic reforms, for which it can take full ownership and deliver.<br><br>

The immediate challenge will be to agree to a stabilisation package, at an appropriate speed and sequencing of adjustment, that protects the country's economic interests. Despite its weak bargaining position, the government should work towards a stabilisation package in which the burden of adjustment primarily falls in a sequenced way on the fiscal deficit rather than on the exchange rate. This is not to deny that we need to adjust the exchange rate, but we must keep this limited to its current overvaluation. We must remain fully aware that the cost of a very steep devaluation is especially high for our heavily indebted economy. Doing so would also raise the value of imports, especially oil products, fuelling inflation and eroding competitiveness. To that extent, it would neutralise the gains from devaluation. Most importantly, it would increase the cost of our defence preparedness, which in the current volatile situation cannot be compromised at any cost.<br><br>


Of course, cutting the fiscal deficit is not without cost, even if the decline is made gradual. A 2pc drop in the fiscal deficit would reduce our current GDP growth of around 6pc to near half this amount. Most importantly, to counteract this, we must put in place measures that allow the recent growth momentum to build on the revival of manufacturing and upturn in exports and create the climate to encourage the much-awaited revival in private investment. All this will ensure that the decline in GDP growth is minimised. The emphasis here should be on reversing the anti-export trade and tariff regime and making a serious attempt at cutting down on losses from public-sector enterprises. This should entail including workers and their elected representatives in any restructuring negotiations.<br><br>

Over the medium to long term, the policy focus must shift to expediting coal mining in Thar (which could finally remove our dependency on imported oil and gas), preserving and supplementing our water resources, and switching the emphasis in education from merely increasing numbers to improving the quality of education imparted and the social skills of our graduates.<br><br>

If seriously and successfully monitored and implemented, this agenda will likely keep the newly elected government busy through its term in office. Come 2023, it will be judged on these achievements. Inshallah.
</p>
		
