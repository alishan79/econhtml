		<br>
		
		<h3>Female Empowerment and the Uptake of Maternity Care Services in Pakistan</h3>
		<p><b>Researcher: </b>Amber Masood (2015)</p>
		
		<h5>Abstract</h5>
		
		<p>
According to the UNICEF Report (2013), the maternal mortality ratio (MMR) in Pakistan is very high as opposed to the MDG 5 target1. This high maternal mortality can be reduced considerably if the females utilize the required maternity care services. However, in terms of the uptake of antenatal care, skilled birth attendants and post-natal care, Pakistan is lagging behind other South Asian Countries (UNICEF Report, 2013).This brings us to the question that despite such high mortality rates, why are females not utilizing the required services for the sake of their own and their newborn's health? One of the factors, which is a rising focus of demographic and health research and needs to be explored in much more detail for Pakistan, is the empowerment of females. Using information from Pakistan Demographic and Health Survey (2012-13), on currently married females (aged 15-49), who had a baby within five years prior to the survey, this study aims to look at the impact of different dimensions of empowerment on maternity care uptake. Female empowerment is divided into three dimensions: behavioral, attitudinal and exposure to domestic violence. To address the possible endogeneity, an IV Approach combined with Cluster Fixed Effects is used and couple's age difference and female's premarital empowerment status (proxied through her mother's exposure to domestic violence) are used as instruments. According to the results, all dimensions of female empowerment have a significant impact on antenatal care but safe delivery postnatal care remains unaffected by it.<br><br>
