		<br>
		
		<h3>The Impact of Remittances and Parental Absence on Children's Wellbeing in Rural Punjab</h3>
		<p><b>Researcher: </b>Nida Jamil (2014)</p>
		
		<h5>Abstract</h5>
		
		<p>
This study examines the impact of migration on children left behind in terms of schooling and child labor by quantifying both aspects of migration, i.e., remittances and parental absence. In particular, it focuses on cases where the father is the migrant. The study is based on a panel analysis of data drawn from the Multiple Indicator Cluster Survey for 2007 and the Privatization in Education Research Initiative survey for 2011. The sample comprises 820 households with children aged 5-14 years. The study uses the instrumental variable approach as well as household fixed effects and random effects to resolve any possible endogeneity. Exogenous variations in parental absence and remittances sent by migrants from a given kinship network are employed as instrumental variables. The study finds that (i) an increase in remittances of PRs 1,000 ($10) raises the probability of being enrolled in school by 13 percentage points, and (ii) the absence of the father increases the probability of a child engaging in labor by 25 percentage points. Remittances, while benefiting the household, emerge as an incomplete substitute for the absent father. This effect is particularly strong for children who already lack a mother due to death or divorce. The mother's presence, however, compensates fully for the father's absence. Moreover, the father's absence has worse consequences for girls in terms of increased child labor, where even the money coming in through remittances is more likely to be spent on boys.<br><br>
Published Working Paper: <a href="http://www.creb.org.pk/uploads/Working%20Paper%20Series%20No.03-15%20Complete.pdf" target="_blank">CREB Working Paper No. 03-15.</a>


		</p>

