		<br>
		
		<h3>The Determinants of Child Health and Nutritional Status in Punjab: An Economic Analysis</h3>
		<p><b>Researcher: </b>Uzma Afzal (2012)</p>
		
		
		<p>
Abstract Child health is considered a key indicator of economic development and the quality of life in developing countries. It is also closely related to other development indicators such as adult health, educational attainment, income, and occupational productivity. In the past few years, despite having had clear health targets in the form of the Millennium Development Goals, Pakistan has been unable to attain significant progress in improving child and maternal health. This study focuses on the health and nutritional status of children in Punjab. It attempts to identify the socioeconomic factors that affect child health at the household level, and presents policy recommendations based on its findings. The study's theoretical framework is based on the household production model, and we use the instrumental variable technique for estimation purposes. The Multiple Indicator Cluster Survey for 2007/08, a household-level dataset gathered by the Punjab Bureau of Statistics, provides our sample data. The study's results imply that maternal education and health knowledge are important determinants of child health, among other significant indicators.<br><br>
Published Working Paper: <a href="http://www.creb.org.pk/uploads/Working-Paper-No.-02-12-Final-Complete.pdf" target="_blank">CREB Working Paper No. 02-12.</a>


