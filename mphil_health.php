		<h3>The Impact of Early Childbearing on Child Health in Punjab</h3>
		<p><b>Researcher: </b>Aeman Nadeem (2017)</p>
		
		<h5>Abstract</h5>
		
		<p>
Early Childbearing is a major social and public health concern. Empirical studies have focused on both the consequences and causes of early childbearing. Much of the empirical work has focused on the impact of early childbearing on children's developmental and academic outcomes. However, there is limited research pertaining to the effect of early childbearing on child health outcomes in Pakistan. Using the newly available data, Multiple Index Cluster Survey for Punjab 2014; the paper aims to test if early childbearing affects child health outcomes, both in the short and long term. In addition to this, studies have established that the decision of early childbearing is influenced by a wide array of factors which include individual and household level characteristics as well as social and cultural norms. The paper also deals with the issue of omitted variable bias concerning early childbearing. As part of the estimation strategy, the paper employs ordinary least square, cluster fixed effects and household fixed effects. The empirical results shows that early childbearing exerts negative impact on child health outcomes. However; controlling for unobserved characteristics at household and cluster level shrinks the size of the coefficient of early childbearing compared to a simple ordinary least square estimate; indicating the influence of important household and cluster unobserved factors on early childbearing decision. The results of the paper also confirms that the analysis is robust to changing the specifications of early childbearing as well as the data set used in the analysis.<br><br>
