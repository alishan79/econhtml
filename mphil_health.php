		<a name="Fasting"></a>
		<br>
		
		<h3>The Impact of Prenatal Exposure to Fasting on Child Health Outcomes </h3>
		<p><b>Researcher: </b>Azka Sarosh Mir (2017)</p>
		
		<h5>Abstract</h5>
		
		<p>
Early-life factors play an important role in fetal development, according to Barker's (1990) 'womb-with-a-view' hypothesis. This study examines how prenatal exposure to fasting in the month of Ramadan has an impact on child health outcomes in terms of height-for-age and weightfor-age z-scores. In the absence of actual reported fasting behaviors, we use the 'intent-totreat' (ITT) approach, as applied in randomized control trials. This allows one to measure the unbiased impact of an intervention, even when there is imperfect compliance with random assignment into control and treatment groups. While the ITT approach does not require or assume that all women pregnant during Ramadan necessarily fast, it is critical for Ramadan to remain exogenous to the timing of pregnancy. In other words, we assume that women do not intentionally time their pregnancies to fall outside of Ramadan. The ITT framework is used to compare the health outcomes of two groups of children under five in the Punjab - those whose period in utero coincided with the month of Ramadan (the treatment group) and those who were not exposed (the control group). This is done without using any information on maternal fasting behavior. In each case, the child's date of birth establishes whether the pregnancy overlapped with the month of Ramadan and this information is used to construct a set of gestational month-of-exposure variables. The data for this analysis is drawn from the Multiple Indicator Cluster Survey for 2008 and 2011, carried out by the (Punjab) Bureau of Statistics. The study's results indicate that prenatal exposure to fasting during the first two trimesters has negative implications for children's height-forage. In addition, children who were prenatally exposed to fasting in the second and third trimesters were, on average, thinner than nonexposed children. We find no evidence of selection bias arising from the decision to selectively time a pregnancy to avoid Ramadan - a major concern of our study.<br><br>
Published Paper: <a href="http://www.creb.org.pk/uploads/WorkingPaperSeriesNo.01-18.pdf" target="_blank">CREB Working Paper No. 01-18.</a>

		</p>

