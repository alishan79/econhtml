		<h3>Effects of Birth Order on Child Nutritional Status</h3>
		<p><b>Researcher: </b>Maha Khan (2014)</p>
		
		<h5>Abstract</h5>
		
		<p>
Birth order and the associated parental discrimination are evidenced to detrimentally affect a child's long-term nutritional status. This research explores the in depth role of birth order in determining child nutritional status through a between- and intra- family analysis of stunting, wasting, and underweight in children. It further tries to evaluate whether the child's height-for-age varies with gender; and if the drop off in height with each additional birth order can be attributed to pre and post-natal health disinvestments in pregnancies and births. The Punjab Multiple Indicator Cluster Survey for 2011, a household-level dataset gathered by the Punjab Bureau of Statistics, provides our sample data. The results of this study imply that birth order has negative effects on child health, with child height and weight gradient monotonically declining with increasing birth order children. Moreover, birth order effects become stronger in larger families, even after controlling for birth spacing. Yet, we find only limited evidence of gender based postnatal disinvestment on mothers in household fixed effects regressions, while no gender specific effects were observed for child health investments.<br><br>

