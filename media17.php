		<h3>Firms Experience Lower Growth, Investment</h3><br>

		<p>As Pakistan transitions from a low growth to a higher growth trajectory, the business community will have to take a leading role, especially for export promotion and employment generation. Recently the Technology Management & Innovation Centre of the Lahore School of Economics conducted its third business confidence survey of firms with the Lahore Chamber of Commerce & Industry (LCCI). Data was collected from 445 firms who completed the 2017 year's survey, and firms that responded were from a variety of sectors (manufacturing, services and retail sectors) which helped in finding the differences in the economic performance across various sectors.  </p>
		
		<p>The survey began by looking at how both domestic and global factors impacted firm's output. Comparison of revenues from total sales between this years' and last years' survey results revealed that across industries total sales have decreased (as this year only 45% of firms have reported an increase in total sales as compared to 49% of firms in the previous year). The manufacturing sector seems to been the most affected as only 43% of manufacturing firms have reported increase in revenues from total sales in this year as compared to 61% of firms in the previous year. Similarly, this year only 24% retail firms experienced an increase in total sales as compared to 31% firms in the previous year. On the other hand, it is interesting to note that 10% more firms from the services sector have reported an increase in their total sales this year, with 61% of firms reporting an increase in total sales this year as compared to 51% of firms in the previous year.
</p>
		
		<p>When the firms were asked about their expectations about sales in 2017, it was revealed that firms were less optimistic about total sales in the current year as compared to last year. In particular, both manufacturing and retail firms were less optimistic about sales over the coming year. The main reason behind these lower expectations may be the decline in exports, as well as the current economic uncertainty.<br><br>

Since investment is considered a critical component of growth, firms were also asked to compare their level of investment in 2015 with 2016 alongside expectations about investment in 2017. This year's survey results showed a slowdown in investment by firms compared to last year's survey results. The survey showed that the largest slowdown in investment in 2016 came in the manufacturing sector, followed by the retail sector. However, services sector firms were more resilient and these firms had almost the same level of investment as compared to last year. <br><br>

Bank borrowing is also an important aspect of investment. When firms were asked about their expectations over the current year, a significantly smaller percentage of manufacturing and retail firms said that they plan to increase their investment in 2017 as compared to the previous year. However, service sector firms seemed to be more optimistic with a significantly greater number of firms planning to increase their investment in 2017. <br><br>

When asked about comparing bank borrowing in 2016 to 2015, it was found that 17% more firms said that their bank borrowing increased as compared to the previous year which shows that increases in investment levels were generally financed by bank borrowing in 2016. This is an important result because it shows that the financial sector in Pakistan may be better meeting the financial and investment needs of the business community as compared to previous years. Although there was only slight increase in bank borrowing in the case of the manufacturing sector firms, both the services and the retail sector firms significantly increased their bank borrowing over the last year.<br><br>

However, when asked about expectations about bank borrowing in the current year it was found that except for the service sector firms, most firms did not plan on utilizing bank borrowing facility to finance their planned investments. So, in spite of the prevailing low interest rates in Pakistan, these results show that the business community is uncertain about expansions in their investment levels and thus they are not relying much on the financial sector in the current year. <br><br>

Private sector employment is also a critical issue for any developing country. This year's survey also showed that there was reduction in the overall employment levels reported by firms. Both the manufacturing and the services sector firms reported significant reductions in the number of employees. Moreover, most firms did not seem optimistic about increases in their employment levels in 2017 as this year's survey results showed that most firms are planning a significant reduction in their employee base in 2017.<br><br>

This reduction in employment trend in all three sectors may be because of the different set of constraints faced, such as lower anticipated domestic & export sales and higher cost of inputs. Lack of optimism regarding sales growth in these sectors may have added to firms being skeptical about increasing employment in 2017.<br><br>

Finally, in order to get a general sense of feeling amongst the firms concerning the state of Pakistan's economy, firms were also asked a general question about their expectations about the Pakistani economy in 2017. There was a significant increase in the percentage of firms who had positive expectations about the Pakistan's economy, with 58% of firms expecting the economy to do better in 2017 as compared to 51% of firms from last year's survey. Interestingly the level of optimism has mainly come from the service sector firms, with the percentage of service firms expecting an economic improvement increasing from 53% in 2015 to 60% in 2016. However, there was a slight decline in the level of optimism in the case of the manufacturing and the retail sectors. <br><br>

The recent trend in GDP growth, improvements in law and order conditions, lower inflation, lower interest rates and expected completion of various energy and infrastructure projects may be some of the factors driving this optimism. Whereas, the deterioration in Pakistan's balance of payments due to a decline in exports and increase in imports may be some factors which could explain the uncertainty found amongst manufacturing and the retail sector firms.<br><br>

The overall analysis showed that while manufacturing and the retail sectors registered declines in their growth and employment rates, the service sector emerged as a key sector experiencing higher growth and higher employment rates. This suggests that there that the government should be proactive in formulating strategies to encourage firms and find new avenues to further improve the growth in this sector, which in turn can positively affect output, employment and exports.

