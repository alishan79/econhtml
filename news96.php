	<h3>Orientation of Lahore School Economics Society</h3><br>
		<p>The Lahore School Economics Society (LSES) organized its first orientation on November 17, 2014. Dr. Shahid Amjad Chaudhry, Rector of the Lahore School was the guest of honour. A large number of students and faculty (including Dr. Azam Chaudhry, Dr. Theresa Chaudhry, Shamyla Chaudry and members of executive council) attended.</p>
		

		<div align="center"><img src="images/n19.jpg" width="450px"></div>				
		
		<p>First, Dr. Waqar Wadho, Patron of the Society, thanked Dr. Shahid Amjad for providing this platform and addressed students on LSES' aim to provide them with a platform to connect with fellow economics students within and outside the Lahore School (both within and outside Pakistan), to connect with eminent economists (Pakistani and foreigners) and to connect with professionals (including Lahore School alumni) to form a vibrant economics community that is more informed, engaged and aware of the scope of opportunities and relevance of economics.</p>


		<div align="center"><img src="images/n20.jpg" width="450px"></div>				
		
		<p>The President and Vice President of the LSES Council briefed attendees about the proposed activities that include lectures/guest speaker sessions and seminars with economists and other professionals; competitions including an international essay competition, yearly national budget sessions, and quiz competitions; regular sessions with the Dean/Rector; and creation of a central information system that would actively gather information on different opportunities available for students pursuing economics as a major.</p>
		
<p>This was followed by an address by Dr. Theresa Chaudhry, Associate Professor, who enlightened students about the ongoing research collaborations between the Lahore School of Economics and other universities including Oxford, Columbia, Yale and Warwick, and international institutions such as the International Growth Centre. These collaborations have led to research on microfinance, distribution of public services, and manufacturing including the football, fans, and garment sectors.

</p>

		
		
		<div align="center"><img src="images/n21.jpg" width="450px"></div>

		<p>Then, Dr. Azam Chaudhry, Dean Faculty of Economics addressed the gathering and greatly applauded the efforts of the Society. He spoke about different collaborations and exchange programs with international universities (Lahore School-Oxford doctoral exchange program and Memorandums of understanding which cover areas like student and faculty exchange with Bahçesehir University in Turkey, University of Colombo in Sri Lanka and with Macquarie University in Australia. Dr. Azam also discussed a variety of different course combinations offered at the School that makes it a unique institution, as well as the job market and scholarships prospects for economics students.</p>
		
		<p>Dr. Shahid Amjad Chaudhry, Rector, Lahore School of Economics congratulated the Council and the Society members for this initiative. Dr. Shahid Amjad elaborated on both the teaching and research sides of the Lahore School that make it what it is. He told students that the course combinations and teaching facilities at Lahore School are of high standards, this is evident from the recent acknowledgement by the National Business and Accreditation Council that gave Lahore School the highest ratings in Pakistan. He informed students that the Lahore School produces top quality research that is published in international journals, and both the government and private sectors repeatedly seek policy guidance from Lahore School researchers. What makes it even more unique is that Lahore School researchers do not charge for these research studies and they are done purely for academic purposes.</p>
		
