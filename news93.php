		<h3>Lahore School of Economics Hosts Former Greek Finance Minister</h3><br>
		<p>"In Greece the GDP fell by 25% in five years, unemployment reached 28% while public debt as percent of GDP rose to 175% reflecting mainly the collapse of output," according to Dr. Yannos Papantoniou in a lecture delivered at the Lahore School of Economics March 5, 2015.</p>
		<div align="center"><img src="images/n11.jpg" width="450px"></div>
		
		<p>Dr. Papantiniou, Former Economy and Finance Minister of Greece (1994-2001) and President of the Centre for Progressive Policy Research, in his lecture "Economic Crisis in Greece and the Future of the Euro" was of the view that if talks between the lenders (ECB, IMF, and the EU) and Greece stall, Greece's economy would sink further into recession, and social tensions would rise as living standards would register a further sharp decline.</p>
		
		<div align="center"><img src="images/n12.jpg" width="450px"></div>
		
	<p>He emphasized that Greece's economic problems did not arise during the process of acceding to the euro area, but began when large current imbalances emerged among member countries soon after the currency union's creation in 2000. Massive current account deficits in the weaker economies led to the accumulation of public and private debt while the northern Europeans were running surpluses. The policies that the eurozone has initiated to tackle the debt crisis have been inadequate and even self-defeating. 

</p>
		
		<div align="center"><img src="images/n13.jpg" width="450px"></div>
		
		<p>"Greece's never ending economic crisis revealed critical flaws and weaknesses in the eurozone's constitution," according to Dr. Papantiniou. In a currency union, individual economies cannot alter their exchange rates to account for changes in relative competitiveness. The resulting price stickiness tends to delay macroeconomic stabilization and structural adjustment, leading to rising debt and unemployment in weaker economies. Without free labour mobility, fiscal transfers are the euro area's only option to ease debt repayment and, by stimulating economic activity, boost employment.</p>
		<p>Moving to a fiscal union, along the lines of the U.S. federal model, will not be easy according to Dr. Papantoniou, as it requires a resource that is in short supply in Europe today: mutual trust, particularly between the north and the south. Binding the union closer together could prove critical to building such trust. One strategy that combines rationality with the gradualism needed to overcome political resistance would be to increase the union's central budget so that it can ultimately play a macroeconomic role, promoting stability and reinforcing cohesion in the euro area. He stressed that "it is a tough sell, but also a vital one."</p>		
		
		<div align="center"><img src="images/n14.jpg" width="450px"></div>		
		
