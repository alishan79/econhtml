		<h3>Pakistani workers Resist New Technology</h3><br>
		
		<p>Pakistan's soccer ball manufacturing industry is facing a major challenge internationally because of increasing production costs and quality issues. Research at the Lahore School of Economics has found that one of the main reasons for this is that workers in the soccer ball industry resist new technology. </p>
		
		<p>All of Pakistan's soccer ball production is concentrated in Sialkot, which remains the major source for the world's hand stitched soccer balls. In recent years the industry has faced fierce competition from East Asian countries, especially China, which has significantly hurt exporters. In order to sustain its share in the global market, the industry desperately needs to invest in innovation and adopt to new trends in order to achieve and sustain a competitive advantage over its foreign competitors.</p>
		<p>Researchers at the Lahore School of Economics studying the cluster of soccer-ball producing firms in Sialkot found that workers in the soccer ball industry tend to resist new technology, even if it was given for free. This was despite the fact that the use of a new cutting technology (or "cutting die") invented by the researchers could achieve more pentagons per sheet of rexine (artificial leather), thus reducing rexine cost per pentagon as compared to the traditional technology. Adoption of this die could reduce the total cost by approximately 1% and even though this number was smaller in absolute terms, given the low profit margin of around 8% in the industry, the net benefit from adopting this new technology was quite significant for the industry's competiveness. </p>
		<p>The researchers found that workers resisted the new technology because they were worried that their overall wages would fall since most of the employees in this sector are typically paid piece rates, with no incentive to reduce waste, and they thought that the new technology would slow them down. To further investigate the reasons behind slow adoption, the researchers paid the factory workers Rs. 15,000 to practice with the new technology. This modest change in the incentive payment lead to the exciting result that workers started to adopt the new technology. </p>				
		
		<p>The researchers concluded that to bring technological innovation to life, both technology adoption and organizational changes have to go hand in hand. The findings also suggested that the firms have to become more flexible in accommodating modified organizational structure and workable wage contracts. A mechanism where the employees are expected to share some gains from new adoption is absolutely crucial if important sectors in Pakistan are to grow in terms of technology.</p>
		<p>This project was funded by IGC and PEDL and the researchers included Dr. Azam Chaudhry, Professor and Dean, Lahore School of Economics, Shamyla Chaudhry, Assistant Professor, Lahore School of Economics along with fellow researchers including David Atkins (Department of Economics, MIT), Amit K. Khandewal (Graduate School of Business, Columbia University), and Eric Verhoogen (Department of Economics, Columbia University).		
		
