		<a name="Microinsurance"></a>
		<br>
		
		<h3>Microinsurance Demand after a Rare Flood Event: Evidence from a Field Experiment in Pakistan</h3>
		<p><b>Researcher: </b>Farah Said (Lahore School), Uzma Afzal (University of Nottingham) Ginger Turner (World Bank)</p>
		
		<p>
		This paper examines the characteristics that determine demand for microinsurance when individuals have personal or observed experience with a rare weather event: the severe 2010 flooding in Pakistan. Using a sample of 384 individuals (192 in flood-affected and 192 non-affected villages matched using pre-flood propensity data), we combine post-flood survey data with behavioural experiments to test the impact of prior loss experience on willingness to purchase insurance. 
In the framed experiment setting, we allow participants to choose insurance payments through many rounds of random flood losses, testing whether experiment behaviour is significantly related to real-world experience or observation and whether individuals change insurance demand after experiencing losses.<br><br>
 We find that 2010 flood-affected individuals demand significantly more insurance than non-affected individuals, and that both personal losses and observations of others' losses are significant determinants of demand, when controlling for location-specific flood propensity, pre-flood mitigation, information sources, post-flood assistance, exogenous changes in assets, potential migrant attrition and other household characteristics. <br><br>
Having prior experience with less severe floods before the 2010 event also increases insurance demand, although the effect disappears when controlling for 2010 flood. Contrary to expectation, household beliefs that insurance is non-Islamic are not found to be a significant barrier to take-up.<br><br>
Published Article: <a href="https://ssrn.com/abstract=2476088" target="_blank">Turner, Ginger and Said, Farah and Afzal, Uzma, Microinsurance Demand After a Rare Flood Event: Evidence From a Field Experiment in Pakistan (August 4, 2014). Geneva Risk and Insurance Review, Vol. 39, No. 201-223, 2013. </a>

		</p>
	
