		<a name="Consanguineous"></a>
		<br>
		
		<h3>Consanguineous Marriage and Investments in Children </h3>
		<p><b>Researcher: </b>Theresa Chaudhry (Lahore School), Rabia Arif (Lahore School)</p>
		
		<p>
		Hamilton's Rule (1964), taken from evolutionary biology, suggests that people will display greater altruism towards individuals that are more closely related to help ensure the survival of their own genes. Dr. Theresa Chaudhry (Lahore School) and Rabia Arif (Lahore School) are working on an analysis of the impact of consanguineous (first cousin) marriage on children, including child labor, household chores, education, and nutrition, to see whether children of cousin marriages may be the recipients of greater altruism due to the stronger genetic tie. <br><br>
Results are showing that children of consanguineous parents are as likely to attend school, attend private school, and be engaged child labor as other children. Also, educational expenditures do not seem differ either between the two groups. However, children of consanguineous parents miss more school and do less work at home (especially boys) as compared to children of non-consanguineous parents.

		</p>
	
