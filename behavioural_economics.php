		<a name="Force"></a>
		<br>
		
		<h3>Overcoming Constraints to Female Labor Force Entry </h3>
		<p><b>Researcher: </b>Hamna Ahmed (Lahore School), Mahreen Mahmud (Lahore School), Farah Said (Lahore School) and Zunia Tirmazee (Lahore School)</p>
		
		<p>
		The project has been funded by the Private Enterprise Development in Low-Income Countries (PEDL) Exploratory grant, 2017. This study is a randomised controlled trial to test the impact of two low-cost interventions to overcome psychological and information constraints to female labor force participation (FLFP). The research questions that this project aims to address are, (i) does motivating female students and (ii) does providing information about the job market, promote female labor force entry. <br><br>
The project focusses on female students, in their final year undergraduate degree, and enrolled in women only Public colleges in urban Lahore, the second largest metropolitan city in Pakistan, comprising of approximately 11 million people. While socioeconomic background, information on available jobs and job skills are significant determinants of entry into the labor market (Humphrey et al 2009; Jenson, 2012); studies have shown only modest impacts of job search assistance and skills training on employment and wages (McKenzie 2017). <br><br>
Further, skills training programmes in particular often suffer from low enrolment (Cheema et al., 2012, 2015) and high cost (Adhoho et al., 2014); suggesting it is time to think of new interventions that can complement traditional skills training programmes to promote employment. One possibility for these results may be presence of psychological barriers and low motivation as binding constraints to FLFP which this project aims to address.

		</p>
	
