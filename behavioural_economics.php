		<a name="Pressures"></a>
		<br>
		
		<h3>Pressures from peers and spouses and self-control problems as constraints to microenterprise growth: Experimental evidence from Pakistan</h3>
		<p><b>Researcher: </b>Farah Said (Lahore School), Uzma Afzal (Lahore School), Marcel Fafchamps (Stanford University)  and Giovanna d'Adda (University of Milan). </p>
		
		<p>
		This study will assess the role of self-control problems and peer pressures on take-up and use of a financial product by female micro-entrepreneurs in Pakistan by combining laboratory and field experiments. The project received International Growth Centre (IGC) funding in March 2015 and experimental sessions were carried out in Chakwal in April-May 2015. A second round of experiments was conducted in April 2017. <br><br>
Results indicate that female involvement in household decision making is decreasing in the importance of the decision, a. Authors find no evidence that women have pent up demand for agency. Instead, women are less willing to pay for agency when facing an unknown man. This evidence suggests that women in the study population have internalized gender norms, and that these norms regulate interactions between genders most strongly outside of the household. <br><br>
Status: Fieldwork completed, data analysis under way. Preliminary results presented in various seminars in the US, SEEDEC 2018 (expected). <br>
Expected publication: Oct 2018.

		</p>
	
