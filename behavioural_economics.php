		<h3>Constraints to Female Entrepreneurship in Pakistan: the role of women's goals and aspirations </h3>
		<p><b>Researcher: </b>Farah Said (Lahore School), Giovanna d'Adda (University of Milan), Mahreen Mahmud (University of Oxford) and Diego Ubfal (Bocconi University)</p>
		
		<p>
		This research is funded by the International Growth Centre (IGC) and will be conducted in collaboration with National Rural Support Programme (NRSP). Evidence on the impact of microfinance loans on business outcomes shows that the effects are moderately positive, but not transformational, especially for women (Banerjee, 2013; Banerjee, Karlan, & Zinman, 2015). This is confirmed even when access to finance is coupled with business training (Gine and Mansuri, 2016, Berge, Bjorvatn and Tungodden, 2014), suggesting that other factors, beyond credit and knowledge constraints, reduce women's ability to start or grow a business. <br><br>Psychological and social factors, limiting female autonomous decision making and control over resources, have been cited as important obstacles to the impact of microfinance and business training programs (Jakiela and Ozer 2016, Field, Jayachandran and Pande, 2010, Field et al. 2015, De mel, Mckenzie and Woodruff 2014). Psychological factors include self-control problems, feeling incapable of resisting demands from peers and family members and low perceived self-efficacy. Social factors may include intra-household constraints on women's ability to work outside the home or interact with non-household members and lack of bargaining power. The study involves a RCT in Punjab, Pakistan to test for these two explanations directly through two interventions, one targeting women's aspirations, and the other focusing on their ability to achieve objectives. This study will determine which of these psychological elements, combined with microcredit loans, can lead to enterprise creation and improve business outcomes.<br><br>
Status: Fieldwork ongoing, expected to finish by December 2018.<br>
Expected publication: June 2019.

		</p>
	
