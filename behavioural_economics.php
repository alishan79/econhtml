		<a name="Institutional"></a>
		<br>
		
		<h3>Institutional capacity as an organizational challenge: a field experiment in Pakistan</h3>
		<p><b>Researcher: </b>(Hamna Ahmed (Lahore School), Simon Quinn (University of Oxford), Kate Vyborny (Duke University) and Asha Gul (University of New South Wales)</p>
		
		<p>
		This project is a novel field experiment involving a large donor organization and over 800 recipient community organizations across Pakistan. The project involves two components: The first component aims to study whether community organizations can be incentivized to improve their performance through: (i) systematic self-assessment and reporting of defined Key Performance Indicators (KPIs), and (ii) A transparent non-financial rewards scheme based on these KPIs. Pakistan Poverty Alleviation Fund (PPAF) and International Growth Centre (IGC) have funded this component of the project. Initiated in 2014, four rounds of reporting and incentives for recipient organizations have been completed to date.<br><br>
The second component of the project will explore (i) how each part of a large, complex organization (the donor) responds to new information on performance (of recipient community organizations) on KPIs; (ii) How the responses of both donor and recipients to new information and incentives relate to organizational characteristics of theoretical importance, including divergence of preferences between members of the organization; communication costs between parts of the organization; and decentralization of decision-making authority. This part will build on an existing initial stage of the research carried out in component 1 of the project. National Science Foundation (NSF) is providing support that will allow the continuation and strengthening of Component 1 and expansion of the scope of the study to cover component 2. A first pilot of this component was carried out in February 2018. The full experiment will be carried out in summer while the endline survey for the study is scheduled for fall of this year.<br><br>
Hamna Ahmed and Kate Vyborny presented some initial findings from this project at PPAF's second international research conference to deliberate converting Knowledge to Action, held in Islamabad in April 2017.

		</p>
	
