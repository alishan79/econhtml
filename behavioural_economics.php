		<h3>Two Sides of the Same Rupee? Comparing Demand for Microcredit and Microsaving in a Framed Field Experiment in Rural Pakistan</h3>
		<p><b>Researcher: </b>Farah Said (Lahore School), Uzma Afzal (Lahore School), Naved Hamid (Lahore School), Marcel Faffchamps (Stanford University), Giovanna d'Adda (University of Milan) and Simon Quinn (Oxford University)</p>
		
		<p>
		The study is a Randomized Control Trial designed to compare the effectiveness of micro-savings and micro-loans as means for households to manage risk and liquidity.<br><br>
This research is funded by the IGC and is conducted in collaboration with the National Rural Support Programme (NRSP) funded by the Department for International Development (DfID) and Economic and Social Research Council (ESRC). The study explores the role of commitment and reminders in product takeup and repayment. This project builds on existing research and pilots funded by the International Growth Centre (IGC) with a sample of microfinance clients of NRSP. Results indicate that for microfinance clients, savings and credit products are often substitutes, satisfying the same underlying demand for a regular schedule of deposits and a lump-sum withdrawal <br><br>
Published Article: <a href="https://onlinelibrary.wiley.com/doi/epdf/10.1111/ecoj.12512" target="_blank">Afzal, Uzma and d'Adda, Giovanna and Fafchamps, Marcel and Quinn, Simon and Said, Farah, (2017). "Two Sides of the Same Rupee? Comparing Demand for Microcredit and Microsaving in a Framed Field Experiment in Rural Pakistan", The Economic Journal. </a>


		</p>
	
