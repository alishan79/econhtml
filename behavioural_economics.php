		<a name="Behavioural"></a>
		<br>
		
		<h3>Behavioural insights for evidence-based policy making - impact of depression intervention on individual preferences</h3>
		<p><b>Researcher: </b>Farah Said (Lahore School), Sonia Balhotra (University of Essex), Utteeyo Dasgupta (Wagner College & Fordham University) and Joseph Vecci (University of Gothenburg). </p>
		
		<p>
		This research aims to use lab-in-the-field experiments to identify behavioural effects of depression on women. Among preferences that we expect are modified by depression or violence exposure are the valuation of agency, present-biasedness, risk-aversion and confidence. <br><br>
This research is funded by the University of Essex .Depression is often associated with significant economic costs. For instance, depression is considered to reduce productivity by decreasing the motivation in performing everyday tasks or creating pessimistic views on returns on effort (de Quidt & Haushofer, 2016). However, economics literature does not provide us with any evidence quantify these effects. This research aims to use lab-in-the-field experiments to identify such behavioural effects of depression on economic decisions. Among preferences that we expect are modified by depression are risk-aversion, decisiveness, altruism, trust and effort.<br><br>
Status: Piloting expected in May 2018; fieldwork in Sept-Oct 2018 <br>
Expected publication: June 2019. 

		</p>
	
