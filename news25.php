			<h3>Corruption, Institutional Barriers and Innovation</h3><br>
			
			<p><b>Zainab Idrees</b></p>
			
			<h3>Abstract</h3>			
			<p>Innovation and technological progress are considered as crucial factors for economic growth in the long run (Grossman and Helpman, 1993; Nelson and Winter, 1982; Romer, 1990). Differences in innovation and growth performance are often explained by differences in institutions and quality of governance, in particular corruption (Mo, 2001).There are two views regarding role of corruption for innovation and growth; first, corruption causes an increase in transaction cost which may hinder investment in R&D and eventually innovation (Mauro, 1995; Mo, 2001; Guetat, 2006; Dridi, 2013) and second, corruption may accelerate the innovation by speeding up the bureaucratic procedures especially in a developing country where institutional quality is relatively poor (Leff, 1964; Huntington, 1968; Acemoglu and Verdier, 1998). The aim of our research is to identify whether corruption greases the innovation and growth of firms by accelerating bureaucratic procedures, or rather sands it by deviating investments away from productive activities for Pakistan, using World Bank Enterprise Survey by World Bank. </p><br>
			
			
