			<h3>The Impact of the Presence of an External Migrant and Remittance Inflows on Women Empowerment, Punjab</h3><br>
			
			<p><b>Mehreen Irshad</b></p>
			
			<h3>Abstract</h3>			
			<p>Pakistan is a developing country, and faces issues regarding gender inequality. Over the past 50 years a considerable number of people have migrated from Pakistan and inflow of remittances has increased over the past several decades which is marked as an important source of foreign exchange in Pakistan. This research aims to see the impact of external remittances and presence of an external migrant in a household on women empowerment. The data set being used is Multiple Indicator Cluster Survey 2014 (MICS) for Punjab. Women have been divided into 3 categories; All females, Married Females and Unmarried Females. These 3 categories have further been divided into age groups of 15-30 (younger women) and 31-49 (older women). Tobit model has been used for estimation. Furthermore, two separate instrument variables have been used; rainfall shocks for external remittances and historic migration for external migrant. The results show that households receiving external remittances have a higher women empowerment, while Households having an external migrant have a lower women empowerment, but households having an external migrant with a female household head have a higher women empowerment. Younger married women show a greater impact of empowerment on external remittances and external migrants as compared to other categories. </p><br>
			
