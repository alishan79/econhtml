		<h3>Political Connections Affect Development Spending in Punjab</h3><br>

		<p>Development related funding is critical for the long-term growth of any country, but the allocation of funds between politicians and different areas can vary due to various factors and one of the reasons can be strong connection of politicians with those who approve or allocate these funds. Networks and connections can be beneficial socially and economically especially in a country like Pakistan where you need strong contacts to get most work done. But even though having strong political connections and networks can be very beneficial for individuals, these connections can result in actions that are detrimental to society as a whole. <br><br>
	Researchers at the Lahore School of Economics conducted a research project to look at the impact of political connections on the allocation of development projects in Punjab, using Faisalabad district data from 2010-2016 as an example of development in a district of Punjab. The Faisalabad district has national assembly constituencies from NA75 - NA85 and provincial constituencies from PP51 - PP72, and MNA's and MPA's from these districts execute different schemes through different programs approved by local, provincial and central government depending on the cost of the project.  The development funds are allocated through different channels and sources like the provincial government, the central government and the local governments. In the development funding process, Members of the Provincial Assembly (MPAs) and Members of the National Assembly (MNAs) must propose their schemes and the government evaluates these schemes, so strong political connections play an important role in determining approval of projects.<br><br>
	When voters were surveyed, the data showed that the most popular projects were water supply projects followed by health facilities and water filtration projects. The voter survey also showed that the most commonly executed projects were sewerage projects and these types of projects were also fairly popular with voters. Other projects that were often executed in large numbers included bricked streets, roads and school infrastructure. But when we look at water supply projects, they were second in terms of voter popularity but very low in terms of overall percentage of project executed in Faisalabad. Similarly, water filtration plants and health facilities were very popular with voters, but were executed far less often. The percentage share of budget of each type from the total budget spent from 2010-2016, showed that the highest budget allocated was for roads and the lowest for electricity installation. The two highest ranked project types in terms of popularity, water supply and filtration plant have the 2nd and 3rd lowest budget allocations, while health facilities had also been allocated relatively less funds, although they are ranked extremely high by the constituents.<br><br>
	When looking at the politics, the researchers found that the political connections of politicians played a very important role in the allocation of the development projects. The overall political connections of the politicians increased the budget of projects that the politician had approved and executed while the probability of executing popular or important projects decreased if the political connections were stronger.<br><br>
	Researchers also found that the popularity of projects and the budget of projects were inversely related which meant that the lower budget projects were more popular. This means that well-connected politician spend money on projects that were generally less popular with their constituents and this may have promoted rent seeking behavior, as well as decreasing the efficiency of government investment. <br><br>
	It was found that TMA's approved lower budget projects (which is in accordance with P & D guidelines). But it is opposite for the popularity of the projects, since the most popular projects were approved by the TMA's and least popular by the provincial government. This shows that the TMA's have a better understanding of the needs of the constituents and smaller, more popular budgets projects are being allocated by TMAs especially after the 2015 local bodies elections. These two results imply that a more decentralized system may be better since they will understand the demands of general public.<br><br>
	Further when looking at the projects executed from 2012-13 i.e. the year before election, the researchers found interesting results: if politicians were well connected they executed higher budget project as well as more popular projects. This shows that politicians are aware of the voting behavior of the general public in that voters are myopic and vote according to what politicians achieve right before an election.<br><br>
	When looking at the impact of electoral strength on the budget of projects and the popularity of projects for 2010-2013, it was found that if the politician's margin of votes increased from the previous election, they executed higher budget projects and if a politician won for the second time he executed projects that were more popular but were of lower budgets (as compared to the politicians who won for the first time). Also, it is important to note that politicians who were closely tied to the federal government executed lower budget projects as compared to others and if a politician was affiliated with the provincial government, then the probability of executing less popular projects increased. <br><br>
	Further when looking at the same relationship for the years 2013-16 it was found that if the politician won for the third time he executed higher budget project than those who won for the second time and first time. Also, if a politician was affiliated with the ruling party (both Federal and Provincial) then he executed higher budget projects.<br><br>
	Finally the results showed that both the popularity of projects and the budget of projects increased the number of votes politicians received in 2013, but more popular projects lead to more votes than higher budget projects. Again this shows, the myopic nature of the voters i.e. if the politicians executed more popular projects in the year before the election then they received more votes i.e. people vote according to the recent performance of the government and if their recent performance was good they overlooked their past performance.  <br><br>
	This research has been conducted by Ms. Tahreem Zia, Lahore School of Economics and Dr. Azam Chaudhry, Professor & Dean, Faculty of Economics, Lahore School of Economics.


