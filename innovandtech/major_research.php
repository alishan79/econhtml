	  <h5>Major Research Projects:</h5>
	  
	  
	  <h5>i)	Analysis of Technology in the Sialkot Soccer Ball industry Dr. Azam Chaudhry and Prof. Shamyla Chaudry</h5>

	  <p>The research team invented a new cutting technology in 2012 that reduces waste of the primary raw material. This cost saving technology was given to a number of soccer ball manufacturers in Pakistan in order to determine the factors affecting technology adoption in firms and technology spillovers between firms.  Despite the apparent net benefits of the invented technology, adoption remained low even after 15 months. Thus the research team has also looked at how misaligned incentives between firms and employees can affect technology adoption. The incentive payment had a significant positive effect on technology adoption which shows that misalignment of incentives within firms is an important barrier to technology adoption.</p>
	  
	  <p>Presently the research team is looking at how subsidizing high quality inputs impact quality upgradation in the soccer ball sector. In order to do this, the research team is giving up to up to Rs 600,000 in rexine subsidizes to a randomly selected group of football manufacturers.  The project has been funded by IGC and PEDL and collaborators include Eric Verhoogen (Columbia University), Amit Khandelwal (Columbia University) and David Atkin (MIT).</p>
	  
	  	<div align="center"><img src="images/one.png" width="250px"/></div>
		
		<h5>ii)	Benchmarking of Productivity in Pakistan’s Readymade Garment Sector Dr. Azam Chaudhry, Dr. Theresa Chaudhry and Mahvish Faran</h5>
		
		<p>The research team is working to benchmark the productivity of garment factories in Pakistan. </p>
		<p>The pilot phase of the project will be used to understand bottlenecks to increasing productivity with the aim to develop productivity-enhancing interventions for the sector, to be evaluated through the method of a Randomized Controlled Trial (RCT). </p>
		<p>The project has been funded by the IGC and collaborators include with Christopher Woodruff (University of Warwick) and Rocco MacChiavello (University of Warwick).</p>
		
			  	<div align="center"><img src="images/two.png" width="250px"/></div>
				
		<h5>iii)	Technology and Innovation Mapping Project Dr. Azam Chaudhry, Dr. Theresa Chaudhry and Mahvish Faran</h5>
		<p>The aim of this initiative is to map the production process and the technologies used in the soccer ball and readymade garments sector.  Then using this map, compare the technologies in these sectors with the forefront of international technologies to see how far behind firms in Pakistan are.   Finally, the research team will see what incentives can be given to Pakistani firms to adopt new technologies (like financial incentives, supplying information about these technologies, providing assistance of adoption of these new technologies, etc.)</p>						
		
		<p>Collaborators include Rajah Rasiah (University of Malaya).</p>
	  
	  
	  
	  <div align="center"><img src="images/three.jpg" width="300px"/></div>
		
		<h5>iv)	Measuring Innovation in the Textiles Sector of Pakistan Dr. Waqar Wadho and Dr. Azam Chaudhry</h5>
		<p>Textiles is the major manufacturing sector of Pakistan contributing one-fourth of industrial value added, employing 40 percent of industrial labor force, and on average 60 percent share of national exports. </p>
		
		<p>In this project, they plan to study innovation activities of firms in the textiles sector of Pakistan during 2011- 2013. The survey will be completed by the end of 2015 across a sample of 500 textile firms in Punjab and Sindh and will measure both the technological (product and process) and the non-technological (organizational and marketing) aspects of innovation.</p>
		<p>The project has been developed under the firm capabilities program of the IGC.</p>
		
