	  <h5>Research Agenda</h5>
	  <p>The Centre for Technology Management & Innovation was established in April 2015 with an aim to promote innovation, a key to growth for Pakistan’s future. The Centre is headed by Dr. Azam Chaudhry, Dean and Professor, Faculty of Economics, Lahore School of Economics. <br><br>
	  
	  The objectives of the centre are:
	  </p>
	  
	  <ul>
	  	<li>Work on firm level research on innovation and productivity.</li>
	  	<li>Conduct surveys with international researchers on innovation and technology.</li>
	  	<li>Collaborate with the government on Policy based research focusing on technological upgradation in the manufacturing sector.</li>
	  	<li>Work with the Lahore Chamber of Commerce and other industrial organizations to promote technological innovation in Pakistan.</li>
	  </ul>
	  
	  <p>The centre is working on various exciting projects in the soccer balls industry, readymade garments sector and textiles. Currently, the research team is working on soccer ball project in which the team is looking at how subsidizing high quality inputs impact quality upgradation in the soccer ball sector, technology and innovation mapping project which aims to compare the technologies used in the soccer ball and readymade garments sector in Pakistan in comparison with the international market to see how far behind Pakistan is in adopting new technologies. Also, the research team is working to benchmark the productivity of garment factories in Pakistan.</p>
	  <p>Major contributions of the centre includes developing a cost saving technology for the soccer ball manufacturers in Pakistan resulting in 1% cost saving for the industry which operates with a very low profit margin of around 10%. This research has been jointly conducted by researchers at the Lahore School of Economics, Columbia University, University of Warwick and the Massachusetts Institute of Technology (MIT). Inspired by the spillover effect and benefits from this innovation, Government of Punjab has put in place an innovation fund to further promote innovation in other industries and sectors in Punjab.</p>	  
	  
	  
	  
	  
