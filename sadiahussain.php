		<h5>Research Projects</h5>
		<h5>Kashf Microinsurance Program  -  Ongoing</h5>
		<p>Working as a Project Investigator on Impact assessment of the Kashf microinsurance program on client retention. Design an effective intervention to overcome the constraints in the uptake of microinsurance amongst Kashf female clients. We plan to disseminate information about program use to improve program utilization. This project includes Dr. Hamna Ahmad in collaboration with the Kashf Foundation.</p>

		<h5>Impact of Charity Ratings on Giving  -  Ongoing</h5>
		<p>Working as Project Investigator on a designed intervention to understand the impact of charity ratings from a third party on donor's behavior. The experiment along with focus groups captures the behavioral constraints that donor's face in giving. We introduce elements of religious framing and institutional differences as additional treatments to the experiment. This project includes Dr. Kate Vybrony and Dr. Hamna Ahmad. </p>


		<h5>Impact Assessment of Bundling Health Insurance with Microcredit using PSM  -  Ongoing</h5>
		<p>We will incorporate the feedback given by Dr. Kate Vyborny and Dr. Naved Hamid to improve the richness of the results. We aim to incorporate previous trends in the Kashf clientele and branch level analysis in the study. This paper is expected to be sent out to the short-listed conferences and journals on completion such as Geneva Papers on Risk and Insurance-Issues and Practice.  The focus of the paper would be to fit the theme of the journal. The deadline for submission is 17th June, 2018. </p>
		
		<h5>RDF Proposal: Role of Information as a constraint in using Microinsurance (Phase I and II)  -  Ongoing</h5>
		<p>We designed an RDF proposal to understand the constraints faced by the Kashf clients in using the program. We split the study into two phases: Firstly, we identify whether information is a perceived constraints by the Kashf clients in using the health insurance program. Secondly, we address the information constraint through a designed intervention. The plan for this project is conditional upon RDF funding.</p>
		
		
		<h5>RDF Proposal: Impact of Charity Ratings on Donor's Behavior (Pilot)   -  Ongoing</h5>
		<p>At this stage, we will conduct a pilot session with Lahore School of Economics students through a lab experiment. The purpose of this study is to test the research question at a preliminary level. The findings will motivate the actual intervention.</p>
		
		
		<h5>RDF Proposal: Impact of Charity Ratings on Donor's Behavior  -  Ongoing</h5>
		<p>We propose an experiment to study the impact of information on donor behaviour. We will test (i) how potential donors respond to additional information; and (ii) whether their response to additional information varies if charitable giving is framed religiously or if giving decisions are made public. We will study the impact of information on three aspects of donor behaviour: their perceptions about quality of recipient organizations, choice of organizations to donate to, and amount of donations. The project is contingent upon acceptance of the proposal.</p>
		
		
		<h5>Textile and Innovation  -  Ongoing</h5>
		<p>Worked as Research Assistant on a project aimed to understand different types of innovation that can impact the declining competitiveness of the textile sector in Pakistan. Designated tasks with respect to this project were data-centric ranging from data cleaning to rigorous empirical strategies. This project includes Dr. Waqar Wadho and Dr. Azam Chaudhary in collaboration with the World Bank.</p>
		
		
