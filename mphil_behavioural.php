		<br>
		
		<h3>Cognitive Reflection and Decision Making</h3>
		<p><b>Researcher: </b>Taimoor Naseer Waraich (2016)</p>
		
		<h5>Abstract</h5>
		
		<p>
		We conduct a study which tests the impact of Cognitive Reflection on decision making theories of time and risk preferences. Decision theorists have suggest that people make lottery decisions according to the expected maximum value assigned to the lottery and they also claim that people generally discount the future more. We test these theories using the Cognitive Reflection Test (CRT). We use an extension of the original CRT which was a 3 item test based on the dual system theory and was used to divide people into different cognitive groups according to their scores. Originally the test was developed by Fredrick (2005) and then worked on by Primi etal (2013); they extended it to a 7 version item of the test which has greater predictive powers as compared to the original one. We also conduct an additional behavioral experiment which measures time and risk preferences but with real monetary rewards to see whether the decisions of the respondents change when presented with real life choices. The aim is to see the impact on both risk and time preferences using the instruments initially used by Fredrick (2005) and our additional experiment. We find a significant difference between decision making of respondents divided into two groups of low and high CRT scores. We also find that that new version of CRT has a stronger correlation with time and risk preferences. Our results show that the relation persists when we switch from hypothetical to real choices.<br><br>
