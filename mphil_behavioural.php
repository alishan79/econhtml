		<br>
		
		<h3>Homophily and Strategic Behavior in Social Interactions: Evidence from a Lab Experiment </h3>
		<p><b>Researcher: </b>Zeenat Ansari (2017)</p>
		
		<h5>Abstract</h5>
		
		<p>
		Social networks play an important role in human interaction. It is possible for social differentiation and segregation to discourage links that are desirable from an efficiency point of view, or for social matches to encourage them. This study looks at how individual behavior and the diffusion of demographic information affects social interactions. Based on a controlled lab experiment, we assess three key determinants of social interaction: (i) homophily, (ii) preference for fairness and (iii) past behavior. Of the experiment's three treatment groups - an out-group and two variations of in-group pairings - we find that participants show less homophily toward an in-group match when they know their partner cannot influence their outcome. However, if there is a chance of the behavior being reprimanded, the opposite is true. Finally, we present strong evidence in favor of reciprocity and coordination because participants are responsive to their partner's decision in prior interactions.<br><br>
Published Working Paper: <a href="http://www.creb.org.pk/uploads/WorkingPaperSeriesNo.02-17V1Complete.pdf" target="_blank">CREB Working Paper No. 02-17</a>


		</p>

