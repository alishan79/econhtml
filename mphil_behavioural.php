		<h3>Intergenerational Transfer of Aspirations: The Impact of Gender</h3>
		<p><b>Researcher: </b>Fatimah Shah (Ongoing)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This research seeks to identify the relationship between parental occupations and gender specific career aspirations of undergraduate students. To do this surveys would be conducted on fourth year undergraduate students in different universities of Lahore. These would be capturing data on demographic characteristics, parental occupations and career aspirations. As career aspirations are determinant of one's career and lifestyle choices, the results from this study may help in understanding prevailing gender gap in various labor market outcomes.
