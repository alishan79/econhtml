		<h3>Socioeconomic Determinants of Maternal Health Behavior in Pakistan: An Instrumental Variable Approach</h3>
		<p><b>Researcher: </b>Shandana Shahid Dar (2013)</p>
		
		<h5>Abstract</h5>
		
		<p>
		Utilization of maternal health services is a complex behavioral phenomenon. Empirical studies have established that the use of maternal health services is related to social and cultural structures, household factors and personal characteristics of women such as education and health knowledge. Yet the causal mechanism through which education of women in Pakistan affects their health seeking behavior is poorly understood. Using the 2006-07 Pakistan Demographic Health Survey (PDHS) information on women aged 15-49 who had given at least one birth in the three years prior to the survey, this study aims to; (i) determine which socioeconomic factors affect maternal health care utilization behavior of women, and (ii) identify the pathway through which effect of women's education is transmitted to their maternal health seeking behavior. Empirical analysis is conducted using instrumental variable technique and community fixed effects. The results of the analysis indicate that women's predisposing factors such as educational attainment, child's birth order, spouse's educational attainment and type of occupation, along with female empowerment are important determinants of maternal health seeking behavior of women in Pakistan. Results of this study also confirm an important role played by women's health knowledge, independent of educational attainment, on their maternal health care utilization.<br><br>
		
