		<h3>Fuzzy Judgment Aggregation: Distance Based Methods</h3>
		<p><b>Researcher: </b>Ayesha Syed (2016)</p>
		
		<h5>Abstract</h5>
		
		<p>
		Arrow (1963) established that a group cannot always reach logically consistent collective outcome. Subsequently many developments like premise based, conclusion based and distance based methods have emerged in literature to reach group consistency in crisp logic. This study is focused on the judgment aggregation in fuzzy logic based setting with novel involvement of family of t-norms. We compare four distance based methods due to Miller and Osherson (2009) using Lukasiewicz and min t-norm. These methods in fuzzy setting gives closer results to consistency of outcome. It also broaden the set of properties and authenticity of the methods. Distance method in our study satisfy Arrow's axioms in solution method that previously failed in crisp logic.<br><br>
