		<h3>Education, Employment, and Women's Say in Household Decision-Making in Pakistan</h3>
		<p><b>Researcher: </b>Duryab Fatima (2014)</p>
		
		<h5>Abstract</h5>
		
		<p>
		This study uses data from the Pakistan Social and Living Standards Measurement Survey for 2007/08 to conduct an empirical analysis of the relationship between education, employment, and women's say in household decision-making in Pakistan. Using a linear probability model with fixed effects, we analyze decisions pertaining to family planning and to expenditure on food, clothing, medical costs, and recreation. In order to address the reverse causality between employment and empowerment, the study uses district cotton production as an instrument for employment. We find that education and employment have a significant effect on women's say in certain- though not all-household decisions. In most cases, employment in nonagriculture increases women's say as opposed to employment in agriculture. <br><br>However, employment tends to empower women mainly in terms of expenditure-related decisions and not decisions pertaining to family planning.<br><br>
Published Working Paper: <a href="http://www.creb.org.pk/uploads/Working-Paper-Series-No.04-14-Complete.pdf" target="_blank">CREB Working Paper Series No.04-14.</a>


		</p>

