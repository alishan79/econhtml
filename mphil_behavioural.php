		<a name="Measuring"></a>
		<br>
		
		<h3>Measuring Mother's Empowerment through the Culture of Son Preference in Pakistan</h3>
		<p><b>Researcher: </b>Amal Tanvir (Ongoing) </p>
		
		<h5>Abstract</h5>
		
		<p>
		In a developing country, the changes in the economic conditions of a household can lead to parents treating their children differently based on their gender. According to Global Gender Gap Index (2016), out of 144 countries, Pakistan's global ranking in terms of gender gap in economic participation, educational attainment, health and survival along with political empowerment are 143, 135, 124 and 90 respectively. This gender discrimination has given rise to the concept of son preference where the social and economic utility from having a son outweighs that of a daughter.<br><br>
This study analyzed the factors having an impact on mother's empowerment in Pakistan and focuses on how the prevalent culture of son preference is translated in the gender along with birth-order of the child to play an important role in determining mother's status within the household. To explore this relationship Pakistan Demographic and Health Survey (PDHS) for the year 2012-13 has been used.<br><br>
The results suggest that having a son or a number of sons (regardless of the birth-order they are born in) is more likely to have a significant and a positive impact on mother's overall empowerment as compared to having sons belonging to different birth-orders. These results hold true even when mother's empowerment is segregated in terms of behavioral and attitudinal dimensions. Moreover, young mothers who belong to an age bracket of 16 to 30 years are more positively impacted by the birth-order of the son(s) as compared to mothers belonging to an age bracket of 30 to 49 years. Similarly, mothers who are old are more likely to see an increase in their empowerment by the mere presence of a son or a number of sons (regardless of the order they are born in) as compared to young mothers.


		</p>

