		<br>
		
		<h3>Impact of Parents' Subjective Aspirations on Schooling Investment in Rural Punjab</h3>
		<p><b>Researcher: </b>Anam Ashraf (2013)</p>
		
		<h5>Abstract</h5>
		
		<p>
		Human capital accumulation is deemed as a key indicator in the development process. It is closely related to other development indicators like socioeconomic status and occupational productivity. This study focuses on human capital investment decisions of parents for their child's schooling. By conducting a cross sectional empirical analysis, we attempt to identify parental subjective aspirations as a causal channel which impacts investment decisions regarding schooling of children. This study utilizes the 2010-11 Privatization in Education Research Initiative (PERI) dataset for children aged 5-14 who are currently enrolled in a school. The aim of this study is to; (i) assess the impact of differences in subjective aspirations between communities on schooling investment, (instrumented by the arrival of a factory) and, (ii) assess how different capabilities of siblings can influence investment decisions of parents. By exploiting exogenous variation in the arrival of new factories to a community and household fixed effects technique, parents' desire for the level of education they want for their child plays a major role in shaping up investment behaviour, but aspirations which are motivated by external factors such as arrival of new factories have a more pronounced impact on investment in schooling than differences in aspirations caused by differences between siblings. Moreover, from this study it can be deduced that this impact on investment goes into expenditure rather than towards private school enrolment.<br><br>
		
