		<h3>Lahore School of Economics and LCCI Survey Finds Guarded Optimism amongst Firms about Pakistan Economy</h3><br>
		
		<p>After facing some serious challenges over the past few years, the Pakistan economy has begun to stabilize and there is a strong need to develop policies that consider both long term economic needs of the country and short term needs of the business community. In contrary to general perception that the economic growth of the country has slowed down significantly, the recent survey conducted by the Lahore School of Economics and the Lahore Chamber of Commerce & Industry (LCCI), has found that the surveyed firms have expressed optimism, anticipated higher growth, and have done investments in innovation and technology which positively points towards renewed economic growth in sectors covered in the survey. </p>
		<p>The recent business confidence survey conducted by the Lahore School of Economics and the Lahore Chamber of Commerce & Industry on a variety of firms across three sectors (the manufacturing sector, the services sector and the retail sector) has found that the surveyed firms in all these sectors have grown over the last year in terms of sales, investment and size and were further planning to increase their investment levels especially in technology advancement and innovation. However, the results from the survey point out some serious issues that are being faced by the firms and some policy actions are needed to be taken in order to address these issues. </p>				
		
		<p>Firstly, the firms were found to be extremely concerned about sources of financing and were mostly using their own internal funding from sales as the financial sector was falling short in providing adequate capital which could possibly lead to a significant drag on future growth. Thus, there is strong need for the financial sector to play a more constructive role in economic growth by meeting the needs of the business community. </p>
		
		<p>Secondly, the researchers found that there was no incentive for firms to invest in indigenous technologies and very few firms were found to be using their own research & development and the rest were mostly relying on exhibitions, the internet and in some cases from suppliers of machinery and trade association for information on new technology. Thirdly, it was found that there was significant shortages in the pool of skilled labour which was a major constraint to growth. This issue could be tackled by governmental intervention through imparting vocational training to bridge this growing skill gap. The firms also pointed towards energy shortages, deterioration in law and order, security related issues, provincial and federal regulations and taxes, lack of transparency of policies, as major constraints to their overall growth. </p>
		
		<p>The researchers concluded that there is an urgent need for closer collaboration between the government and business community, and keeping in view all the issues raised by the business community, the government should come up with a comprehensive industrial strategy that promotes growth.
		
