Most economists are not much impressed by the inclusiveness of past growth cycles, and strongly suggest that the government put in place policies at the beginning of another growth crest to benefit a majority of the people, rather than only a few.<br><br>




According to a recent case study by Zunia Saif Tirmazee and Mariyiam Haroon of the Lahore School of Economics presented at a Pakistan Institute of Development Economics (PIDE) seminar earlier this month, income distribution has been inequitable.<br>

Pakistan has experienced tremendous economic growth over the last decade, but this did not reduce poverty and inequality. While discussing two indicators - efficiency and equity - the paper explained that efficiency required the overall improvement and equity required the improvement to be equitably distributed across various segments of the population.<br>

The paper revealed that there has been an overall improvement as indicated by an upward shift of the concentration curve, but concentration curves got steeper over time, indicating efficiency without equity.<br><br>

Between 2008-09 and 2010-11, the average per capita income was higher than the social mobility index, implying that income distribution was inequitable. And the income equity index is less than one across all regions for both years, which depicts a high level of inequality. However, the magnitude of inequality varied across regions.<br><br>

The paper also noted that growth in per capita income was achieved at the expense of equity. This meant that increasing incomes were being channeled towards higher expenditures and were not facilitating savings. The paper concluded that growth was not inclusive since it was achieved at the expense of equity - as the benefits of growth were unevenly distributed, with the poor benefiting less than the rich.<br><br>-->

Perhaps mainly because of this reason, the country is far from achieving 16 targets of the Millennium Development Goals set for 2015. These particularly relate to universal primary education, reduction in extreme poverty and hunger (notwithstanding major contribution by an effective BISP), gender equality and women empowerment and lowering of child mortality rates.<br><br>

Over 12m children are still out of school. Half of young children are under nourished and more than 39m people are still defined as poor. And the population growth rate of 2pc suggests that Pakistan would become the 5th most populous nation by 2050, further constraining limited resources. The youth bulge is already emerging more as a challenge than a dividend.<br><br>

