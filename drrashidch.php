			<P>Dr. Amjad is the Director of the Graduate Institute of Development Studies (GIDS). He was Vice-Chancellor of the Pakistan Institute of Development Economics (PIDE) for a term of five years 2007-2012. He served for two and a half years (2008-10) as Chief Economist at the Pakistan Planning Commission and as Member, Planning Commission. Dr. Amjad taught at the Punjab University, Lahore for seven years between 1969 and 1980, when he left to join the ILO. In the ILO, he held a number of positions including Director, South East Asia and the Pacific and Director, Employment Strategy Department. He was Chief of the ILO's World Employment Report Team and principal author of two of ILOs global reports on "Employability and Training" and "Life at Work in the Information Economy", published in 1998 and 2001 respectively. Dr. Rashid has published a number of books and a large number of articles in internationally recognized journals.</P>
			
