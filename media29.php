		<h3>CPEC and industrial policy</h3><br>

		<table border="0">
		<tr><td valign="top"><p><img src="images/drazam_cpec.png"></p></td>
		<td valign="top"><p>As Pakistan enters the CPEC era, it is important we take a moment to reflect. Most people believe the initiative offers both Pakistan and China a unique opportunity to reshape the region's economic dynamics. For this reason, there is, rightly, great excitement among academics, policymakers and the public. As Pakistan embarks on this journey, it is worth examining the direction its industrial policy should take.</p>
		<p>The most obvious question is this: why worry about industrial policy now? To begin with, the sheer scope of CPEC means that decisions made now will have consequences in the long term. Also, the experience of most industrially developed countries shows that a coherent, well-functioning industrial policy can accelerate industrial growth, lead</p>
		</td>
		</tr></table>
		
		<p>to higher long-term growth and improve living standards. This is important because the lack of an industrial policy, compounded by free-trade agreements hastily cobbled together, has led to low industrial growth in Pakistan. Unsurprisingly, this means that the business community still has several concerns about the impact of CPEC on their own ventures.</p>
		
		
		<p>CPEC gives Pakistan & China a chance to be at the forefront of a sustainable industrial policy. </p>
		
		
		<p>Having accepted the need for a coherent industrial policy, how should it be structured? The first step is getting the fundamentals right: ensuring an uninterrupted supply of electricity and gas, rationalising taxes and easing regulations. So far, the government's focus on these areas is well placed and a good starting point for industrial policy.<br>	
		
		<p>At the same time, CPEC-related industrial activities must have well-defined local stakeholders. This could be done easily by instituting a minimum requirement for local partner involvement and ensuring that each is allocated a minimum financial share of that project. Another concern is Chinese versus local ownership of resources. One way to allay this fear would be to guarantee that all CPEC industrial projects involve long-term leases for land as opposed to sale.</p>
		
		<p>Having come this far in theory, policymakers will need to make pragmatic decisions about which sectors to focus on. The idea is to not choose 'winners', but rather 'winning sectors'. Most sensible people understand that, in Pakistan's case, the domestic production of textiles is more important than that of supercomputers. Policymakers need to identify which industries make sense in the context of CPEC and target them accordingly.</p>
		
		<p>Continuing this thought experiment, let's say that policymakers manage to pinpoint which sectors to focus on. The next step would be to decide what level of industrial technology and sophistication of goods is most appropriate. If policymakers choose to focus on textiles, does this mean high-value-added textiles? Should Pakistan produce low-end or high-end sports goods? Does focusing on surgical goods mean continuing to produce low-value-added items that fetch low prices and, in many cases, carry foreign brand names?
</p>
		
		<p>The bottom line is that policymakers must make a conscious decision to move up the technology ladder. One way of doing this is for the government to create firm level incentives for investment in advanced machinery based on the technological sophistication of output.</p>
		
		<p>Another way to ensure that Pakistan's technology is upgraded is to focus on technology transfers. It doesn't make sense for Chinese companies producing the lowest-value-added textiles to shift to Pakistan and continue producing at this level while taking advantage of Pakistan's cheap labour and raw materials. Pakistan will merely end up producing the same goods as now, but on a higher scale. Consequently, all CPEC project agreements - including those on establishing industrial zones - should make it mandatory for a minimum level of technology transfer to take place over the life of the initiative.</p>	
		
<p>One perception is that industrial policy (and industry itself) is concerned solely with the welfare of the elite, while any benefits to ordinary citizens are simply a by-product of industrial success (implying, for instance, that more factories mean more jobs). To many so-called proponents of growth, this makes sense: lower labour costs lead to lower production costs, enabling firms to compete internationally. But this loses sight of the fact that a weakened, low-skilled, low-wage labour force will prevent firms from moving beyond the production of low-value-added goods. Both policymakers and industrialists must realise that immediately investing in labour is the only way to adopt new technologies successfully and drive higher growth.</p>
		
		<p>This means that Pakistan's industrial policy model must enable industry to switch from low-skilled to high-skilled labour. In the context of CPEC, policymakers should consider the following. First, stipulate a minimum level of domestic labour for all joint industrial initiatives. Second, create a model that improves working conditions and thereby workers' productivity. Third, ensure that all industrial zones and joint projects automatically include training facilities, with a minimum proportion of these devoted to training women. Fourth, make it mandatory for all industrial zones and joint projects to provide their workers with insurance benefits. In doing so, CPEC could revolutionise a new model of labour in Pakistan.</p>
		
		<p>Finally, CPEC gives both Pakistan and China an opportunity to be at the forefront of a sustainable industrial policy. Despite widespread misconceptions, the fact is that protecting the environment is not only an overwhelming social concern, but also affects bottom-line businesses. This makes it critical to institute a well-thought-out environmental protection strategy for all CPEC-related industrial projects and activities at the outset. Such initiatives could include imposing a minimum renewable energy requirement as well as very clear environmental requirements. Equally important would be to develop a separate EPA evaluation process - one that would ensure that all such environmental safeguards remain in place while also fast-tracking CPEC-related projects and activities.</p>
		
		<p>The CPEC initiative could, potentially, transform Pakistan - and its industry in particular. That said, now is the time to sit down and formulate a coherent industrial policy or we may find ourselves trapped in producing the same, low-value-added goods in the future.</p>		
		
