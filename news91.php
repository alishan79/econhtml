		<h3>IMF Chief Visits Lahore School to Meet Students</h3><br>
		
		<p>On March 6th, 2015, the Lahore School of Economics hosted the IMF Director for Middle East and Central Asia, Mr. Masood Ahmed, who held a roundtable discussion with a group of students from local universities to discuss the IMF and its program in Pakistan. Students from the Lahore School of Economics, the Lahore College for Women University, the Lahore University of Management Sciences, Beaconhouse National University and Forman Christian College University attended the roundtable discussion to share their views with the visiting IMF team. The roundtable was chaired by Mr. Ahmed, Dr. Shahid Amjad Chaudhry, Rector of the Lahore School of Economics and Dr. Azam Chaudhry, Dean Faculty of Economics, Lahore School of Economics.</p>

		<div align="center"><img src="images/n5.jpg" width="450px"></div>
		
		
		<p>Dr. Shahid Amjad Chaudhry, Rector of the Lahore School of Economics welcomed Mr. Masood Ahmed, the IMF Director of the Middle East and Central Asia region, Mr. Herald Finger, the IMF Pakistan Mission Chief and Mr. Tokhir Mirzoev, the IMF Resident Representative for Pakistan. Dr. Chaudhry discussed the macroeconomic situation in Pakistan at the time when Pakistan entered the IMF program as well as the prevailing macroeconomic challenges faced by the country.</p>
		
		<div align="center"><img src="images/n6.jpg" width="450px"></div>
		
<p>Mr. Masood Ahmed began by discussing the history and role of the IMF in the global economy and explained how the IMF assists countries in terms of technical assistance, macroeconomic advice and lending in case of macroeconomic crises. Mr. Ahmed then explained how the balance of payments problem that Pakistan faced in 2013 led to a significant reduction in its foreign exchange reserves which in turn led the Pakistani government to ask the IMF for assistance. Finally, Mr. Ahmed explained how the IMF was different from other international agencies like the World Bank and the Asian Development Bank because these other institutions focus on development assistance with lending for specific projects and initiatives while the IMF focuses on macroeconomic stabilization with a specific emphasis on fiscal deficits and exchange rate adjustments.</p>
		<div align="center"><img src="images/n7.jpg" width="450px"></div>
		<p>This introduction was followed by a series of questions regarding the role of the IMF in Pakistan. Many students asked Mr. Ahmed why the IMF insisted on significant reductions in expenditures which mostly led to significant reductions in development expenditures that could impact long term growth. At the same time other students asked why the IMF insisted on increasing tax revenues which in many cases led the government to adopt taxes that many considered regressive. Mr. Ahmed explained that one of the major issues facing developing countries like Pakistan is that countries spend far more money than they collect and this leads to macroeconomic problems. He then went on to explain that the IMF has recognized that governments tend to slash development expenditures immediately which means that there are significant reductions in spending for critical sectors like health and education. For this reason, the IMF now includes in its program a requirement that governments maintain a certain level of spending in critical social sectors. Mr. Ahmed also explained that the IMF does not get involved in determining specific taxes but is more concerned with the overall objective of macroeconomic stabilization. </p>
		<div align="center"><img src="images/n8.jpg" width="450px"></div>
		
		<p>Another set of questions by the students focused on why Pakistan constantly kept reentering IMF programs but failed to complete any programs. Mr. Ahmed explained that these two issues were closely interlinked: Pakistan has some basic structural problems which become severe every few years and requires IMF assistance, but after a year or two of the IMF program the Pakistan government feels that the crisis has passed so they do not continue to take measures to address these issues. This has led to a cycle in which the Pakistan government asks for IMF assistance, stabilizes the economy, and then stops following IMF advice for long term reform which then leads to the same crisis reoccurring after a few years.</p>		
		
		<p>Finally, the students asked Mr. Ahmed's views on the energy crisis facing Pakistan as well as the impact of falling oil prices on oil exporting countries (like those in the Gulf, Nigeria and Venezuela) and oil importing countries like Pakistan. Mr. Ahmed says that the energy crisis in Pakistan is a significant issue facing policymakers as well those interested in macroeconomic stabilization and long-term growth and that the significant fall in oil prices has been handled differently by developing countries. Some countries like Pakistan have decided to pass on a portion of the fall in oil prices to the final purchasers, while others like India have kept the final prices of fuel the same by increasing the taxes on these products in order to shore up their domestic revenues. Mr. Ahmed also discussed how the significant decline in oil prices has had a major impact on the economies that are heavily reliant on oil and he also discussed the balance of payments impact of falling oil prices on countries like Pakistan.</p>
		
		<div align="center"><img src="images/n9.jpg" width="450px"></div>
		
		<p>Mr. Ahmed then ended the roundtable discussion by thanking Dr. Shahid Amjad Chaudhry, Dr. Azam Chaudhry, the student participants from various universities and the Lahore School of Economics for hosting this event. He told the audience that it was important for economists and macroeconomic policy makers to interact with students to understand the issues that were important to them and to society and to discuss long term economic strategies in order to develop inclusive policies that are understood and lead to long term growth.</p>
