		<h3>Mass transit projects increasing air pollution in Lahore</h3><br>

		<table border="0">
		<tr><td valign="top"><p><img src="images/mas.png"></p></td>
		<td valign="top"><p>Air pollution has enmeshed the city of Lahore in its vicious claws primarily due to the ill-planned construction of mass transit projects like Lahore Orange Line Metro Train. Prior to this, the Lahore Metro Bus, which gobbled up Rs30 billion, was considered a bane because it was supposedly executed for the sake of gaining a bigger vote bank for the May 2013 elections. Even after its implementation, one can see no amelioration in terms of incessant traffic congestion in Lahore or air pollution - hence it can be equated to a white elephant! Basically undertaking massive-scale projects such as the construction of rapid transit systems, if marred by loopholes, result in heavy socio-ecological costs that are borne by marginalised groups and communities. This can be clearly observed with the ongoing construction of the Lahore Orange Line Metro Train Project.
</p>
		</td>
		</tr></table>
		<h3>Rs112m tax exemption for OLMT affected people</h3><br>
		
		
		<p>The Orange Line Train (OLT) extends over a route of 27 km covering a total of 26 stations ranging from Dera Gujjran (Raiwind Road) to Ali Town (GT Road). Out of these, 24 stations are elevated while two are underground. Bascially, this project was initiated with a signed memorandum of understanding (MoU) between Pakistan and China in May 2014. Financing was secured in December 2015 when China's Exim Bank agreed to provide a soft loan of Rs165 billion for the project. Originally the completion of this project required 27 months but now it is expected to be completed by October 2019.
		</p>
		
		<p>As you enter Lahore, you are flabbergasted to see the city buzzing with streams of heavy traffic.</p>
		
		<p>This is the main reason which is leading towards increasing air pollution hence posing a severe threat to the health of citizens. There is no doubt that Lahore is one of the largest metropolitan cities of Pakistan with a population of 18.5 million and escalating migration rates from rural and peri-urban areas. According to the 2015 figures of the Bureau of Statistics Punjab, there has been a considerable increase in motor vehicles in Lahore over the past decade resulting in grave traffic congestion which is a major contributor to air pollution. This triggered the Punjab government to undertake another mass transit project, Orange Line Train, to curb traffic congestion in the city, but you can clearly see that the situation has aggravated in terms of increasing pollution.</p>		
		
		<p>According to Section 6 of Anticipated Environmental Impacts and Mitigation Measures of Lahore Orange Line Metro Train, it is explicitly stated that the proposed project will lead to air pollution and an increase in health risks associated with it. If you happen to visit the construction site, you can feel a high magnitude of particulate matter in air as well as proliferating dust pollution - heaps of mud can be seen in old Anarkali. This scenario is further exacerbated by increasing population of motor vehicles. Densely populated areas like Lakshmi Chowk and the surroundings of the Shalimar Gardens are acutely hit by deteriorating air quality. Vehicular emissions (carbon monoxide, nitrogen oxide and sulfur oxide) are adversely affecting the environment and human health therefore jeopardising air quality, damaging materials and property via acid rain and dust pollution, retarding growth of trees and plants and leading to impairment of atmospheric visibility affecting transportation safety.</p>
		
		<h3>If cleared, Orange Line may be delayed by a year</h3>
		
		<p>Similarly these emissions are negatively influencing human health resulting in serious lung damage, respiratory diseases, heart attacks due to lack of oxygen in blood, dizziness, headaches and nausea. Basically large-scale developmental projects are executed at the expense of disrupting the environment: industries are set up on deforested lands, infrastructure is built up by excavating earth, clearing vegetation and leveling off land. In a similar way, for the construction of Orange Line train 620 trees were uprooted and massive vegetation cleared. This has also resulted in the havoc episode of smog, that we all witnessed, which had predominantly engulfed Lahore in the prior year, making thousands of people victims to severe respiratory ailments such as bronchitis and incessant asthma attacks. Other aspects of environmental degradation that are associated with this project include contamination of groundwater, soil erosion, water pollution and noise pollution which have created a synergising effect on environmental degradation and health of citizens.</p>
		
		<p>Evidently, another downside of this project is that cut and cover technology is being used instead of tunnel boring. The latter one is believed to be less disruptive to the socio-ecological fabric as it accounts for meagre destruction of private property and environment. Tunnel boring technology is used across the globe - London, Paris, Delhi are a few examples - because cut and cover technology demolishes everything on the surface which is very risky and dangerous to environment as well as to the people.</p>
		
		<p>Secondly, the life of a surface train is not more than 15-20 years, hence spending billions on a surface train that covers a distance of 27 kilometres and accommodates only an insignificant number of passengers is not worth pursuing.</p>
		
<p>Furthermore, Pakistan is already grappling with an energy crisis and cannot afford a train that runs on electricity. Apparently the government wants to complete the project before the next general election to garner more votes. That is probably why it is applying cut and cover technology which is less time consuming. This manifests the inefficiency of institutions and their extractive nature.</p>						
		
		<p>Project detractors are not against public transport, they just hate the way the project is being implemented. Marginalised communities residing in the vicinity of the construction site are being affected the most while the elite is oblivious to the crisis the construction is causing in the form of environmental degradation and displacement of communities.</p>
		
		<p>Before initiating of any massive "development" project the government should conduct a cost benefit analysis that includes not only financial and economic costs but also social and ecological costs. If the benefits outweigh costs only then a certain project should be undertaken otherwise it should be discarded. However, since the project is under construction, it can't be halted absolutely. Moreover, workers who have lost their lives during construction, people who have been displaced, heritage sites that have been destroyed, and obviously, the environment that has been ripped apart, cannot be recompensed by the local government because of its vested interests associated with the project. So what can be done under such circumstances?</p>
		
		<p>Well there is a dire need to create awareness among the masses with respect to the vested interests of state, which undertakes such projects jeopardising the socio-ecological life. In Pakistan, environment is the most neglected area which is why the state has paid no heed to short- and long-term adverse effects this project will have on people in terms of contributing to the proliferation of pollution.</p>
		
		<p>Is it wise to spend billions on building mass transit projects and polluting the environment when the common people are deprived of basic health facilities, education, water and sanitation system, and electricity?</p>
		
