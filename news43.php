			<h3>Impact of Temporary Migration & Remittances on Child Health Outcomes in Punjab, Pakistan</h3><br>
			
			<p><b>Myda Aslam</b></p>
			
			<h3>Abstract</h3>			
			
			<p>Temporary migration in developing countries like Pakistan is generally in response to the income constraint faced by the households. In an attempt to relax the resource constraint, migrants tend to remit back to their families of origin. This study attempts to look at the impact of temporary migration and remittances on the health outcomes of children as measured by height-for-age z scores (HAZ) and weight-for-age z scores (WAZ) in Punjab. The data has been taken from Multiple Indicator Cluster Survey (MICS) 2011, Bureau of Emigration and Overseas Employment and State Bank of Pakistan. The study uses an Instrumental Variable Approach with Two Stage Least Square and Instrumental Variable Approach estimated through Treatment Effect Model. Historic migration rates and number of banks in each district is used as an IV for migration and remittances. Our results suggest significant and positive impact of migration, external migration and remittances on both the indicators of child health outcomes (Height for Age z-scores and Weight for Age z-scores).</p>
			

<br>
			<h3>Corruption: The Role of Taxes and Wage Incentives</h3><br>
			
			<p><b>Saadia Aizaz Toor</b></p>
			
			<h3>Abstract</h3>			
			
			<p>This paper builds a theoretical model to study the joint effect of an optimal wage scheme and taxes which is decided by a budget balancing government that further determine the level of corruption. Tax revenues are a major source of income for the government used to stabilize the economy, however with the presence of bureaucratic corruption, the tax collection process is prone to several forms of corruption resulting in tax evasions. Deviating from existing literature, we integrate all major factors determining the incentives to be corrupt for both the corrupt tax payers and tax inspectors to capture the joint role of taxes and wages which generate highest net revenues for the government. We study cases for both developing and developed economies. We find that for both types of economies, the government is better off eliminating corruption by giving wage incentives at low levels of cost of compliance and accepting corruption by paying wages below the market level at high levels of cost of compliance. However, for developed economies there exists an intermediate range of cost of compliance where the government where the government is better off paying wages equal to the outside option. The significance of efficiency wages falls as the portion of dishonest agents rises in a society.</p>





