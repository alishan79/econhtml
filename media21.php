		<p><b>Facing an election year, will this government behave any differently from its predecessors?</b><br>
		The last year under the previous civilian government (2012/13) was no different from the period under the earlier military regime. Pakistan may have initially regained some macroeconomic stability under the IMF programme, but there was no serious attempt to carry out the economic reforms promised or to resolve the energy crisis (which cannot be overcome by lining one's pockets or advertisements in newspapers). It left behind an unsustainable fiscal and current account deficit and a looming balance-of-payments crisis. In 2013, the new elected government returned to the IMF for a bailout package. This time, there was no steep decline in growth, but it remained anaemic, hovering around 4.5pc. This was hardly sufficient to create enough jobs for the country's youth, reduce poverty and remain competitive when our neighbours are growing at over 7pc.
		</p>
		
		<p>Having completed the IMF programme at the end of 2016 and restored a modicum of macroeconomic stability, the current government has announced that it now plans to shift gear to a higher growth rate of around 6pc next year - and even higher the year after, if re-elected. But will they be able to post this ambitious target?</p>
		
		<p>The quest emerges just when the macroeconomic situation is under pressure once again. Just as in 2008/09, the church bells have started ringing. Over the last three months, the State Bank has periodically announced a fall in foreign exchange reserves to the tune of half a billion dollars. Despite the brave faces being donned by the mandarins at the finance ministry and the gnomes at the State Bank, both know they have a difficult decision to make: should they start putting on the brakes and err on the side of caution or end up with another foreign exchange crisis?</p>		
		
		<p>The real question is more difficult and, sadly, the decision not theirs to make. Facing an election year, will this government behave any differently from its predecessors?</p>
		
		<p>Before answering this, we must recognise that the Achilles' heel of the economy, historically and today, is our foreign exchange reserves position, which is a binding constraint to our capacity to grow. Once this comes under pressure and falls below a minimum level, there is no option but to seek foreign assistance or then face the prospect of defaulting.</p>
		
		<p>Keep in mind that the current macroeconomic situation will be judged not on the bonanza that CPEC is expected to bring over the next few years or that overpasses and underpasses emerge at every corner or even that the electricity crisis may be over by the end of next year. As important as these achievements may turn out to be, the real concern for the global financial markets is how long Pakistan's falling foreign reserves last and whether the rupee can hold its current rate against the dollar.</p>
		
		<p>The signs are not promising: stagnant exports, declining remittances (made worse by the recent knee-jerk policies and controls imposed by the State Bank) and oil prices that show signs of rising from their slumber. All this as our import bill rises by leaps and bounds and is now well over double our exports of goods and services.</p>
		
<p>As always, the government has an answer. Exports, we are told, were simply waiting for the recently announced export package to take off. Devaluation will serve no purpose in the current global milieu and make debt repayments higher. CPEC will bring in the foreign exchange needed to bolster reserves. The smiling faces around the table when we launched foreign-dominated bonds to raise reserves through foreign debt at favourable terms reflect our investors' faith in us and in the economy.</p>						
		
		<p>As many governments have learnt the hard way, it takes very little to wipe off these smiles. The same financiers will not deign to answer one's calls, come the financial crunch.</p>
		
		<p>The fact is that one can grow only as fast as the true strength of the economy allows - that too, only when macroeconomic stability is assured. Pakistan's stagnant exports reflect the long list of reforms it never implemented and the waivers or exemptions for which it pleaded and finally got from the IMF in Dubai. The reforms agenda was never implemented seriously, most of it representing one step forward and two steps back. Pakistan's Achilles' heel - reflecting its lack of global competitiveness and domestic inefficiencies - remains exposed.</p>
		
		<p>Will we finally take the more prudent economic path and make some hard decisions before deciding to accelerate prematurely and running into a serious macroeconomic imbalance and a teetering economy? We have several shrewd economic policy managers in place, but will they be able to steer the economy well and stagger development expenditures (including CPEC) at a pace that ensures macroeconomic stability?</p>
		
		<p>The answer - and one hopes one is proven wrong - is that, as in the past, we will fill up the tank with borrowed money for fuel, press down hard on the accelerator and enjoy the ride. After all, the IMF is always ready to tow us back to town when the fuel runs out and make us pay through our nose to carry out the repairs. And remember, it is an election year..</p>
		
