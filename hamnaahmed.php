			<H5>Ongoing Research</H5>
			<p>

1. Institutional capacity as an organizational challenge: a field experiment in Pakistan: (with Simon Quinn, Kate Vyborny and Asha Gul). This project involves a Randomized Control Trial (RCT) that aims to test whether community organizations can be incentivized to improve their performance through (i) Systematic self-assessment and reporting of defined key Performance Indicators (KPIs), and (ii) a transparent non-financial rewards scheme based on these KPIs. In addition, we study how each part of a large, complex organization (the donor) responds to new information on performance (of recipient organizations) on KPIs. Expected publications from this project: 
</p><br>
	<ul>
		<li>Study on RCT results (expected in 2020) </li>
		<li>Study based on a Regression Discontinuity Design: The aim of this study will be to test 	whether community organizations that support the winning politicians able to mobilize 	more resources and funding for their communities relative to organizations that were 	supporting the runner up politicians. (expected in 2020) </li>
		<li>Group decision making: evidence from a framed field experiment in rural Pakistan. The 	objective of this paper is to understand the process of collective decision-making within 	community development organizations in rural Pakistan. We investigate how individual 	preferences of members running community organizations aggregate into group 	preferences and we study the role of intrinsic characteristics in determining whether 	member's individual preferences are represented or ignored in-group preferences. 	(expected in 2019). </li>
		
	</ul>
	<br>
	
	
	
	<p>2. The impact of bundling health microinsurance with microcredit: Evidence from Punjab, Pakistan  </p><br>
	<ul>
