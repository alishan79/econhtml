	<p>4. Overcoming constraints to giving (with Kate Vyborny and Sadia Hussain). In this study we propose a lab experiment to test  </p><br>
	<ul>
		<li>how potential donors respond to additional information; and </li>
		<li>whether their response to additional information varies if charitable giving is framed religiously or if giving decisions are made public. We study the impact of information on three aspects of donor behaviour: their perceptions about quality of recipient organizations, choice of organizations to donate to, and amount of donations. (expected in 2019) </li>
		
	</ul>

<br>

	<p>5. Overcoming constraints to female labor force entry (with Mahreen Mahmud, Farah Said and Zunia Tirmazee). We propose a randomised controlled trial to test the impact of two low-cost interventions to overcome psychological and information constraints to female labor force participation (FLFP). In particular, our research questions are, does (i) motivating female students and (ii) providing information about searching and applying for jobs, promote female labor force entry. We have recently received funding from PEDL for this study. (expected 2019) </p>
	
	
	
