		<li>(with Sadia Hussain) An academic paper that employs a Diff-in-Diff design to study the causal impact of bundling health microinsurance with microcredit on utilization of health facilities and client retention. (to be sent out to Geneva Papers on risk and insurance by June 2018). </li>
		
	</ul>

