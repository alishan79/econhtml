	<h3>The Minister of Finance (Government of Pakistan) visits the Lahore School of Economics</h3><br>
	
	<p>The Minister of Finance (Government of Pakistan), Dr. Shamshad Akhter, visited the Lahore School of Economics on July 21st, 2017 to discuss the current economic situation in Pakistan as well as hear policy recommendations from the Lahore School research team.</p>
	
	
	
	<div align="center"><img src="images/minfin1.png"></div><br>
	
	
		<p>The Minister was hosted by the Rector of the Lahore School of Economics who gave his views of potential economic stabilization policies. The Rector's recommendations were followed by presentations on growth and stabilization policies by Dr. Moazzam Mahmood, Dr. Azam Chaudhry, Dr. Ayesha Afzal. Dr. Kumail Rizvi and Dr. Zahid Riaz.</p><br>
			
		<div align="center"><img src="images/minfin2.png"></div><br>			
		
		
			
		<p>	Dr. Moazzam Mahmood discussed the balance of payments situation as well as the impact of the recent devaluation. Dr. Azam Chaudhry discussed the long-term constraints on economic growth and the need for a structural change in the nature of Pakistani exports. Dr. Ayesha Afzal discussed the impact of social programs on poverty in Pakistan and the need to maintain (and potentially increase) the budget on these programs during times of fiscal contraction.  Dr. Kumail Rizvi discussed the need for reforms in the government's policies in the real estate sector and in the stock market in order to revive domestic and foreign investment. Finally, Dr. Zahid Riaz discussed the sectors that the government must focus on to promote higher value added exports.
 </p>
		
		<div align="center"><img src="images/minfin3.png"></div><br>		
		
		
			
		<p>The Minister then briefed the participants on the government's recent economic policies and discussed potential avenues of collaboration between the Lahore School and the Ministry of Finance. </p>
		 
		 
