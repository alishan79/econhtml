		<h3>What steps are needed to tackle the deterioration in Pakistan's trade balance and what are the expectations of the exporting sector firms about the impact of CPEC? : A Survey based of the Business Community of Lahore
</h3><br>

		<p>Over the course of this year, Pakistan's trade deficit has significantly increased compared to the previous fiscal year. The main reason behind this is that exports have declined at a steady pace, while imports have gone up alarmingly. In order to explain this unprecedented trade deficit, policy makers need to urgently determine what factors have led to the fall in exports. Also, what measures do we need to tackle the deterioration in Pakistan's trade balance? Currently, the economy is in the process of stabilizing and it is critical to determine policies that lead to sustainable economic growth.</p>
		
		<p>Recently the Technology Management & Innovation Centre of the Lahore School of Economics conducted a business confidence survey of firms with the Lahore Chamber of Commerce & Industry (LCCI) and the emphasis of the survey was exports. Out of the 445 firms who completed this year's survey, there were 252 exporting firms including 167 from the manufacturing sector, 30 from the services sector and 53 from the retail sector. The results from this survey shed light on the factors that have led to the decline in exports and what steps could be taken to boost exports in the coming years.<br><br>
The first part of the survey looked at the state of the Pakistani exports from the viewpoint of firms. When the firms were asked about revenues from export sales in 2015 and 2016, 67% of firms reported a decrease in their revenues from exports.  The major sector affected by this decline in exports was the retail sector where 87% of firms said their revenues from exports had declined, followed by the manufacturing sector in which 66% of firms reported a decline. In the service sector on the other hand, 53% of firms reported an increase in their revenues from exports sales. This seems to imply that the government should urgently focus on promoting service sector exports.
</p>
			
		<div align="center"><img src="images/pie1.png" width="450px"></div>


		<p>When asked about their expectations of exports in 2017, most firms said that they expect their revenues from export sales to decrease over the current year. Looking across sectors, only 8% of the retail sector firms expect their revenues from exports to increase in 2017. At the same time, only 31% of manufacturing sector firms expected exports to increase.  Finally 43% firms from the services sectors said that they are expecting export sales to increase in 2017 again pointing to the fact that service sector exports may drive future export growth.</p>


		<div align="center"><img src="images/pie2.png" width="450px"></div>
		
		<p>When asked about the reasons for lower export sales, firms pointed to unstable exchange rates, the domestic energy crises, changes in global market preferences and inappropriate government policies. In terms of policies to boost flagging exports, the firms pointed to un-interrupted electricity and gas supply, lower cost of raw materials & labour and increased access to foreign buyers. 
<br><br>
The survey also found that the firms have limited export destinations, with most exports going to Asia and Europe, and at the same time most firms only export one major product to their export market. This strongly implies that firms in collaboration with the government need to expand their product base and explore newer markets for their products in order to optimize their role and share in the international markets.
</p>
		
		
		<div align="center"><img src="images/pie3.png" width="450px"></div>

		<p>When asked about their levels of investment, exporting firms mostly reported that their level of investment decreased, though when asked about firm size, most firms reported an increase in the number of employees in 2016 as compared to 2015. However, most exporting firms are expecting both their bank borrowing and employment levels to remain the same in 2017. Overall, the export firms are split on their expectations about the performance of Pakistan's Economy in the current year, with 44% of firms expecting it to perform worse and 42% firms expecting it perform better in 2017.
<br><br>
Finally, the survey asked firms about their expectations of the possible impact of the China- Pak Corridor (CPEC). Interestingly, a large proportion of firms (62%) had positive expectations about the impact of CPEC on their business, while 21% of firms said it would have no impact on their business and only 17% firms were of the view that it might have a negative impact on their business. A comparison of expectations across sectors revealed that the services sector was the most positive about CPEC where 73% firms were positive about the impact of CPEC, followed by the manufacturing sector in which 63% of the firms expected a positive impact of CPEC. However, the retail sector was the least positive about the impact of CPEC, with 49% of retail firms expecting CPEC to have a positive impact.
</p>
			<div align="center"><img src="images/pie4.png" width="450px"></div>
			
			<p>The findings of the survey indicate that the country's retail and manufacturing sectors remained under stress during 2016, whereas the services sector excelled and performed the best of all the three sectors. The higher growth of the service sector has given a new dimension to economic growth in the country since it accounts for more than half of Pakistan's GDP.  The 	 showed that while the manufacturing and retail sectors registered declines in their export growth and employment, the service sector emerged as a key sector recording higher export growth and employment rates. There is an urgent need for the government to formulate a strategy to encourage firms to expand and diversify their products and exports markets which is critical to stabilize the current balance of payments crisis in Pakistan.</p>
			
			
							
