		<h3>Lahore School PhD Students Attend Summer Workshop at the Paris School of Economics</h3><br>

		<div align="center"><img src="images/sumwork1.png" ></div><br>

		<p>Farah Said, Maryiam Haroon, Zunia Saif Tirmazee, Rabia Arif and Nida Jamil,  PhD students from the Department of Economics of the Lahore School of Economics visited Paris in Summer 2018 from 18th June to 29th June to attend a two week Summer School Program organized by the Paris School of Economics. </p>
			
			
			
				<div align="center"><img src="images/sumwork2.png" ></div><br>

		<p>The students enrolled in courses on Microeconometrics, and Migration and International Trade (in week 1) and Experimental Economics - Empirical tools and methods to investigate economic behavior (in Week 2). The summer school was an excellent opportunity for the Lahore School PhD students to benefit from research -oriented teaching by leading experts in their fields.<br><br>
All of the programs offered an all-inclusive blend of lectures, workshops and seminars taught by PSE professors and other externally invited leading researchers. In the first week of the summer school, students got an opportunity to learn about the theoretical underpinning of impact evaluation. This was followed by daily workshops on the practical implementation of techniques learnt in class by replicating analyses of seminal academic articles. The week concluded with group presentations on a topic provided to the students, for which they had to formulate a study design and present preliminary results. This last activity was also an excellent exercise in networking and building links with graduate students in other universities.  Over the course of 5 days, instructors of the course Experimental Economics provided a very well rounded review of recent developments in Experimental Economics. The course was structured to provide students with a comprehensive review of how to design an experiment - from modeling to execution. The course also included a module on psychometric measures and non-parametric estimations - methods and techniques that are at the forefront of experimental design and analysis. 
</p>
			
			
			
				<div align="center"><img src="images/sumwork3.png" ></div><br>

		<p> 
The PSE Summer School was aimed at professionals, researchers, as well as graduate students in Economics and provided students an enriching experience to learn from top academics and professionals and network with students and faculty from their own fields of interest. The summer school was well attended by participants from top-ranked universities in Europe, USA and the UK. 
</p>
			
						

