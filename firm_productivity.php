		<a name="Exporters"></a>
		<br>
		
		<h3>Exporters in Pakistan and Firms Who Do Not Export: What's the Big Difference? </h3>
		<p><b>Researcher: </b>Theresa Chaudhry (Lahore School) and Muhammad Haseeb (University of Warrick)</p>
		
		<p>
		Developing countries have sought to promote exports as a growth strategy since the area source of both higher demand and of coveted foreign exchange. Proponents of trade liberalization argue that there is a positive relationship between openness of economy and productivity of its firms.<br><br>
However, the mechanism through which this works is by the introduction of imports which reduce the markups that the firms charge from consumers due to greater competition which in turn lowers the average cost of production due to the exit of low-productivity firms. In Pakistan, exporting firms use more imported inputs, are more productive and capital intensive and have higher growth potential reveals recent research conducted by the Lahore School of Economics.<br><br>
Researchers from the Lahore School of Economics showed that the average sales from exports, among exporters, was approximately 51% of total sales in Punjab and that many exporters in Pakistan don't have a significant domestic presence.<br><br>
Exporting firms have been found to have 29% higher revenues and 150% more output even after controlling for firm level characteristics such as geographical locations and ownership status. The labor productivity of exporting firms was found to be 2-3 times higher than that of non-exporters and exporters were doing better in terms of larger firm sizes, more employment opportunities, higher compensation and greater productivity.<br><br>
The researchers from the Lahore School of Economics found that apparel producers are doing well and exporting nearly 93% of their output. This sector employs on average 400 workers per organization and offers significantly higher compensation which makes the sector favorable for exports. Therefore, the government's recent emphasis on developing the readymade garments sector is well placed.<br><br>
The researchers also found generally that the capital-labor ratio among exporters was twice than that of non-exporters. Exploring some additional dimensions by which the exporting and non-exporting firms differ, it was found that the average exporting firms tended to use a larger share of imported material in their input mixes than non-exporting firms. Also, looking at the number of days a factory is in operation, it was found that exporters' factories operates, on average, more days than the non-exporting factories.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%20SP/09%20Chaudhry%20and%20Ashraf%20Final.pdf" target="_blank">The Lahore Journal of Economics 19: SE (September 2014): pp. 207-246.</a>


		</p>
		
