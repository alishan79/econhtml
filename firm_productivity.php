		<a name="Same"></a>
		<br>
		
		<h3>Same Jeans, Same Stitch? A Comparison of Denim Production across Three Factories in Punjab, Pakistan </h3>
		<p><b>Researcher: </b>Theresa Thompson Chaudhry (Lahore School) and Mahvish Faran (Lahore School, PhD candidate, Sheffield University) </p>
		
		<p>
		In this paper, we look at denim production in three different factories in Punjab, Pakistan. We map the manufacturing process for a standard pair of denim jeans produced for an international retailer. <br><br>
We asked three factories of different scales and proximities to the technological frontier to stitch, finish and wash an identical pair of jeans. These firms included a large-scale exporter with established links to a major multinational brand, a medium exporter with links to regional European labels and a small producer selling primarily to the domestic market. <br><br>
Timing the operations ourselves, we find that the stitching time of the large-scale exporter is about one-third less than that of the medium exporter and about half the stitching time of the small firm. Of the three firms, only the large exporter pays wages based strictly on standard minute value - the time expected to complete an operation. The two smaller firms pay piece rates that reflect the market rates paid for individual operations by firms throughout the sector. Even without increases in stitching efficiency, the two smaller firms could reduce their stitching costs by 30-50 percent if they were able to switch to paying wages based on stitching times. <br><br>
We also calculate the labor cost savings that the two smaller firms could accrue by adopting some of the more advanced equipment used by the large exporter, along with lower piece rates. Of these, the most reasonable investment would be in better loop-making machines; the cost of equipment could be recuperated by producing 325,000-500,000 garments, which for the medium firm is four to eight months' production at current levels. However, piece rates are entrenched and, if sticky, could reduce the incentives for firms to adopt labor-saving technologies.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2021/Issue%20SP/09%20Chaudhry%20and%20Faran.pdf" target="_blank">The Lahore Journal of Economics, Volume 21: SE (September 2016): pp. 211-236.</a>


		
