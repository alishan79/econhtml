		<a name="Incentives"></a>
		<br>
		
		<h3>Incentives and Productivity: Work Groups vs. Production Lines in Pakistani Electric Fan Production </h3>
		<p><b>Researcher: </b>Theresa Chaudhry (Lahore School), Chris Woodruff (Oxford University)</p>
		
		<p>
		The fan sector is an important source of employment in the region around Gujrat in the Punjab province of Pakistan. Moreover, it is representative of other light engineering sectors in Pakistan. Exporting has developed as an important market for fans produced in Pakistan, with the largest markets being in the Middle East. However, competition with Chinese firms is a threat to the Pakistani fan sector, and the largest firms are focused on increasing productivity in order to remain competitive. We will carry out this project with one of the largest producers of fans. Our work with the firm will focus on the organisation of production in the factory.<br><br>
The major objective is to understand the role of incentives in worker performance in manufacturing firms in Pakistan. The manufacturer(s) with whom we are working report issues with both workmanship issues and worker absences. They also report that workers resist changes in production techniques which are proposed by management.<br><br>
The project has been developed under the firm capabilities programme of the International Growth Centre (IGC) and is led by a team of researchers including Dr. Theresa Chaudhry and Zunia Tirmazee (Lahore School), Muhammad Haseeb (student, Warwick) and Dr. Christopher Woodruff (University of Warwick). They have attempted to test whether short-term financial incentives can be used to decrease worker absenteeism in one of Gujrat's leading fan factories. <br><br>
In early 2012, workers from eight work groups (teams) were surveyed on demographic characteristics, work habits, and team interactions. From February to June 2012, two incentive schemes, one based on individual attendance and one based on meeting a team attendance target, were rotated among the eight teams on a monthly basis. Results of the pilot indicated that the team attendance target appeared to have some effect on coordinating workers' absences as compared to the bonus based on individual attendance. <br><br>
In order to build stronger evidence, we completed a second round of the experiment conducted in 2013 of just the team attendance incentive against a control group on a sample of 14 new teams. For three months, 7 out of the 14 teams were offered the team attendance incentive. For most teams, the team attendance target was N-1 workers, for a team of N workers. Using data on individual and team attendance from 2011 - 2013, we find that offering the team attendance incentive helped to smooth attendance by coordinating workers' absences; there were three fewer days each month such that more than two workers were absent on average for the treatment group as compared to the control group, controlling for team and month fixed effects.

		</p>
		
