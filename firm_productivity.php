		<h3>The Textiles and Garments Sector: Moving Up the Value Chain </h3>
		<p><b>Researcher: </b>Naved Hamid (Lahore School), Ijaz Nabi (LUMS & IGC), and Rafia Zafar (Lahore School)</p>
		
		<p>
		The textiles and garments (T&G) sector accounts for almost 50% of Pakistan's exports and is the largest component of manufacturing. T&G sector, because of recent favorable developments for the industry in Pakistan and the expected future changes in the international trade structure for the sector, has the potential to play an important role in expanding Pakistan's exports. In addition, garments manufacturing is the least energy and capital intensive industrial activity and thus resonates with Pakistan's resource endowment to generate economic growth and employment. Garment manufacturers have tried to overcome the constraints arising from the energy shortages and adverse security and country risk perceptions by investing in power generation, upgrading IT, developing design and R&D capability. Punjab Government's focus on garments as a central plank of its industrial strategy has also helped. However, this paper argues that for the sector to fully realize its potential, government policies that shape the incentive structure faced by the industry need to be re-aligned In this regard, the most important is Pakistan's import policies and customs procedures that discourage the import of materials such as synthetic yarn and fabric, technical textiles and specialized trimmings and accessories needed by exporters to move up the value chain, and a significant bump up in the growth trajectory will only take place if import policy and custom procedures are substantially reformed. This paper focuses on the following themes: First, structural changes and trends in T&G exports; second, the associated constraints to growth of the garments sector; and third, to highlight some of the steps taken by the industry leaders in terms of policy reforms and by firms, particularly with regards to managing resources to enhance competitiveness.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%20SP/12%20Hamid,%20Nabi%20and%20Zafar%20Final.pdf" target="_blank">Lahore Journal of Economics, 2014, Vol. 19, issue Special Edition, 283-306.</a>


		</p>
		
