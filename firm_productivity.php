		<a name="Effect"></a>
		<br>
		
		<h3>The Effect of Trade Liberalization on Firm Entry and Exit in Punjab, Pakistan </h3>
		<p><b>Researcher: </b>Marjan Nasir (Lahore School)</p>
		
		<p>
		The industrial organization literature has traditionally emphasized the role of new firms as stimulators of economic development. The entry of new firms is associated with employment changes, product and technological innovation, and other structural changes in that particular industry (Roberts & Thompson, 2003). <br><br>Furthermore, as incumbent firms face growing competition from the new arrivals, their productivity is expected to improve. <br><br>
Researchers have examined the relationship between trade liberalization and firm turnover to determine the extent to which international markets and policies influence regional industries and their development. Exchange rate depreciation and tariff reductions can lead to the expansion of exports as the output of existing firms increases or new firms enter the industry (Bernard and Jensen, 2004; Gu, Sawchuk, and Whewell, 2003). Domestic firms can face increased competition from abroad when domestic tariff rates fall or the domestic currency appreciates (Baggs, Beaulieu, & Fung, 2009; Fung, 2008; Head and Ries, 1999; Klein, Schuh, and Triest, 2000). <br><br>
The primary aim of this study was to analyze the impact of exchange rate depreciation and tariff reductions on the output resulting from the entry of new firms. However, it is pertinent to note that entry into the exports sector requires that firms are at least as productive as the incumbent firms in order to survive both local and foreign competition, which could otherwise lead them to exit if they do not deliver efficiently. <br><br>
Over the last decade, Pakistan has experienced currency depreciations against the US dollar and the euro, together with an increase in export volume. In 2010, nearly 22 percent and 17 percent of its exports went to the European Union (EU) and the US, respectively; 48 other countries, each receiving a minimal share, accounted for the remaining volume. Accordingly, we look only at those sectors that export to the US and EU, while the depreciating rupee provides an opportunity to study its effects on firm turnover on the export industries in Punjab. <br><br>
At the same time, the tariff rates of member countries of the World Trade Organization (WTO) have decreased since 2000, in an effort to boost world exports. Pakistan has also experienced this decline together with an increase in exports to the US and EU. <br><br>
This study focuses on the impact of trade liberalization on firm entry and exit in Punjab's export manufacturing sector over the decade 2001-10. As far as the province's export industries are concerned, real exchange rate depreciation attracts new firms but also leads weaker firms to exit. A reduction in local or international tariffs, however, has no significant impact on firm entry or exit.<br><br>
This study has shown that a real exchange rate appreciation or depreciation is more likely to influence firm entry and exit than large tariff changes. Whether these changes in tariff rates take place in the domestic market or foreign market, they seem to have very little impact on firm turnover. Firm entry is lower and firm exit higher in industries comprising smaller or medium firms, suggesting that they are more competitive and may pose a threat to new as well as existing firms. Finally, the results highlight the significant role of high initial investment in deterring firm entry and exit.<br><br>
Published Article:  <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2019/Issue%201/Marjan%20Nasir.pdf" target="_blank">The Lahore Journal of Economics, Volume 19: 1 (Summer 2014): pp. 67-89.</a>

		</p>
		
