		<a name="Pakistan"></a>
		<br>
		
		<h3>Pakistan: A Case of Premature Deindustrialization?</h3>
		<p><b>Researcher: </b>Naved Hamid (Lahore School) and Maha Khan (Lahore School; Current PhD student, The University of York) </p>
		
		<p>
		Manufacturing growth has played a vital role in the development of advanced economies as well as in most developing economies and has also helped in closing the income gap between the two. Presently, the manufacturing industry in Pakistan, which was seen as the engine of growth, is in crisis.<br><br>
Growth in the large scale manufacturing (LSM) sector, which accounts for 80 percent of the manufacturing sector, has shrunk to its all-time lowest level of 1.1 percent per annum over the last seven years, with no signs of a pickup in the current period. One key reason is that over the last few decades, Pakistan's exports have not been significantly upgraded in terms of technology or sophistication. As a result of this stagnation in product sophistication, researchers from the Lahore School of Economics have found that Pakistan is on the brink of, if not already in the midst of, premature deindustrialization that blocks off the main avenue for the country to catch up with advanced economies.<br><br>
However, some positive recent developments like the improvement in the internal security conditions and the announcement of China-Pakistan Economic Corridor investment package of about US$ 46 billion could boost both domestic and foreign direct investment in the country. This raises the chance that Pakistan's manufacturing growth might be revived to once again achieve the levels reached in the previous high growth cycles.<br><br>
The researchers found that the main reason for the crises in the Pakistani manufacturing sector is that there has not been any pro-active industrial policy since the 1990s. Based on their analysis, the researchers suggest that there is an urgent need to develop and implement a comprehensive and pro-active industrial policy. This policy should consider the main constraints of the sector such as, curtailing unnecessary taxes on manufacturing, prioritizing manufacturing in the management of power and gas shortages, insulating the sector from the chronic exchange rate overvaluation, and finally helping the sector move up the sophistication curve by developing the required technical and skilled manpower. Only with a focused industrial policy can there be a revival in manufacturing growth in Pakistan.<br><br>
This research was funded by the Lahore School of Economics (LSE) and was published in the Lahore Journal of Economics. The researchers included Dr. Naved Hamid, Director, Centre for Research in Economics and Business, Lahore School of Economics and Ms. Maha Khan, Research and Teaching Fellow, Centre for Research in Economics and Business, Lahore School of Economics.<br><br>
Published Article: <a href="http://www.lahoreschoolofeconomics.edu.pk/EconomicsJournal/Journals/Volume%2020/Issue%20SP/06%20Hamid%20and%20Khan.pdf" target="_blank">The Lahore Journal of Economics, Volume 20: SE (September 2015): pp. 107-141.</a>


		</p>
		
